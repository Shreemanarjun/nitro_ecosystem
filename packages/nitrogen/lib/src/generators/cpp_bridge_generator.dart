import '../bridge_spec.dart';

class CppBridgeGenerator {
  static String generate(BridgeSpec spec) {
    final s = StringBuffer();
    s.writeln('// Generated by Nitrogen Modules. Do not edit.');
    s.writeln('#include "${spec.dartClassName.toLowerCase()}.bridge.g.h"');
    s.writeln('');

    // Preprocessor branch for Android JNI vs iOS Swift
    s.writeln('#ifdef __ANDROID__');
    s.writeln('#include <jni.h>');
    s.writeln('// --- Android JNI Implementation ---');
    s.writeln('extern "C" {');
    s.writeln('');
    s.writeln('// TODO: Global JNIEnv reference management for Android.');
    s.writeln('');

    for (final func in spec.functions) {
      final cType = _typeToC(func.returnType.name);
      final params = func.params.map((p) => '${_typeToC(p.type.name)} ${p.name}').join(', ');
      s.writeln('$cType ${func.cSymbol}(${params.isEmpty ? 'void' : params}) {');
      s.writeln('    // Call into JNI implementation: ${spec.dartClassName}JniBridge.${func.dartName}_jni(...)');
      if (func.returnType.name != 'void') {
        s.writeln('    return ${_defaultValue(func.returnType.name)}; // Placeholder');
      }
      s.writeln('}');
      s.writeln('');
    }

    // (Properties and Streams omitted for brevity in template)
    
    s.writeln('} // extern "C"');
    s.writeln('#elif __APPLE__');
    s.writeln('// --- iOS Swift C-Bridge Proxy Implementation ---');
    s.writeln('extern "C" {');
    s.writeln('');
    s.writeln('// Swift exposes these symbols via @objc or @_cdecl');
    
    for (final func in spec.functions) {
      final cType = _typeToC(func.returnType.name);
      final params = func.params.map((p) => '${_typeToC(p.type.name)} ${p.name}').join(', ');
      final callParams = func.params.map((p) => p.name).join(', ');
      
      // Declaring the Swift registry symbol so C++ can link to it
      s.writeln('extern $cType _call_${func.dartName}(${params.isEmpty ? 'void' : params});');
      
      // Implementing our pure C symbol from dart FFI which bounces to Swift
      s.writeln('$cType ${func.cSymbol}(${params.isEmpty ? 'void' : params}) {');
      if (func.returnType.name != 'void') {
        s.writeln('    return _call_${func.dartName}($callParams);');
      } else {
        s.writeln('    _call_${func.dartName}($callParams);');
      }
      s.writeln('}');
      s.writeln('');
    }
    s.writeln('} // extern "C"');
    s.writeln('#endif');

    return s.toString();
  }

  static String _typeToC(String dartType) {
    switch (dartType.replaceFirst('?', '')) {
      case 'int': return 'int64_t';
      case 'double': return 'double';
      case 'bool': return 'int8_t';
      case 'String': return 'const char*';
      case 'Uint8List': return 'uint8_t*';
      case 'void': return 'void';
      default: return 'void*';
    }
  }

  static String _defaultValue(String t) {
    switch (t.replaceFirst('?', '')) {
      case 'int': return '0';
      case 'double': return '0.0';
      case 'bool': return 'false';
      case 'String': return '""';
      default: return 'nullptr';
    }
  }
}
