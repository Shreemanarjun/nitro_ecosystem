import '../bridge_spec.dart';
import 'struct_generator.dart';
import 'enum_generator.dart';

class KotlinGenerator {
  static String generate(BridgeSpec spec) {
    final s = StringBuffer();
    s.writeln('// Generated by Nitrogen Modules. Do not edit.');
    s.writeln('package nitro.${spec.lib.replaceAll('-', '_')}_module');
    s.writeln();
    s.writeln('import androidx.annotation.Keep');
    s.writeln('import kotlinx.coroutines.flow.Flow');
    s.writeln('import kotlinx.coroutines.launch');
    s.writeln('import kotlinx.coroutines.CoroutineScope');
    s.writeln('import kotlinx.coroutines.Dispatchers');
    s.writeln('import kotlinx.coroutines.runBlocking');
    s.writeln();

    final kotlinEnums = EnumGenerator.generateKotlin(spec);
    if (kotlinEnums.isNotEmpty) s.write(kotlinEnums);

    final kotlinStructs = StructGenerator.generateKotlin(spec);
    if (kotlinStructs.isNotEmpty) s.write(kotlinStructs);

    // ── Interface ─────────────────────────────────────────────────────────
    s.writeln('/**');
    s.writeln(' * Contract for the [${spec.dartClassName}] module.');
    s.writeln(' * Implement this in your Kotlin source code.');
    s.writeln(' */');
    s.writeln('interface Hybrid${spec.dartClassName}Spec {');

    for (final func in spec.functions) {
      final retType = _toKotlinType(spec, func.returnType.name);
      final params = func.params
          .map((p) => '${p.name}: ${_toKotlinType(spec, p.type.name)}')
          .join(', ');
      final suspend = func.isAsync ? 'suspend ' : '';
      s.writeln('    ${suspend}fun ${func.dartName}($params): $retType');
    }

    for (final prop in spec.properties) {
      final kt = _toKotlinType(spec, prop.type.name);
      if (prop.hasSetter) {
        s.writeln('    var ${prop.dartName}: $kt');
      } else {
        s.writeln('    val ${prop.dartName}: $kt');
      }
    }

    for (final stream in spec.streams) {
      final itemType = _toKotlinType(spec, stream.itemType.name);
      s.writeln('    val ${stream.dartName}: Flow<$itemType>');
    }

    s.writeln('}');
    s.writeln();

    // ── JNI Bridge ────────────────────────────────────────────────────────
    s.writeln('@Keep');
    s.writeln('object ${spec.dartClassName}JniBridge {');
    s.writeln('    private var implementation: Hybrid${spec.dartClassName}Spec? = null');
    s.writeln();
    s.writeln('    @JvmStatic external fun initialize(bridgeClass: Class<*>)');
    s.writeln();
    s.writeln('    fun register(impl: Hybrid${spec.dartClassName}Spec) {');
    s.writeln('        implementation = impl');
    s.writeln('        initialize(this::class.java)');
    s.writeln('    }');
    s.writeln();

    for (final func in spec.functions) {
      final retType = _toKotlinType(spec, func.returnType.name);
      final paramsDecl = func.params.map((p) => '${p.name}: ${_toKotlinType(spec, p.type.name)}').join(', ');
      final callParams = func.params.map((p) => p.name).join(', ');
      
      final isUnit = (retType == 'Unit');
      
      s.writeln('    @JvmStatic fun ${func.dartName}_call($paramsDecl): $retType {');
      s.writeln('        val impl = implementation ?: throw IllegalStateException("${spec.dartClassName} not registered")');
      if (func.isAsync) {
        s.writeln('        return runBlocking {');
        s.writeln('            impl.${func.dartName}($callParams)');
        s.writeln('        }');
      } else {
        if (isUnit) {
          s.writeln('        impl.${func.dartName}($callParams)');
        } else {
          s.writeln('        return impl.${func.dartName}($callParams)');
        }
      }
      s.writeln('    }');
    }

    for (final prop in spec.properties) {
      final kt = _toKotlinType(spec, prop.type.name);
      if (prop.hasGetter) {
        s.writeln('    @JvmStatic fun ${prop.getSymbol}_call(): $kt {');
        s.writeln('        val impl = implementation ?: throw IllegalStateException("${spec.dartClassName} not registered")');
        s.writeln('        return impl.${prop.dartName}');
        s.writeln('    }');
      }
      if (prop.hasSetter) {
        s.writeln('    @JvmStatic fun ${prop.setSymbol}_call(value: $kt) {');
        s.writeln('        val impl = implementation ?: throw IllegalStateException("${spec.dartClassName} not registered")');
        s.writeln('        impl.${prop.dartName} = value');
        s.writeln('    }');
      }
    }

    s.writeln('    private val _streamJobs = mutableMapOf<Long, kotlinx.coroutines.Job>()');
    s.writeln();

    for (final stream in spec.streams) {
      s.writeln('    @JvmStatic external fun emit_${stream.dartName}(dartPort: Long, item: ${_toKotlinType(spec, stream.itemType.name)}): Unit');
      s.writeln('');
      s.writeln('    @JvmStatic fun ${stream.registerSymbol}_call(dartPort: Long) {');
      s.writeln('        val impl = implementation ?: return');
      s.writeln('        _streamJobs[dartPort] = CoroutineScope(Dispatchers.Default).launch {');
      s.writeln('            impl.${stream.dartName}.collect { item -> ');
      s.writeln('                emit_${stream.dartName}(dartPort, item)');
      s.writeln('            }');
      s.writeln('        }');
      s.writeln('    }');
      s.writeln('    @JvmStatic fun ${stream.releaseSymbol}_call(dartPort: Long) {');
      s.writeln('        _streamJobs.remove(dartPort)?.cancel()');
      s.writeln('    }');
    }

    s.writeln('}');
    return s.toString();
  }

  static String _toKotlinType(BridgeSpec spec, String t) {
    switch (t.replaceFirst('?', '')) {
      case 'int': return 'Long';
      case 'double': return 'Double';
      case 'bool': return 'Boolean';
      case 'String': return 'String';
      case 'void': return 'Unit';
      case 'Uint8List': return 'ByteArray';
      default:
        if (spec.structs.any((st) => st.name == t.replaceFirst('?', ''))) {
          return t;
        }
        return 'Any?';
    }
  }

  static String _ktDefaultValue(String t) {
    switch (t.replaceFirst('?', '')) {
      case 'int': return '0L';
      case 'double': return '0.0';
      case 'bool': return 'false';
      case 'String': return '""';
      default: return 'null';
    }
  }
}
