import '../bridge_spec.dart';

class CMakeGenerator {
  static String generate(BridgeSpec spec) {
    final s = StringBuffer();
    s.writeln('# Generated by Nitrogen Modules. Do not edit.');
    s.writeln('cmake_minimum_required(VERSION 3.10)');
    s.writeln();

    s.writeln('set(NITRO_MODULE_NAME ${spec.lib})');
    s.writeln('add_library(\${\${NITRO_MODULE_NAME}} SHARED');
    s.writeln('    \${\${NITRO_MODULE_NAME}}_bridge.g.h');
    s.writeln('    # Add your source files here');
    s.writeln(')');
    s.writeln();

    s.writeln('target_link_libraries(\${\${NITRO_MODULE_NAME}}');
    s.writeln('    android');
    s.writeln('    log');
    s.writeln(')');

    return s.toString();
  }
}
