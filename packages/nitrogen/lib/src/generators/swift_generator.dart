import '../bridge_spec.dart';
import 'struct_generator.dart';
import 'enum_generator.dart';

class SwiftGenerator {
  static String generate(BridgeSpec spec) {
    final s = StringBuffer();
    s.writeln('// Generated by Nitrogen Modules. Do not edit.');
    s.writeln('import Foundation');
    s.writeln('import Combine');
    s.writeln();

    final swiftEnums = EnumGenerator.generateSwift(spec);
    if (swiftEnums.isNotEmpty) s.write(swiftEnums);

    final swiftStructs = StructGenerator.generateSwift(spec);
    if (swiftStructs.isNotEmpty) s.write(swiftStructs);

    // ── Protocol ──────────────────────────────────────────────────────────
    s.writeln('/**');
    s.writeln(' * Protocol for the ${spec.dartClassName} module.');
    s.writeln(' * Conform to this in your Swift source code.');
    s.writeln(' */');
    s.writeln('public protocol Hybrid${spec.dartClassName}Protocol: AnyObject {');

    for (final func in spec.functions) {
      final retType = _toSwiftType(func.returnType.name);
      final params = func.params
          .map((p) => '${p.name}: ${_toSwiftType(p.type.name)}')
          .join(', ');
      if (func.isAsync) {
        s.writeln('    func ${func.dartName}($params) async throws -> $retType');
      } else {
        s.writeln('    func ${func.dartName}($params) -> $retType');
      }
    }

    for (final prop in spec.properties) {
      final swiftType = _toSwiftType(prop.type.name);
      if (prop.hasSetter) {
        s.writeln('    var ${prop.dartName}: $swiftType { get set }');
      } else {
        s.writeln('    var ${prop.dartName}: $swiftType { get }');
      }
    }

    for (final stream in spec.streams) {
      final itemType = _toSwiftType(stream.itemType.name);
      s.writeln('    var ${stream.dartName}: AnyPublisher<$itemType, Never> { get }');
    }

    s.writeln('}');
    s.writeln();

    // ── Registry + C-bridge call stubs ────────────────────────────────────
    s.writeln('@objc');
    s.writeln('public class ${spec.dartClassName}Registry: NSObject {');
    s.writeln('    private static var impl: Hybrid${spec.dartClassName}Protocol?');
    s.writeln();
    s.writeln('    @objc public static func register(_ impl: Hybrid${spec.dartClassName}Protocol) {');
    s.writeln('        self.impl = impl');
    s.writeln('    }');
    s.writeln();
    s.writeln('    // MARK: - C bridge stubs (called by the generated .c shim)');

    for (final func in spec.functions) {
      final retType = _toSwiftType(func.returnType.name);
      final params = func.params
          .map((p) => '_ ${p.name}: ${_toSwiftType(p.type.name)}')
          .join(', ');
      final callArgs = func.params.map((p) => '${p.name}: ${p.name}').join(', ');
      s.writeln('    @objc public static func _call_${func.dartName}($params) -> $retType {');
      if (func.returnType.name == 'void') {
        s.writeln('        impl?.${func.dartName}($callArgs)');
        s.writeln('        return ${_defaultValue(func.returnType.name)}');
      } else {
        s.writeln('        return impl?.${func.dartName}($callArgs) ?? ${_defaultValue(func.returnType.name)}');
      }
      s.writeln('    }');
    }

    for (final prop in spec.properties) {
      final swiftType = _toSwiftType(prop.type.name);
      if (prop.hasGetter) {
        s.writeln('    @objc public static func _get_${prop.dartName}() -> $swiftType {');
        s.writeln('        return impl?.${prop.dartName} ?? ${_defaultValue(prop.type.name)}');
        s.writeln('    }');
      }
      if (prop.hasSetter) {
        s.writeln('    @objc public static func _set_${prop.dartName}(_ value: $swiftType) {');
        s.writeln('        impl?.${prop.dartName} = value');
        s.writeln('    }');
      }
    }

    for (final stream in spec.streams) {
      final cType = _toSwiftCType(stream.itemType.name);
      s.writeln('    // Stream: ${stream.dartName} — register with C callback');
      s.writeln('    private static var _${stream.dartName}Cancellable: AnyCancellable?');
      s.writeln('    @objc public static func _register_${stream.dartName}_stream(_ dartPort: Int64, _ emitCb: @escaping @convention(c) (Int64, $cType) -> Void) {');
      s.writeln('        _${stream.dartName}Cancellable = impl?.${stream.dartName}.sink { item in');
      s.writeln('            emitCb(dartPort, item)');
      s.writeln('        }');
      s.writeln('    }');
      s.writeln('    @objc public static func _release_${stream.dartName}_stream() {');
      s.writeln('        _${stream.dartName}Cancellable?.cancel()');
      s.writeln('        _${stream.dartName}Cancellable = nil');
      s.writeln('    }');
    }

    s.writeln('}');
    return s.toString();
  }

  static String _toSwiftType(String t) {
    switch (t.replaceFirst('?', '')) {
      case 'int': return 'Int64';
      case 'double': return 'Double';
      case 'bool': return 'Bool';
      case 'String': return 'String';
      case 'void': return 'Void';
      case 'Uint8List': return 'Data';
      default: return 'Any?';
    }
  }

  static String _toSwiftCType(String t) {
    switch (t.replaceFirst('?', '')) {
      case 'int': return 'Int64';
      case 'double': return 'Double';
      case 'bool': return 'Bool';
      case 'String': return 'UnsafeMutablePointer<Int8>?';
      case 'void': return 'Void';
      case 'Uint8List': return 'UnsafeMutablePointer<UInt8>?';
      default: return 'Any?';
    }
  }

  static String _defaultValue(String t) {
    switch (t.replaceFirst('?', '')) {
      case 'int': return '0';
      case 'double': return '0.0';
      case 'bool': return 'false';
      case 'String': return '""';
      default: return '()';
    }
  }
}
