// ── Package.swift template ────────────────────────────────────────────────────

/// Generates the SPM [Package.swift] for [pluginName].
///
/// [platformSpec] is e.g. `'iOS(.v13)'` or `'macOS(.v10_15)'`.
/// [isMacos] switches the Xcode flag path between iOS and macOS conventions.
String packageSwiftTemplate(
  String pluginName,
  String className,
  String platformSpec, {
  bool isMacos = false,
}) {
  return '''
// swift-tools-version: 5.9
import PackageDescription

let package = Package(
    name: "$pluginName",
    platforms: [.$platformSpec],
    products: [
        .library(name: "${pluginName.replaceAll('_', '-')}", targets: ["$pluginName"]),
    ],
    dependencies: [
        .package(name: "FlutterFramework", path: "../FlutterFramework"),
    ],
    targets: [
        // C/C++ bridge — SPM requires Swift and C++ in separate targets.
        // nitro headers (nitro.h, dart_api_dl.h …) are copied into include/
        // by `nitrogen link`, so no extra header search path is needed.
        .target(
            name: "${className}Cpp",
            path: "Sources/${className}Cpp",
            publicHeadersPath: "include",
            cxxSettings: [
                .headerSearchPath("include"),
                .unsafeFlags(["-std=c++17"])
            ]
        ),
        // Swift implementation + generated bridge.
        .target(
            name: "$pluginName",
            dependencies: [
                "${className}Cpp",
                .product(name: "FlutterFramework", package: "FlutterFramework"),
            ],
            path: "Sources/$className"
        ),
    ]
)
''';
}

// ── Android templates ─────────────────────────────────────────────────────────

/// Generates `android/build.gradle` using the modern `plugins {}` DSL.
///
/// Uses [plugins {}] at the very top as required by Gradle 7+ — no legacy
/// `buildscript {}` or `apply plugin` blocks which break AGP 8/9+.
String androidBuildGradleTemplate(String org, String pluginName) =>
    '''
plugins {
    id "com.android.library"
    id "org.jetbrains.kotlin.android"
}

group = "$org.$pluginName"
version = "1.0"

android {
    namespace = "$org.$pluginName"
    compileSdk = 36
    ndkVersion = "27.0.12077973"

    externalNativeBuild {
        cmake { path = "../src/CMakeLists.txt" }
    }

    compileOptions {
        sourceCompatibility = JavaVersion.VERSION_17
        targetCompatibility = JavaVersion.VERSION_17
    }

    kotlinOptions { jvmTarget = "17" }

    defaultConfig { minSdk = 24 }

    sourceSets {
        main {
            // Do NOT add to java.srcDirs — in AGP 8.x that routes .kt files
            // through the Java compiler and causes "Unresolved reference" errors.
            kotlin.srcDirs += "\${project.projectDir}/../lib/src/generated/kotlin"
        }
    }
}

dependencies {
    implementation "org.jetbrains.kotlinx:kotlinx-coroutines-android:1.7.3"
    implementation "org.jetbrains.kotlinx:kotlinx-coroutines-core:1.7.3"
}
''';

String androidPluginKtTemplate(
  String org,
  String pluginName,
  String className,
  String moduleName,
) =>
    '''
package $org.$pluginName

import io.flutter.embedding.engine.plugins.FlutterPlugin
import io.flutter.embedding.engine.plugins.activity.ActivityAware
import io.flutter.embedding.engine.plugins.activity.ActivityPluginBinding
import nitro.$moduleName.${className}JniBridge

class ${className}Plugin : FlutterPlugin, ActivityAware {

    companion object {
        init { System.loadLibrary("$pluginName") }
    }

    override fun onAttachedToEngine(binding: FlutterPlugin.FlutterPluginBinding) {
        ${className}JniBridge.register(${className}Impl(), binding.applicationContext)
    }

    override fun onDetachedFromEngine(binding: FlutterPlugin.FlutterPluginBinding) {
        ${className}JniBridge.onDetached()
    }

    override fun onAttachedToActivity(binding: ActivityPluginBinding) {
        ${className}JniBridge.onActivityAttached(binding.activity)
    }

    override fun onDetachedFromActivityForConfigChanges() {
        ${className}JniBridge.onActivityDetached()
    }

    override fun onReattachedToActivityForConfigChanges(binding: ActivityPluginBinding) {
        ${className}JniBridge.onActivityAttached(binding.activity)
    }

    override fun onDetachedFromActivity() {
        ${className}JniBridge.onActivityDetached()
    }
}''';

