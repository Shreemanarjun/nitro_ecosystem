// Small #include forwarder file templates used by `nitrogen init` and
// `nitrogen link` to proxy source files into platform-specific build targets.
library;

// ── C++ source forwarders ──────────────────────────────────────────────────────

/// A CocoaPods-style `Classes/plugin.cpp` forwarder to `src/plugin.cpp`.
/// The relative path `../../src/` is correct from `{platform}/Classes/`.
String classesCppForwarder(String pluginName) =>
    '#include "../../src/$pluginName.cpp"\n';

/// A CocoaPods-style `Classes/plugin.c` forwarder to `src/plugin.c`.
String classesCForwarder(String pluginName) =>
    '#include "../../src/$pluginName.c"\n';

/// A `nitrogen link` managed C++ source forwarder — used in both CocoaPods
/// `Classes/` and SPM `Sources/<Plugin>Cpp/` targets.
///
/// [relativePath] is the relative path from the forwarder to the source file.
String managedCppForwarder(String relativePath) =>
    '// Generated by nitrogen link — do not edit.\n'
    '#include "$relativePath"\n';

/// A `nitrogen init` C++ source forwarder in an SPM Cpp target.
/// Points from `<platform>/<plugin>/Sources/<Plugin>Cpp/` up to `Classes/`.
String spmCppClassesForwarder(String pluginName) =>
    '// Generated by nitrogen init — do not edit.\n'
    '#include "../../../Classes/$pluginName.cpp"\n';

// ── Obj-C++ bridge forwarders (.mm) ───────────────────────────────────────────

/// A `nitrogen link` managed `.mm` forwarder that wraps the generated C++
/// bridge so SPM compiles it as Obj-C++ and links the C ABI symbols.
///
/// [relativePath] is the relative path from the forwarder to `.bridge.g.cpp`.
String managedBridgeMmForwarder(String relativePath) =>
    '// Generated by nitrogen link — do not edit.\n'
    '#import <Foundation/Foundation.h>\n'
    '#include "$relativePath"\n';

/// A `nitrogen init` `.mm` forwarder for the SPM C++ target.
/// The 4-level relative path is fixed for the nested SPM layout:
///   `{platform}/{plugin}/Sources/{Plugin}Cpp/` → `lib/src/generated/cpp/`
String spmBridgeMmForwarder(String pluginName) =>
    '// Generated by nitrogen init — do not edit.\n'
    '// Updated with resolved paths by nitrogen link.\n'
    '#import <Foundation/Foundation.h>\n'
    '#include "../../../../lib/src/generated/cpp/$pluginName.bridge.g.cpp"\n';

// ── dart_api_dl.c forwarders ───────────────────────────────────────────────────

/// `dart_api_dl.c` forwarder for CocoaPods `Classes/` targets.
/// Must be a `.c` file (not `.cpp`) so the void*/function-pointer casts
/// inside `dart_api_dl.c` are accepted without C++ complaints.
const String classesDartApiDlForwarder =
    '#include "../../src/dart_api_dl.c"\n';

/// `dart_api_dl.c` forwarder written into `src/` during `nitrogen init`.
/// Points to the monorepo placeholder path; `nitrogen link` rewrites it with
/// the resolved pub-cache path after `flutter pub get`.
const String srcDartApiDlInitForwarder =
    '// Generated by nitrogen — do not edit.\n'
    '// Run `nitrogen link` to update this path after `flutter pub get`.\n'
    '#include "../../packages/nitro/src/native/dart_api_dl.c"\n';

/// `dart_api_dl.c` forwarder for CocoaPods ios/macos `Classes/` targets with
/// the comment explaining why it must remain `.c`.
const String classesIosDartApiDlForwarder =
    '// Forwarder — compiled by CocoaPods/SPM so the Dart DL API is\n'
    '// available in the dylib. Kept as .c so it compiles as C, not C++.\n'
    '#include "../../src/dart_api_dl.c"\n';

const String classesMacosDartApiDlForwarder =
    '// Forwarder — compiled as C so it can be used in the dylib.\n'
    '#include "../../src/dart_api_dl.c"\n';
