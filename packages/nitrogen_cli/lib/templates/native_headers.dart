// Canonical `nitro.h` content. Written unconditionally to every location that
// needs it so all translation units share the same typedef with the correct
// `NITRO_ERROR_DEFINED` guard (prevents "Typedef redefinition" errors).
const String nitroHContent = r'''#pragma once

#include <stdint.h>
#include <stdbool.h>

#if _WIN32
#define NITRO_EXPORT __declspec(dllexport)
#else
#define NITRO_EXPORT __attribute__((visibility("default"))) __attribute__((used))
#endif

#ifdef __cplusplus
extern "C" {
#endif

#ifndef NITRO_ERROR_DEFINED
#define NITRO_ERROR_DEFINED
typedef struct {
  int8_t hasError;
  const char* name;
  const char* message;
  const char* code;
  const char* stackTrace;
} NitroError;
#endif

#ifdef __cplusplus
}
#endif
''';

/// Returns the content of a `dart_api_dl.c` forwarder that includes the real
/// file from the resolved nitro native path.
///
/// The forwarder is compiled as plain C (not C++) because the void*/function-pointer
/// casts inside `dart_api_dl.c` would be rejected by a C++ compiler.
String dartApiDlForwarderContent(String nitroNativePath) {
  final includePath = nitroNativePath
      .replaceAll(r'\', '/')
      .trimRight();
  return '// Generated by nitrogen link — do not edit.\n'
      '// Compiled as C (not C++) so the void*/function-pointer casts\n'
      '// in dart_api_dl.c are accepted by the compiler.\n'
      '#include "$includePath/dart_api_dl.c"\n';
}
