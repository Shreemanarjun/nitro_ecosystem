// ── Package-level helpers (also used in tests) ─────────────────────────────

/// Resolves the absolute path to the installed `nitro` package's `src/native`
/// directory by reading `.dart_tool/package_config.json` inside [pluginDir].
///
/// Falls back to the monorepo-relative default
/// (`<pluginDir>/../packages/nitro/src/native`) when the file is absent or
/// the `nitro` entry cannot be parsed.
String resolveNitroNativePath(String pluginDir) {
  final configFile =
      File(p.join(pluginDir, '.dart_tool', 'package_config.json'));
  if (configFile.existsSync()) {
    try {
      final config =
          jsonDecode(configFile.readAsStringSync()) as Map<String, dynamic>;
      final packages = (config['packages'] as List<dynamic>?) ?? [];
      for (final pkg in packages) {
        final pkgMap = pkg as Map<String, dynamic>;
        if (pkgMap['name'] == 'nitro') {
          final rootUri = pkgMap['rootUri'] as String;
          final uri = Uri.parse(rootUri);
          if (uri.scheme == 'file') {
            // Absolute file:// URI — pub cache installation.
            return p.join(uri.toFilePath(), 'src', 'native');
          } else {
            // Relative URI — relative to the .dart_tool/ directory.
            final dartToolDir = p.join(pluginDir, '.dart_tool');
            final resolved = p.normalize(p.join(dartToolDir, rootUri));
            return p.join(resolved, 'src', 'native');
          }
        }
      }
    } catch (_) {}
  }
  // Fallback: monorepo layout.
  return p.normalize(p
      .absolute(p.join(pluginDir, '..', 'packages', 'nitro', 'src', 'native')));
}

/// Returns `true` when [cmakeNitroNative] (which may contain
/// `${CMAKE_CURRENT_SOURCE_DIR}`) resolves to a directory that contains
/// `dart_api_dl.h`.
///
/// [srcDir] is the absolute path to the plugin's `src/` directory, used to
/// expand `${CMAKE_CURRENT_SOURCE_DIR}`.
bool nitroNativePathExists(String cmakeNitroNative, String srcDir) {
  final resolved =
      cmakeNitroNative.replaceAll(r'${CMAKE_CURRENT_SOURCE_DIR}', srcDir);
  return File(p.join(resolved, 'dart_api_dl.h')).existsSync();
}

/// Returns the content for a `src/dart_api_dl.c` forwarder file that includes
/// the real `dart_api_dl.c` from [nitroNativePath].
String dartApiDlForwarderContent(String nitroNativePath) {
  final includePath =
      p.join(nitroNativePath, 'dart_api_dl.c').replaceAll(r'\', '/');
  return '// Generated by nitrogen link — do not edit.\n'
      '// Compiled as C (not C++) so the void*/function-pointer casts\n'
      '// in dart_api_dl.c are accepted by the compiler.\n'
      '#include "$includePath"\n';
}

/// Extracts the `lib:` value from a `@NitroModule(...)` annotation in [specFile].
/// Returns null when the annotation has no explicit `lib` parameter.
String? extractLibNameFromSpec(File specFile) {
  final content = specFile.readAsStringSync();
  final match = RegExp(r'''@NitroModule\s*\([^)]*lib\s*:\s*['"]([^'"]+)['"]''')
      .firstMatch(content);
  return match?.group(1);
}

/// Discovers all module lib names by scanning `lib/` for `*.native.dart` files.
/// Falls back to [pluginName] when no specs are found.
List<String> discoverModuleLibs(String pluginName) {
  final libDir = Directory('lib');
  if (!libDir.existsSync()) return [pluginName];
  final specs = libDir
      .listSync(recursive: true)
      .whereType<File>()
      .where((f) => f.path.endsWith('.native.dart'))
      .toList();
  if (specs.isEmpty) return [pluginName];
  final libs = <String>[];
  for (final spec in specs) {
    final stem =
        p.basename(spec.path).replaceAll(RegExp(r'\.native\.dart$'), '');
    final libName = extractLibNameFromSpec(spec) ?? stem.replaceAll('-', '_');
    if (!libs.contains(libName)) libs.add(libName);
  }
  return libs.isEmpty ? [pluginName] : libs;
}

// ── Progress model ──────────────────────────────

enum LinkStepState { pending, running, done, failed, skipped }

class LinkStep {
  final String label;
  LinkStepState state;
  String? detail;

  LinkStep(this.label) : state = LinkStepState.pending;
}

class LinkStepRow extends StatelessComponent {
  const LinkStepRow(this.step, {super.key});
  final LinkStep step;

  @override
  Component build(BuildContext context) {
    final String icon;
    final Color color;
    switch (step.state) {
      case LinkStepState.pending:
        icon = '○';
        color = Colors.gray;
      case LinkStepState.running:
        icon = '◉';
        color = Colors.cyan;
      case LinkStepState.done:
        icon = '✔';
        color = Colors.green;
      case LinkStepState.failed:
        icon = '✘';
        color = Colors.red;
      case LinkStepState.skipped:
        icon = '–';
        color = Colors.gray;
    }

    return Padding(
      padding: const EdgeInsets.only(left: 2),
      child: Column(
        children: [
          Row(
            children: [
              Text(icon,
                  style: TextStyle(color: color, fontWeight: FontWeight.bold)),
              const Text(' '),
              Expanded(
                child: Text(
                  step.label,
                  style: TextStyle(
                    color: step.state == LinkStepState.running
                        ? Colors.cyan
                        : null,
                    fontWeight: step.state == LinkStepState.running
                        ? FontWeight.bold
                        : null,
                  ),
                ),
              ),
            ],
          ),
          if (step.detail != null)
            Padding(
              padding: const EdgeInsets.only(left: 4),
              child: Text(
                step.detail!,
                style: const TextStyle(
                    color: Colors.gray, fontWeight: FontWeight.dim),
              ),
            ),
        ],
      ),
    );
  }
}

