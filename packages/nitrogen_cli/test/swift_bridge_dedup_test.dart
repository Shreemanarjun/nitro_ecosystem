// Tests for multi-spec Swift bridge deduplication.
//
// When multiple Nitro specs share one Flutter plugin, each generated
// `*.bridge.g.swift` independently declares shared types (NitroEncodable,
// NitroNullableInt, NitroRecordWriter, etc.). Compiling all three files
// into the same Swift module causes "invalid redeclaration" errors.
//
// The fix: `stripSharedSwiftPreamble` removes the shared public-type block
// from every bridge file except the first (alphabetically). `_syncSwiftBridges`
// (tested via `_runSync`) applies this when copying files to ios/Classes/,
// macos/Classes/, and SPM Sources/<ClassName>/ directories.
import 'dart:io';

import 'package:nitrogen_cli/utils.dart' show stripSharedSwiftPreamble;
import 'package:path/path.dart' as p;
import 'package:test/test.dart';

// ── Fixtures ──────────────────────────────────────────────────────────────────

/// Builds a realistic full bridge file with shared preamble + spec-specific content.
String _fullBridge(String specName) {
  final pascal = _toPascal(specName);
  return '''
// Generated by Nitrogen Modules. Do not edit.
// nitro_generator: 0.5.0
// Generated from: $specName.native.dart
import Foundation
import Combine

@inline(__always)
private func _nitroStringFromCString(_ ptr: UnsafePointer<CChar>?) -> String {
    guard let ptr = ptr else { return "" }
    return ""
}

public protocol NitroEncodable {
    func toNative() -> UnsafeMutablePointer<UInt8>?
}

public struct NitroNullableInt: NitroEncodable {
    public var hasValue: Bool
    public var value: Int64
    public func toNative() -> UnsafeMutablePointer<UInt8>? { nil }
}

public class NitroRecordWriter {
    public func writeInt(_ v: Int64) {}
}

public class NitroRecordReader {
    public func readInt() -> Int64 { 0 }
}

/**
 * Protocol for the $pascal module.
 * Conform to this in your Swift source code.
 */
public protocol Hybrid${pascal}Protocol: AnyObject {
    func doSomething() -> Int64
}

public class ${pascal}Registry {
    public static func register(_ impl: Hybrid${pascal}Protocol) {}
}
''';
}

// ── Unit tests: stripSharedSwiftPreamble ──────────────────────────────────────

void main() {
  group('stripSharedSwiftPreamble', () {
    test('removes NitroEncodable and NitroNullableInt', () {
      final result = stripSharedSwiftPreamble(_fullBridge('nitro_ui'));

      expect(result, isNot(contains('public protocol NitroEncodable')));
      expect(result, isNot(contains('public struct NitroNullableInt')));
    });

    test('removes NitroRecordWriter and NitroRecordReader', () {
      final result = stripSharedSwiftPreamble(_fullBridge('nitro_ui'));

      expect(result, isNot(contains('public class NitroRecordWriter')));
      expect(result, isNot(contains('public class NitroRecordReader')));
    });

    test('keeps file header comments', () {
      final result = stripSharedSwiftPreamble(_fullBridge('nitro_ui'));

      expect(result, startsWith('// Generated by Nitrogen Modules. Do not edit.'));
      expect(result, contains('Generated from: nitro_ui.native.dart'));
    });

    test('keeps import statements', () {
      final result = stripSharedSwiftPreamble(_fullBridge('nitro_ui'));

      expect(result, contains('import Foundation'));
      expect(result, contains('import Combine'));
    });

    test('keeps file-private string helpers', () {
      final result = stripSharedSwiftPreamble(_fullBridge('nitro_ui'));

      expect(result, contains('private func _nitroStringFromCString'));
    });

    test('keeps spec-specific Hybrid protocol', () {
      final result = stripSharedSwiftPreamble(_fullBridge('nitro_ui'));

      expect(result, contains('public protocol HybridNitroUiProtocol'));
      expect(result, contains('func doSomething()'));
    });

    test('keeps spec-specific Registry class', () {
      final result = stripSharedSwiftPreamble(_fullBridge('nitro_ui'));

      expect(result, contains('class NitroUiRegistry'));
    });

    test('keeps the /** doc-comment preceding the protocol', () {
      final result = stripSharedSwiftPreamble(_fullBridge('nitro_ui'));

      expect(result, contains('/**'));
      expect(result, contains('Protocol for the NitroUi module.'));
    });

    test('is idempotent: stripping an already-stripped file returns same content', () {
      final firstPass = stripSharedSwiftPreamble(_fullBridge('nitro_ui'));
      final secondPass = stripSharedSwiftPreamble(firstPass);

      expect(firstPass, equals(secondPass));
    });

    test('passes through a file with no NitroEncodable block unchanged', () {
      const noSharedBlock = '''
// Generated by Nitrogen Modules. Do not edit.
import Foundation

/**
 * Protocol for the NitroView module.
 */
public protocol HybridNitroViewProtocol: AnyObject {
    func createView() -> Int64
}
''';
      final result = stripSharedSwiftPreamble(noSharedBlock);

      expect(result, equals(noSharedBlock));
    });

    test('handles empty string without throwing', () {
      expect(() => stripSharedSwiftPreamble(''), returnsNormally);
      expect(stripSharedSwiftPreamble(''), isEmpty);
    });

    test('strips shared block from all three real-world spec names', () {
      for (final spec in ['nitro_view', 'nitro_ui', 'nitro_system']) {
        final result = stripSharedSwiftPreamble(_fullBridge(spec));
        expect(result, isNot(contains('NitroEncodable')), reason: '$spec should have NitroEncodable stripped');
        expect(result, contains('public protocol Hybrid${_toPascal(spec)}Protocol'), reason: '$spec protocol should be retained');
      }
    });
  });

  // ── Integration: multi-spec file sync behaviour ───────────────────────────

  group('multi-spec bridge sync to Classes/ and SPM Sources/', () {
    late Directory tempDir;

    setUp(() {
      tempDir = Directory.systemTemp.createTempSync('nitrogen_swift_dedup_');
    });

    tearDown(() {
      if (tempDir.existsSync()) tempDir.deleteSync(recursive: true);
    });

    test('single-spec: bridge file copied unchanged with full preamble', () {
      _scaffoldPlugin(tempDir, specs: ['nitro_view']);
      _runSync(tempDir.path);

      final dest = File(p.join(tempDir.path, 'ios', 'Classes', 'nitro_view.bridge.g.swift'));
      expect(dest.existsSync(), isTrue);
      expect(dest.readAsStringSync(), contains('public protocol NitroEncodable'),
          reason: 'Single-spec plugin: first (and only) file must have full preamble');
    });

    test('multi-spec: first bridge file (sorted alphabetically) retains shared preamble', () {
      _scaffoldPlugin(tempDir, specs: ['nitro_system', 'nitro_ui', 'nitro_view']);
      _runSync(tempDir.path);

      // 'nitro_system' < 'nitro_ui' < 'nitro_view' alphabetically.
      final first = File(p.join(tempDir.path, 'ios', 'Classes', 'nitro_system.bridge.g.swift'));
      expect(first.readAsStringSync(), contains('public protocol NitroEncodable'),
          reason: 'First bridge file must carry the shared types for the module');
    });

    test('multi-spec: 2nd and 3rd bridge files have shared preamble stripped', () {
      _scaffoldPlugin(tempDir, specs: ['nitro_system', 'nitro_ui', 'nitro_view']);
      _runSync(tempDir.path);

      for (final spec in ['nitro_ui', 'nitro_view']) {
        final content = File(p.join(tempDir.path, 'ios', 'Classes', '$spec.bridge.g.swift'))
            .readAsStringSync();
        expect(content, isNot(contains('public protocol NitroEncodable')),
            reason: '$spec must not redeclare NitroEncodable');
        expect(content, isNot(contains('public class NitroRecordWriter')),
            reason: '$spec must not redeclare NitroRecordWriter');
      }
    });

    test('multi-spec: each stripped file still contains its Hybrid protocol', () {
      _scaffoldPlugin(tempDir, specs: ['nitro_system', 'nitro_ui', 'nitro_view']);
      _runSync(tempDir.path);

      for (final spec in ['nitro_ui', 'nitro_view']) {
        final content = File(p.join(tempDir.path, 'ios', 'Classes', '$spec.bridge.g.swift'))
            .readAsStringSync();
        expect(content, contains('public protocol Hybrid${_toPascal(spec)}Protocol'),
            reason: '$spec: spec-specific protocol must survive stripping');
      }
    });

    test('multi-spec: macos/Classes receives identical treatment to ios/Classes', () {
      _scaffoldPlugin(tempDir, specs: ['nitro_system', 'nitro_ui', 'nitro_view']);
      _runSync(tempDir.path);

      // First file in macos is full.
      expect(
        File(p.join(tempDir.path, 'macos', 'Classes', 'nitro_system.bridge.g.swift'))
            .readAsStringSync(),
        contains('public protocol NitroEncodable'),
      );
      // 2nd+ files in macos are stripped.
      for (final spec in ['nitro_ui', 'nitro_view']) {
        expect(
          File(p.join(tempDir.path, 'macos', 'Classes', '$spec.bridge.g.swift'))
              .readAsStringSync(),
          isNot(contains('public protocol NitroEncodable')),
        );
      }
    });

    test('multi-spec: SPM nested Sources/<ClassName>/ receives stripped 2nd+ files', () {
      _scaffoldPlugin(tempDir, specs: ['nitro_system', 'nitro_ui', 'nitro_view']);

      // Create SPM nested layout: ios/my_plugin/Sources/MyPlugin/
      final spmSwiftDir = Directory(p.join(tempDir.path, 'ios', 'my_plugin', 'Sources', 'MyPlugin'))
        ..createSync(recursive: true);

      _runSync(tempDir.path);

      // First file is full in SPM path.
      expect(
        File(p.join(spmSwiftDir.path, 'nitro_system.bridge.g.swift')).readAsStringSync(),
        contains('public protocol NitroEncodable'),
        reason: 'First bridge in SPM path must have shared types',
      );
      // 2nd and 3rd are stripped.
      for (final spec in ['nitro_ui', 'nitro_view']) {
        final f = File(p.join(spmSwiftDir.path, '$spec.bridge.g.swift'));
        expect(f.existsSync(), isTrue, reason: '$spec not found in SPM Sources/');
        expect(f.readAsStringSync(), isNot(contains('public protocol NitroEncodable')),
            reason: '$spec in SPM path must not redeclare NitroEncodable');
      }
    });

    test('multi-spec: SPM flat Sources/<ClassName>/ (non-nested layout) also gets stripped files', () {
      _scaffoldPlugin(tempDir, specs: ['nitro_system', 'nitro_ui', 'nitro_view']);

      // Flat layout: ios/Sources/MyPlugin/ (no plugin-name sub-directory).
      final spmFlatDir = Directory(p.join(tempDir.path, 'ios', 'Sources', 'MyPlugin'))
        ..createSync(recursive: true);

      _runSync(tempDir.path);

      for (final spec in ['nitro_ui', 'nitro_view']) {
        expect(
          File(p.join(spmFlatDir.path, '$spec.bridge.g.swift')).readAsStringSync(),
          isNot(contains('public protocol NitroEncodable')),
        );
      }
    });

    test('two-spec plugin: only one file is stripped', () {
      _scaffoldPlugin(tempDir, specs: ['nitro_system', 'nitro_ui']);
      _runSync(tempDir.path);

      expect(
        File(p.join(tempDir.path, 'ios', 'Classes', 'nitro_system.bridge.g.swift'))
            .readAsStringSync(),
        contains('public protocol NitroEncodable'),
        reason: 'First of two: full preamble',
      );
      expect(
        File(p.join(tempDir.path, 'ios', 'Classes', 'nitro_ui.bridge.g.swift'))
            .readAsStringSync(),
        isNot(contains('public protocol NitroEncodable')),
        reason: 'Second of two: stripped',
      );
    });
  });
}

// ── Helpers ───────────────────────────────────────────────────────────────────

String _toPascal(String name) =>
    name.split(RegExp(r'[_\-]')).map((w) => w.isEmpty ? '' : '${w[0].toUpperCase()}${w.substring(1)}').join('');

/// Scaffolds a minimal multi-spec plugin directory with bridge files in the
/// generated swift directory and ios/Classes + macos/Classes directories.
void _scaffoldPlugin(Directory root, {required List<String> specs}) {
  File(p.join(root.path, 'pubspec.yaml')).writeAsStringSync('name: my_plugin\nversion: 0.0.1\n');

  final swiftGenDir = Directory(p.join(root.path, 'lib', 'src', 'generated', 'swift'))
    ..createSync(recursive: true);
  for (final spec in specs) {
    File(p.join(swiftGenDir.path, '$spec.bridge.g.swift')).writeAsStringSync(_fullBridge(spec));
  }

  Directory(p.join(root.path, 'ios', 'Classes')).createSync(recursive: true);
  Directory(p.join(root.path, 'macos', 'Classes')).createSync(recursive: true);
}

/// Replicates `GenerateCommand._syncSwiftBridgesToClasses` for test purposes.
///
/// Kept in sync with the production implementation. When the production
/// implementation changes, update this replica too.
void _runSync(String projectRoot) {
  final swiftGenDir = Directory(p.join(projectRoot, 'lib', 'src', 'generated', 'swift'));
  if (!swiftGenDir.existsSync()) return;

  final bridgeFiles = swiftGenDir
      .listSync()
      .whereType<File>()
      .where((f) => f.path.endsWith('.bridge.g.swift'))
      .toList()
    ..sort((a, b) => p.basename(a.path).compareTo(p.basename(b.path)));
  if (bridgeFiles.isEmpty) return;

  final pluginName = _pluginName(projectRoot);
  final className = _toPascal(pluginName);

  for (final platform in ['ios', 'macos']) {
    // Copy to ios/Classes/ or macos/Classes/.
    for (final prefix in ['', 'example/']) {
      final classesDir = Directory(p.join(projectRoot, '$prefix$platform', 'Classes'));
      if (!classesDir.existsSync()) continue;
      _writeFiles(bridgeFiles, classesDir.path);
    }

    // Copy to SPM Sources/<ClassName>/ in flat and nested layouts.
    final platformRoot = p.join(projectRoot, platform);
    for (final spmDir in [
      Directory(p.join(platformRoot, pluginName, 'Sources', className)),
      Directory(p.join(platformRoot, 'Sources', className)),
    ]) {
      if (!spmDir.existsSync()) continue;
      _writeFiles(bridgeFiles, spmDir.path);
    }
  }
}

void _writeFiles(List<File> bridgeFiles, String destDir) {
  for (var i = 0; i < bridgeFiles.length; i++) {
    final bridge = bridgeFiles[i];
    final dest = p.join(destDir, p.basename(bridge.path));
    final content = bridge.readAsStringSync();
    File(dest).writeAsStringSync(
      (i == 0 || bridgeFiles.length == 1) ? content : stripSharedSwiftPreamble(content),
    );
  }
}

String _pluginName(String projectRoot) {
  final f = File(p.join(projectRoot, 'pubspec.yaml'));
  if (!f.existsSync()) return 'unknown';
  return RegExp(r'^name:\s*(\S+)', multiLine: true).firstMatch(f.readAsStringSync())?.group(1) ?? 'unknown';
}
