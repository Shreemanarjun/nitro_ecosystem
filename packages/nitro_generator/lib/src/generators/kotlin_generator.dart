import '../bridge_spec.dart';
import 'struct_generator.dart';
import 'enum_generator.dart';
import 'record_generator.dart';

class KotlinGenerator {
  static String generate(BridgeSpec spec) {
    if (spec.androidImpl == null) {
      return '// Generated by Nitrogen Modules. Do not edit.\n'
          '// Android not targeted — no Kotlin bridge generated.\n';
    }
    final s = StringBuffer();
    // Pre-build O(1) lookup sets — avoids O(n×m) linear scans inside loops.
    final enumNames = spec.enums.map((e) => e.name).toSet();
    final structNames = spec.structs.map((st) => st.name).toSet();
    final recordNames = spec.recordTypes.map((rt) => rt.name).toSet();
    s.writeln('// Generated by Nitrogen Modules. Do not edit.');
    s.writeln('// Generated from: ${spec.sourceUri.split('/').last}');
    s.writeln('package nitro.${spec.lib.replaceAll('-', '_')}_module');
    s.writeln();
    s.writeln('import androidx.annotation.Keep');
    s.writeln('import kotlinx.coroutines.flow.Flow');
    s.writeln('import kotlinx.coroutines.launch');
    s.writeln('import kotlinx.coroutines.CoroutineScope');
    s.writeln('import kotlinx.coroutines.Dispatchers');
    s.writeln('import kotlinx.coroutines.runBlocking');
    s.writeln();

    final kotlinEnums = EnumGenerator.generateKotlin(spec);
    if (kotlinEnums.isNotEmpty) s.write(kotlinEnums);

    final kotlinStructs = StructGenerator.generateKotlin(spec);
    if (kotlinStructs.isNotEmpty) s.write(kotlinStructs);

    final kotlinRecords = RecordGenerator.generateKotlin(spec);
    if (kotlinRecords.isNotEmpty) s.write(kotlinRecords);

    // ── Interface ─────────────────────────────────────────────────────────
    s.writeln('/**');
    s.writeln(' * Contract for the [${spec.dartClassName}] module.');
    s.writeln(' * Implement this in your Kotlin source code.');
    s.writeln(' */');
    s.writeln('interface Hybrid${spec.dartClassName}Spec {');

    for (final func in spec.functions) {
      final retType = _toKotlinRetType(enumNames, structNames, recordNames, func.returnType);
      final params = func.params.map((p) => '${p.name}: ${_toKotlinParamType(enumNames, structNames, recordNames, p)}').join(', ');
      final suspend = func.isAsync ? 'suspend ' : '';
      // Use the actual return type (enum/struct/record class) in the interface
      s.writeln('    ${suspend}fun ${func.dartName}($params): $retType');
    }
    // Note: interface uses strong types; JniBridge _call methods may use primitive
    // bridge types (Long for enums) so that C JNI can read them directly.

    for (final prop in spec.properties) {
      final kt = _toKotlinType(enumNames, structNames, recordNames, prop.type.name);
      if (prop.hasSetter) {
        s.writeln('    var ${prop.dartName}: $kt');
      } else {
        s.writeln('    val ${prop.dartName}: $kt');
      }
    }

    for (final stream in spec.streams) {
      final itemType = _toKotlinType(enumNames, structNames, recordNames, stream.itemType.name);
      s.writeln('    val ${stream.dartName}: Flow<$itemType>');
    }

    s.writeln('}');
    s.writeln();

    // ── JNI Bridge ────────────────────────────────────────────────────────
    s.writeln('@Keep');
    s.writeln('object ${spec.dartClassName}JniBridge {');
    s.writeln(
      '    private var implementation: Hybrid${spec.dartClassName}Spec? = null',
    );
    s.writeln('    private val _asyncExecutor = java.util.concurrent.Executors.newCachedThreadPool()');
    s.writeln();
    s.writeln('    @JvmStatic external fun initialize(bridgeClass: Class<*>)');
    s.writeln();
    s.writeln('    fun register(impl: Hybrid${spec.dartClassName}Spec) {');
    s.writeln('        implementation = impl');
    s.writeln('        initialize(this::class.java)');
    s.writeln('    }');
    s.writeln();

    for (final func in spec.functions) {
      final retType = _toKotlinRetType(enumNames, structNames, recordNames, func.returnType);
      final paramsDecl = func.params.map((p) => '${p.name}: ${_toKotlinParamType(enumNames, structNames, recordNames, p)}').join(', ');
      final callParams = func.params.map((p) => p.name).join(', ');

      final isUnit = (retType == 'Unit');
      final isEnum = enumNames.contains(func.returnType.name);
      final isRecord = func.returnType.isRecord && !func.returnType.isMap;
      final isListRecord = isRecord && func.returnType.recordListItemType != null && !func.returnType.recordListItemIsPrimitive;
      // JniBridge _call methods expose primitive bridge types to JNI:
      // enums → Long (nativeValue), records → ByteArray (serialized binary), everything else → actual type
      final bridgeRetType = isEnum
          ? 'Long'
          : isRecord
          ? 'ByteArray'
          : retType;

      s.writeln(
        '    @JvmStatic fun ${func.dartName}_call($paramsDecl): $bridgeRetType {',
      );
      s.writeln(
        '        val impl = implementation ?: throw IllegalStateException("${spec.dartClassName} not registered")',
      );
      if (isRecord) {
        // Fetch the result, then serialize to ByteArray for JNI
        if (func.isAsync) {
          s.writeln('        val result = _asyncExecutor.submit(java.util.concurrent.Callable { runBlocking { impl.${func.dartName}($callParams) } }).get()');
        } else {
          s.writeln('        val result = impl.${func.dartName}($callParams)');
        }
        if (isListRecord) {
          // Serialize List<@HybridRecord> → ByteArray (wire: [4-byte payload len][4-byte count][item fields...])
          final itemTypeName = func.returnType.recordListItemType!;
          final itemRt = spec.recordTypes.where((rt) => rt.name == itemTypeName).firstOrNull;
          final perItemHint = itemRt != null ? RecordGenerator.recordBytesHint(itemRt) : 64;
          s.writeln('        val out = java.io.ByteArrayOutputStream(result.size * $perItemHint + 8)');
          s.writeln('        val buf = java.nio.ByteBuffer.allocate(8).order(java.nio.ByteOrder.LITTLE_ENDIAN)');
          s.writeln('        val countBuf = java.nio.ByteBuffer.allocate(4).order(java.nio.ByteOrder.LITTLE_ENDIAN)');
          s.writeln('        countBuf.putInt(result.size)');
          s.writeln('        out.write(countBuf.array())');
          s.writeln('        result.forEach { it.writeFieldsTo(out, buf) }');
          s.writeln('        val payload = out.toByteArray()');
          s.writeln('        val lenBuf = java.nio.ByteBuffer.allocate(4).order(java.nio.ByteOrder.LITTLE_ENDIAN)');
          s.writeln('        lenBuf.putInt(payload.size)');
          s.writeln('        return lenBuf.array() + payload');
        } else {
          // Single @HybridRecord — encode() wraps with 4-byte length prefix
          s.writeln('        return result.encode()');
        }
      } else if (func.isAsync) {
        if (isEnum) {
          s.writeln(
            '        return _asyncExecutor.submit(java.util.concurrent.Callable { runBlocking { impl.${func.dartName}($callParams) } }).get().nativeValue',
          );
        } else {
          s.writeln('        return _asyncExecutor.submit(java.util.concurrent.Callable {');
          s.writeln('            runBlocking { impl.${func.dartName}($callParams) }');
          s.writeln('        }).get()');
        }
      } else {
        if (isUnit) {
          s.writeln('        impl.${func.dartName}($callParams)');
        } else if (isEnum) {
          s.writeln(
            '        return impl.${func.dartName}($callParams).nativeValue',
          );
        } else {
          s.writeln('        return impl.${func.dartName}($callParams)');
        }
      }
      s.writeln('    }');
    }

    for (final prop in spec.properties) {
      final kt = _toKotlinType(enumNames, structNames, recordNames, prop.type.name);
      final isEnum = enumNames.contains(prop.type.name);
      final bridgeKt = isEnum ? 'Long' : kt;
      if (prop.hasGetter) {
        s.writeln('    @JvmStatic fun ${prop.getSymbol}_call(): $bridgeKt {');
        s.writeln(
          '        val impl = implementation ?: throw IllegalStateException("${spec.dartClassName} not registered")',
        );
        if (isEnum) {
          s.writeln('        return impl.${prop.dartName}.nativeValue');
        } else {
          s.writeln('        return impl.${prop.dartName}');
        }
        s.writeln('    }');
      }
      if (prop.hasSetter) {
        s.writeln(
          '    @JvmStatic fun ${prop.setSymbol}_call(value: $bridgeKt) {',
        );
        s.writeln(
          '        val impl = implementation ?: throw IllegalStateException("${spec.dartClassName} not registered")',
        );
        if (isEnum) {
          s.writeln(
            '        impl.${prop.dartName} = ${prop.type.name}.fromNative(value)',
          );
        } else {
          s.writeln('        impl.${prop.dartName} = value');
        }
        s.writeln('    }');
      }
    }

    s.writeln(
      '    private val _streamJobs = java.util.concurrent.ConcurrentHashMap<Pair<String, Long>, kotlinx.coroutines.Job>()',
    );
    s.writeln();

    for (final stream in spec.streams) {
      s.writeln(
        '    @JvmStatic external fun emit_${stream.dartName}(dartPort: Long, item: ${_toKotlinType(enumNames, structNames, recordNames, stream.itemType.name)}): Unit',
      );
      s.writeln('');
      s.writeln(
        '    @JvmStatic fun ${stream.registerSymbol}_call(dartPort: Long) {',
      );
      s.writeln('        val impl = implementation ?: return');
      s.writeln(
        '        _streamJobs[Pair("${stream.dartName}", dartPort)] = CoroutineScope(Dispatchers.Default).launch {',
      );
      s.writeln('            impl.${stream.dartName}.collect { item -> ');
      s.writeln('                emit_${stream.dartName}(dartPort, item)');
      s.writeln('            }');
      s.writeln('        }');
      s.writeln('    }');
      s.writeln(
        '    @JvmStatic fun ${stream.releaseSymbol}_call(dartPort: Long) {',
      );
      s.writeln('        _streamJobs.remove(Pair("${stream.dartName}", dartPort))?.cancel()');
      s.writeln('    }');
    }

    s.writeln('}');
    return s.toString();
  }

  /// Returns the Kotlin return type for a function, handling @HybridRecord types.
  /// - `List<@HybridRecord T>` → `List<T>`
  /// - `List<primitive T>` → `List<KotlinType>`
  /// - `@HybridRecord T` → `T`
  /// - everything else → delegated to [_toKotlinType]
  static String _toKotlinRetType(
    Set<String> enumNames,
    Set<String> structNames,
    Set<String> recordNames,
    BridgeType t,
  ) {
    if (t.isRecord && !t.isMap) {
      if (t.recordListItemType != null && !t.recordListItemIsPrimitive) {
        return 'List<${t.recordListItemType}>';
      } else if (t.recordListItemType != null && t.recordListItemIsPrimitive) {
        return 'List<${_toKotlinType(enumNames, structNames, recordNames, t.recordListItemType!)}>';
      }
      // Direct @HybridRecord — use the class name
      return t.name;
    }
    return _toKotlinType(enumNames, structNames, recordNames, t.name);
  }

  /// Returns the Kotlin type for a function parameter, respecting @ZeroCopy().
  /// Zero-copy TypedData params use java.nio.ByteBuffer (direct buffer, no copy).
  static String _toKotlinParamType(
    Set<String> enumNames,
    Set<String> structNames,
    Set<String> recordNames,
    BridgeParam p,
  ) {
    if (p.zeroCopy && p.type.isTypedData) return 'java.nio.ByteBuffer';
    return _toKotlinType(enumNames, structNames, recordNames, p.type.name);
  }

  static String _toKotlinType(
    Set<String> enumNames,
    Set<String> structNames,
    Set<String> recordNames,
    String t,
  ) {
    final name = t.replaceFirst('?', '');
    switch (name) {
      case 'int':
        return 'Long';
      case 'double':
        return 'Double';
      case 'bool':
        return 'Boolean';
      case 'String':
        return 'String';
      case 'void':
        return 'Unit';
      case 'Uint8List':
      case 'Int8List':
        return 'ByteArray';
      case 'Int16List':
      case 'Uint16List':
        return 'ShortArray';
      case 'Int32List':
      case 'Uint32List':
        return 'IntArray';
      case 'Float32List':
        return 'FloatArray';
      case 'Float64List':
        return 'DoubleArray';
      case 'Int64List':
      case 'Uint64List':
        return 'LongArray';
    }
    if (enumNames.contains(name)) return name;
    if (structNames.contains(name)) return name;
    if (recordNames.contains(name)) return name;
    return 'Any?';
  }
}
