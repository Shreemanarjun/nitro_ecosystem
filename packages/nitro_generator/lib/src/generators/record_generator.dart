    ///   int32 count | int64[count] offsets | item_bytes...
    /// Matches Dart's RecordWriter.encodeIndexedList wire format.
    public func writeIndexedList<T>(_ items: [T], writeItem: (T, NitroRecordWriter) -> Void) {
