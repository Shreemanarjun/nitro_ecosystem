import '../bridge_spec.dart';
import 'code_writer.dart';

/// Generates binary encode/decode extension methods for every @HybridRecord
/// type declared in the spec.  The output is written into the `.g.dart` part
/// file and shares the library namespace with the `.native.dart` source.
///
/// Performance design — each type gets an extension named `${TypeName}RecordExt`
/// with four members:
///
///   • `static T fromNative(Pointer<Uint8> ptr)`
///       Top-level decoder: reads the 4-byte length prefix then the fields.
///       Used for a bare return value / property getter.
///
///   • `static T fromReader(RecordReader r)`
///       Inner decoder: reads fields from an already-opened reader.
///       Used when this record is an element inside a list.
///
///   • `void writeFields(RecordWriter writer)`
///       Writes fields into an in-progress writer.
///       Used when this record is inside a list param.
///
///   • `Pointer<Uint8> toNative(Allocator alloc)`
///       Top-level encoder: opens a writer, calls writeFields, seals + copies.
///       Used for a bare param / property setter.
///
/// Wire format is identical to [RecordWriter] / [RecordReader]:
///   int    → 8-byte little-endian int64
///   double → 8-byte little-endian float64
///   bool   → 1 byte
///   String → 4-byte length + UTF-8 bytes
///   null   → 1-byte tag (0=null, 1=present) then value if present
///   list   → 4-byte count then elements
///
/// `Map<String, T>` types are NOT handled here — they continue to use JSON.
class RecordGenerator {
  /// Record types whose Dart codec lives in package:nitro — skip RecordExt for these.
  static const _nitroLibraryRecordTypes = {
    'NitroNullableInt',
    'NitroNullableDouble',
    'NitroNullableBool',
  };

  static String generateDartExtensions(BridgeSpec spec) {
    // Exclude built-in library types: their Dart codec is already on the class
    // in package:nitro/src/nitro_nullable.dart; generating an extension would
    // duplicate methods and cause "already defined" compile errors.
    final localRecords = spec.localRecordTypes
        .where((r) => !_nitroLibraryRecordTypes.contains(r.name))
        .toList();

    // Compute referencedStructs before the early-return so that modules with
    // NO @HybridRecord types (localRecords.isEmpty) but with List<@HybridStruct T>
    // method returns still get their ${T}RecordExt extensions emitted.
    final structMap = {for (final st in spec.structs) st.name: st};
    final enumNames = spec.enums.map((e) => e.name).toSet();
    final structNames = spec.structs.map((s) => s.name).toSet();

    // Collect structs directly referenced in record fields, then transitively.
    final referencedStructs = <String>{};
    for (final rt in localRecords) {
      for (final f in rt.fields) {
        if (f.kind == RecordFieldKind.recordObject || f.kind == RecordFieldKind.listRecordObject) {
          final typeName = f.kind == RecordFieldKind.listRecordObject ? (f.itemTypeName ?? f.dartType.replaceFirst('?', '')) : f.dartType.replaceFirst('?', '');
          if (structMap.containsKey(typeName)) referencedStructs.add(typeName);
        }
      }
    }

    // Also include structs appearing as List<@HybridStruct T> return types on methods/properties.
    for (final func in spec.functions) {
      final item = func.returnType.recordListItemType;
      if (item != null && !func.returnType.recordListItemIsPrimitive && structMap.containsKey(item) && !spec.recordTypes.any((r) => r.name == item)) {
        referencedStructs.add(item);
      }
    }
    for (final prop in spec.properties) {
      final item = prop.type.recordListItemType;
      if (item != null && !prop.type.recordListItemIsPrimitive && structMap.containsKey(item) && !spec.recordTypes.any((r) => r.name == item)) {
        referencedStructs.add(item);
      }
    }

    void collectNestedDart(String typeName) {
      final st = structMap[typeName];
      if (st == null) return;
      for (final f in st.fields) {
        final base = f.type.name.replaceFirst('?', '');
        if (structMap.containsKey(base) && !referencedStructs.contains(base)) {
          referencedStructs.add(base);
          collectNestedDart(base);
        }
      }
    }

    for (final name in referencedStructs.toList()) {
      collectNestedDart(name);
    }

    if (localRecords.isEmpty && referencedStructs.isEmpty) return '';

    final s = CodeWriter();
    s.writeln(
      '// --- @HybridRecord binary extensions (generated by Nitrogen) ---',
    );
    s.writeln();

    for (final stName in referencedStructs) {
      final st = structMap[stName]!;
      s.writeln('extension ${st.name}RecordExt on ${st.name} {');

      // fromNative
      s.writeln('  static ${st.name} fromNative(Pointer<Uint8> ptr) =>');
      s.writeln('      fromReader(RecordReader.fromNative(ptr));');
      s.writeln();

      // fromReader
      s.writeln('  static ${st.name} fromReader(RecordReader r) =>');
      s.writeln('      ${st.name}(');
      for (final f in st.fields.where((f) => !f.isNamed)) {
        s.writeln('        ${_structFieldReadExpr(f, enumNames, structNames)},');
      }
      for (final f in st.fields.where((f) => f.isNamed)) {
        s.writeln('        ${f.name}: ${_structFieldReadExpr(f, enumNames, structNames)},');
      }
      s.writeln('      );');
      s.writeln();

      // writeFields
      s.writeln('  void writeFields(RecordWriter writer) {');
      for (final f in st.fields) {
        _writeStructFieldStmt(s, f, enumNames, structNames);
      }
      s.writeln('  }');
      // NOTE: toNative(Allocator) is intentionally omitted for @HybridStruct types.
      // The struct already has XxxExt.toNative(Arena arena) via its FFI extension,
      // and Arena implements Allocator, so emitting a second toNative(Allocator)
      // here would create an ambiguous call-site error ("member defined in both
      // XxxExt and XxxRecordExt"). Nested serialisation only needs writeFields.
      s.writeln('}');
      s.writeln();
    }

    for (final rt in localRecords) {
      s.writeln('extension ${rt.name}RecordExt on ${rt.name} {');

      // ── static fromNative (top-level, with 4-byte length prefix) ──────
      s.writeln(
        '  static ${rt.name} fromNative(Pointer<Uint8> ptr) =>',
      );
      s.writeln('      fromReader(RecordReader.fromNative(ptr));');
      s.writeln();

      // ── static fromReader (inner, for use inside lists) ───────────────
      s.writeln(
        '  static ${rt.name} fromReader(RecordReader r) =>',
      );
      s.writeln('      ${rt.name}(');
      for (final f in rt.fields) {
        s.writeln('        ${f.name}: ${_readExpr(f)},');
      }
      s.writeln('      );');
      s.writeln();

      // ── writeFields (inner, for use inside lists) ─────────────────────
      s.writeln('  void writeFields(RecordWriter writer) {');
      for (final f in rt.fields) {
        _writeFieldStmt(s, f);
      }
      s.writeln('  }');
      s.writeln();

      // ── toNative (top-level, allocates native buffer) ─────────────────
      s.writeln('  Pointer<Uint8> toNative(Allocator alloc) {');
      s.writeln('    final writer = RecordWriter();');
      s.writeln('    writeFields(writer);');
      s.writeln('    return writer.toNative(alloc);');
      s.writeln('  }');

      s.writeln('}');
      s.writeln();
    }

    return s.toString();
  }

  // ── Read expression helpers ─────────────────────────────────────────────

  static String _readExpr(BridgeRecordField f) {
    switch (f.kind) {
      case RecordFieldKind.primitive:
        return _primitiveReadExpr(f.dartType);

      case RecordFieldKind.enumValue:
        final baseType = f.dartType.replaceFirst('?', '');
        if (f.isNullable) {
          return 'r.readNullTag() ? null : r.readInt().to$baseType()';
        }
        return 'r.readInt().to$baseType()';

      case RecordFieldKind.recordObject:
        final baseType = f.dartType.replaceFirst('?', '');
        // Built-in library types (NitroNullableInt etc.) define fromReader on the
        // class itself — call directly. All others use the generated RecordExt.
        final readerCall = _nitroLibraryRecordTypes.contains(baseType)
            ? '$baseType.fromReader(r)'
            : '${baseType}RecordExt.fromReader(r)';
        if (f.isNullable) {
          return 'r.readNullTag() ? null : $readerCall';
        }
        return readerCall;

      case RecordFieldKind.listPrimitive:
        final item = f.itemTypeName ?? 'dynamic';
        return 'List.generate(r.readInt32(), (_) => ${_primitiveReadCall(item)})';

      case RecordFieldKind.listEnumValue:
        final item = f.itemTypeName!;
        return 'List.generate(r.readInt32(), (_) => r.readInt().to$item())';

      case RecordFieldKind.listRecordObject:
        final item = f.itemTypeName!;
        final itemReaderCall = _nitroLibraryRecordTypes.contains(item)
            ? '$item.fromReader(r)'
            : '${item}RecordExt.fromReader(r)';
        return 'List.generate(r.readInt32(), (_) => $itemReaderCall)';

      case RecordFieldKind.typedData:
        final base = f.dartType.replaceFirst('?', '');
        if (f.isNullable) {
          return 'r.readNullTag() ? null : ${_typedDataReadCall(base)}';
        }
        return _typedDataReadCall(base);
    }
  }

  /// Read expression for a TypedData field from a RecordReader.
  /// All TypedData types are stored as [4B element_count][element_bytes].
  /// Uint8List uses readBlob() directly; others use buffer view over readBlob().
  static String _typedDataReadCall(String dartType) {
    switch (dartType) {
      case 'Uint8List': return 'r.readBlob()';
      case 'Int8List':  return 'Int8List.view(r.readBlob().buffer)';
      case 'Int16List': return 'Int16List.view(r.readBlob().buffer)';
      case 'Uint16List': return 'Uint16List.view(r.readBlob().buffer)';
      case 'Int32List': return 'Int32List.view(r.readBlob().buffer)';
      case 'Uint32List': return 'Uint32List.view(r.readBlob().buffer)';
      case 'Int64List': return 'Int64List.view(r.readBlob().buffer)';
      case 'Uint64List': return 'Uint64List.view(r.readBlob().buffer)';
      case 'Float32List': return 'Float32List.view(r.readBlob().buffer)';
      case 'Float64List': return 'Float64List.view(r.readBlob().buffer)';
      default: return 'r.readBlob()';
    }
  }

  static String _primitiveReadExpr(String dartType) {
    final base = dartType.replaceFirst('?', '');
    final nullable = dartType.endsWith('?');
    if (nullable) {
      return 'r.readNullTag() ? null : ${_primitiveReadCall(base)}';
    }
    return _primitiveReadCall(base);
  }

  static String _primitiveReadCall(String base) {
    switch (base) {
      case 'int':
        return 'r.readInt()';
      case 'double':
        return 'r.readDouble()';
      case 'bool':
        return 'r.readBool()';
      case 'String':
        return 'r.readString()';
      case 'Uint8List':
        return 'r.readBlob()';
      default:
        return 'r.readInt()';
    }
  }

  // ── Write statement helpers ─────────────────────────────────────────────

  static void _writeFieldStmt(CodeWriter s, BridgeRecordField f) {
    switch (f.kind) {
      case RecordFieldKind.primitive:
        _writePrimitiveStmt(s, f.dartType, f.name);
        break;

      case RecordFieldKind.enumValue:
        if (f.isNullable) {
          s.writeln('    writer.writeNullTag(${f.name} == null);');
          s.writeln('    if (${f.name} != null) {');
          s.writeln('      writer.writeInt(${f.name}!.nativeValue);');
          s.writeln('    }');
        } else {
          s.writeln('    writer.writeInt(${f.name}.nativeValue);');
        }
        break;

      case RecordFieldKind.recordObject:
        if (f.isNullable) {
          s.writeln('    writer.writeNullTag(${f.name} == null);');
          s.writeln('    if (${f.name} != null) {');
          s.writeln('      ${f.name}!.writeFields(writer);');
          s.writeln('    }');
        } else {
          s.writeln('    ${f.name}.writeFields(writer);');
        }
        break;

      case RecordFieldKind.listPrimitive:
        final item = f.itemTypeName ?? 'dynamic';
        final writeCall = _primitiveWriteCall(item, 'e');
        s.writeln('    writer.writeInt32(${f.name}.length);');
        s.writeln('    for (final e in ${f.name}) { writer.$writeCall; }');
        break;

      case RecordFieldKind.listEnumValue:
        s.writeln('    writer.writeInt32(${f.name}.length);');
        s.writeln('    for (final e in ${f.name}) { writer.writeInt(e.nativeValue); }');
        break;

      case RecordFieldKind.listRecordObject:
        s.writeln('    writer.writeInt32(${f.name}.length);');
        s.writeln('    for (final e in ${f.name}) { e.writeFields(writer); }');
        break;

      case RecordFieldKind.typedData:
        // TypedData: convert to Uint8List bytes via .buffer.asUint8List() then writeBlob.
        // Uint8List is written directly; others are reinterpreted through the byte buffer.
        final base = f.dartType.replaceFirst('?', '');
        final toBytes = base == 'Uint8List' ? f.name : '${f.name}.buffer.asUint8List()';
        if (f.isNullable) {
          s.writeln('    writer.writeNullTag(${f.name} == null);');
          s.writeln('    if (${f.name} != null) {');
          final toBytesNonNull = base == 'Uint8List' ? '${f.name}!' : '${f.name}!.buffer.asUint8List()';
          s.writeln('      writer.writeBlob($toBytesNonNull);');
          s.writeln('    }');
        } else {
          s.writeln('    writer.writeBlob($toBytes);');
        }
        break;
    }
  }

  /// Write expression for a TypedData field to a RecordWriter.
  /// All TypedData types are stored as [4B byte_length][element_bytes].
  static String _typedDataWriteCall(String base, String expr) {
    final toBytes = base == 'Uint8List' ? expr : '$expr.buffer.asUint8List()';
    return 'writeBlob($toBytes)';
  }

  static void _writePrimitiveStmt(
    CodeWriter s,
    String dartType,
    String fieldName,
  ) {
    final base = dartType.replaceFirst('?', '');
    final nullable = dartType.endsWith('?');
    if (nullable) {
      s.writeln('    writer.writeNullTag($fieldName == null);');
      s.writeln('    if ($fieldName != null) {');
      s.writeln('      writer.${_primitiveWriteCall(base, '$fieldName!')};');
      s.writeln('    }');
    } else {
      s.writeln('    writer.${_primitiveWriteCall(base, fieldName)};');
    }
  }

  static String _primitiveWriteCall(String base, String expr) {
    switch (base) {
      case 'int':
        return 'writeInt($expr)';
      case 'double':
        return 'writeDouble($expr)';
      case 'bool':
        return 'writeBool($expr)';
      case 'String':
        return 'writeString($expr)';
      case 'Uint8List':
        return 'writeBlob($expr)';
      default:
        return 'writeInt($expr)';
    }
  }

  // ── Struct field read/write helpers (for RecordExt generated above) ───────────

  /// Dart read expression for one field of a @HybridStruct embedded in a record.
  static String _structFieldReadExpr(BridgeField f, Set<String> enumNames, Set<String> structNames) {
    final base = f.type.name.replaceFirst('?', '');
    if (enumNames.contains(base)) return _primitiveReadCall('int');
    if (structNames.contains(base)) return '${base}RecordExt.fromReader(r)';
    return _primitiveReadCall(base);
  }

  /// Emits a Dart write statement for one field of a @HybridStruct embedded in a record.
  static void _writeStructFieldStmt(
    CodeWriter s,
    BridgeField f,
    Set<String> enumNames,
    Set<String> structNames,
  ) {
    final base = f.type.name.replaceFirst('?', '');
    if (enumNames.contains(base)) {
      s.writeln('    writer.${_primitiveWriteCall('int', '${f.name}.nativeValue')};');
    } else if (structNames.contains(base)) {
      s.writeln('    ${f.name}.writeFields(writer);');
    } else {
      _writePrimitiveStmt(s, f.type.name, f.name);
    }
  }

  // ── C++ record decoder generator ────────────────────────────────────────────

  /// Generates C++ struct definitions and a bounds-checked `NitroRecordReader`
  /// for every `@HybridRecord` type in the spec.  Emitted into the
  /// `*.native.g.h` header so C++ native implementations can call
  /// `MyRecord::fromNative(buf)` without hand-rolling binary parsing.
  ///
  /// Safety guarantee: every `readNullTag()` call is preceded by an explicit
  /// `if (offset + 1 > size)` check that throws `std::runtime_error`, preventing
  /// out-of-bounds reads on malformed wire data (IMPROVEMENTS.md §3.3).
  static String generateCpp(BridgeSpec spec) {
    final localRecords = spec.localRecordTypes;
    if (localRecords.isEmpty) return '';
    final enumNames = spec.enums.map((e) => e.name).toSet();

    final s = CodeWriter();

    // Forward declarations so mutually-referencing types compile in any order.
    s.writeln('// @HybridRecord C++ structs (generated by Nitrogen)');
    for (final rt in localRecords) {
      s.writeln('struct ${rt.name};');
    }
    s.writeln();

    // NitroRecordReader — shared helper used by all fromReader implementations.
    s.writeln(_cppRecordReaderDef);
    s.writeln();

    // Per-record struct + fromNative / fromReader.
    for (final rt in localRecords) {
      s.writeln('struct ${rt.name} {');
      for (final f in rt.fields) {
        s.writeln('    ${_cppFieldType(f, enumNames)} ${f.name};');
      }
      s.writeln();
      s.writeln('    static ${rt.name} fromNative(NitroCppBuffer buf) {');
      s.writeln('        NitroRecordReader _r(buf);');
      s.writeln('        return fromReader(_r);');
      s.writeln('    }');
      s.writeln();
      s.writeln('    static ${rt.name} fromReader(NitroRecordReader& _r) {');
      s.writeln('        ${rt.name} _obj;');
      for (final f in rt.fields) {
        _cppEmitFieldRead(s, f, enumNames);
      }
      s.writeln('        return _obj;');
      s.writeln('    }');
      s.writeln('};');
      s.writeln();
    }

    return s.toString();
  }

  static String _cppFieldType(BridgeRecordField f, Set<String> enumNames) {
    switch (f.kind) {
      case RecordFieldKind.primitive:
        final t = _cppPrimType(f.dartType.replaceFirst('?', ''));
        return f.isNullable ? 'std::optional<$t>' : t;
      case RecordFieldKind.enumValue:
        final base = f.dartType.replaceFirst('?', '');
        final t = enumNames.contains(base) ? base : 'int64_t';
        return f.isNullable ? 'std::optional<$t>' : t;
      case RecordFieldKind.recordObject:
        final base = f.dartType.replaceFirst('?', '');
        return f.isNullable ? 'std::optional<$base>' : base;
      case RecordFieldKind.listPrimitive:
        return 'std::vector<${_cppPrimType(f.itemTypeName ?? 'int')}>';
      case RecordFieldKind.listEnumValue:
        final item = f.itemTypeName ?? 'int64_t';
        return 'std::vector<${enumNames.contains(item) ? item : 'int64_t'}>';
      case RecordFieldKind.listRecordObject:
        return 'std::vector<${f.itemTypeName}>';
      case RecordFieldKind.typedData:
        // TypedData in @HybridRecord bridges as a raw byte vector in C++.
        return f.isNullable ? 'std::optional<std::vector<uint8_t>>' : 'std::vector<uint8_t>';
    }
  }

  static String _cppPrimType(String dartType) {
    switch (dartType) {
      case 'int':
        return 'int64_t';
      case 'double':
        return 'double';
      case 'bool':
        return 'bool';
      case 'String':
        return 'std::string';
      default:
        return 'int64_t';
    }
  }

  static String _cppPrimRead(String dartType) {
    switch (dartType) {
      case 'int':
        return 'readInt';
      case 'double':
        return 'readDouble';
      case 'bool':
        return 'readBool';
      case 'String':
        return 'readString';
      default:
        return 'readInt';
    }
  }

  static void _cppEmitFieldRead(CodeWriter s, BridgeRecordField f, Set<String> enumNames) {
    switch (f.kind) {
      case RecordFieldKind.primitive:
        final base = f.dartType.replaceFirst('?', '');
        final t = _cppPrimType(base);
        final read = _cppPrimRead(base);
        if (f.isNullable) {
          s.writeln('        { bool _null = _r.readNullTag(); _obj.${f.name} = _null ? std::nullopt : std::optional<$t>(_r.$read()); }');
        } else {
          s.writeln('        _obj.${f.name} = _r.$read();');
        }
        break;
      case RecordFieldKind.enumValue:
        final base = f.dartType.replaceFirst('?', '');
        final t = enumNames.contains(base) ? base : 'int64_t';
        final readExpr = enumNames.contains(base) ? 'static_cast<$base>(_r.readInt())' : '_r.readInt()';
        if (f.isNullable) {
          s.writeln('        { bool _null = _r.readNullTag(); _obj.${f.name} = _null ? std::nullopt : std::optional<$t>($readExpr); }');
        } else {
          s.writeln('        _obj.${f.name} = $readExpr;');
        }
        break;
      case RecordFieldKind.recordObject:
        final base = f.dartType.replaceFirst('?', '');
        if (f.isNullable) {
          s.writeln('        { bool _null = _r.readNullTag(); _obj.${f.name} = _null ? std::nullopt : std::optional<$base>($base::fromReader(_r)); }');
        } else {
          s.writeln('        _obj.${f.name} = $base::fromReader(_r);');
        }
        break;
      case RecordFieldKind.listPrimitive:
        final item = f.itemTypeName ?? 'int';
        final read = _cppPrimRead(item);
        s.writeln('        { int32_t _n = _r.readInt32(); _obj.${f.name}.reserve((size_t)_n); for (int32_t _i = 0; _i < _n; _i++) _obj.${f.name}.push_back(_r.$read()); }');
        break;
      case RecordFieldKind.listEnumValue:
        final item = f.itemTypeName ?? 'int64_t';
        final readExpr = enumNames.contains(item) ? 'static_cast<$item>(_r.readInt())' : '_r.readInt()';
        s.writeln('        { int32_t _n = _r.readInt32(); _obj.${f.name}.reserve((size_t)_n); for (int32_t _i = 0; _i < _n; _i++) _obj.${f.name}.push_back($readExpr); }');
        break;
      case RecordFieldKind.listRecordObject:
        final item = f.itemTypeName!;
        s.writeln('        { int32_t _n = _r.readInt32(); _obj.${f.name}.reserve((size_t)_n); for (int32_t _i = 0; _i < _n; _i++) _obj.${f.name}.push_back($item::fromReader(_r)); }');
        break;
      case RecordFieldKind.typedData:
        // TypedData: [4B count][count bytes] → std::vector<uint8_t>
        if (f.isNullable) {
          s.writeln('        { bool _null = _r.readNullTag(); if (!_null) { int32_t _n = _r.readInt32(); _obj.${f.name}.emplace().resize((size_t)_n); _r.readBytes(_obj.${f.name}->data(), (size_t)_n); } }');
        } else {
          s.writeln('        { int32_t _n = _r.readInt32(); _obj.${f.name}.resize((size_t)_n); _r.readBytes(_obj.${f.name}.data(), (size_t)_n); }');
        }
        break;
    }
  }

  /// Inline C++ definition of the bounds-checked record reader.
  static const _cppRecordReaderDef = '''
// Bounds-checked binary reader for @HybridRecord wire payloads.
// Every readNullTag() bounds-checks before the byte access (IMPROVEMENTS.md §3.3).
struct NitroRecordReader {
    const uint8_t* _data;
    size_t         _size;
    size_t         _offset;
    explicit NitroRecordReader(NitroCppBuffer buf)
        : _data(buf.data), _size(buf.size), _offset(0) {}
    explicit NitroRecordReader(const uint8_t* data, size_t size)
        : _data(data), _size(size), _offset(0) {}
private:
    void _require(size_t n) const {
        if (_offset + n > _size)
            throw std::runtime_error("NitroRecordReader: buffer underflow");
    }
public:
    int64_t readInt() {
        _require(8); int64_t v; std::memcpy(&v, _data + _offset, 8); _offset += 8; return v;
    }
    int32_t readInt32() {
        _require(4); int32_t v; std::memcpy(&v, _data + _offset, 4); _offset += 4; return v;
    }
    double readDouble() {
        _require(8); uint64_t b; std::memcpy(&b, _data + _offset, 8);
        _offset += 8; double v; std::memcpy(&v, &b, 8); return v;
    }
    bool readBool() {
        _require(1); return _data[_offset++] != 0;
    }
    std::string readString() {
        int32_t len = readInt32(); _require((size_t)len);
        std::string s(reinterpret_cast<const char*>(_data + _offset), (size_t)len);
        _offset += (size_t)len; return s;
    }
    // Bounds-checked null tag read — throws if no byte remains.
    bool readNullTag() {
        if (_offset + 1 > _size)
            throw std::runtime_error("NitroRecordReader: null tag read past end of buffer");
        return _data[_offset++] == 0;
    }
    // TypedData blob: [4B count][count bytes] → reads into caller-owned vector.
    void readBytes(uint8_t* dst, size_t n) {
        _require(n); std::memcpy(dst, _data + _offset, n); _offset += n;
    }
};''';

