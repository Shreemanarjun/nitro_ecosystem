import '../bridge_spec.dart';
import 'enum_generator.dart';
import 'struct_generator.dart';
import 'record_generator.dart';

class DartFfiGenerator {
  static String generate(BridgeSpec spec) {
    final s = StringBuffer();
    s.writeln('// Generated by Nitrogen Modules. Do not edit.');
    s.writeln('// Generated from: ${spec.sourceUri.split('/').last}');
    s.writeln("part of '${spec.sourceUri.split('/').last}';");
    s.writeln();

    // Enum & struct extensions (class bodies live in .native.dart)
    final enumExt = EnumGenerator.generateDartExtensions(spec);
    if (enumExt.isNotEmpty) s.write(enumExt);
    final structExt = StructGenerator.generateDartExtensions(spec);
    if (structExt.isNotEmpty) s.write(structExt);

    // @HybridRecord fromJson / toJson extensions
    final recordExt = RecordGenerator.generateDartExtensions(spec);
    if (recordExt.isNotEmpty) s.write(recordExt);

    // ── Impl class ──────────────────────────────────────────────────────────
    final libStem = spec.lib.replaceAll('-', '_');
    s.writeln(
      'class _${spec.dartClassName}Impl extends ${spec.dartClassName} {',
    );
    s.writeln('  final DynamicLibrary _dylib;');
    s.writeln();
    s.writeln(
      '  _${spec.dartClassName}Impl() : _dylib = NitroRuntime.loadLib(\'${spec.lib}\') {',
    );
    s.writeln(
      "    final initFunc = _dylib.lookupFunction<IntPtr Function(Pointer<Void>), int Function(Pointer<Void>)>('${libStem}_init_dart_api_dl');",
    );
    s.writeln('    initFunc(NativeApi.initializeApiDLData);');
    s.writeln('  }');
    s.writeln();

    // ── Method pointers ─────────────────────────────────────────────────────
    for (final func in spec.functions) {
      final nativeType = _toNativeType(func, spec);
      final dartType = _toDartType(func, spec);
      s.writeln(
        "  late final $dartType _${func.dartName}Ptr = _dylib.lookupFunction<$nativeType, $dartType>('${func.cSymbol}');",
      );
    }

    // ── Property pointers ───────────────────────────────────────────────────
    for (final prop in spec.properties) {
      final ffiType = _typeToFFI(prop.type, spec);
      final dartType = _typeToDartFFI(prop.type, spec);
      final cap = _cap(prop.dartName);
      if (prop.hasGetter) {
        s.writeln(
          "  late final $dartType Function() _get${cap}Ptr = _dylib.lookupFunction<$ffiType Function(), $dartType Function()>('${prop.getSymbol}');",
        );
      }
      if (prop.hasSetter) {
        s.writeln(
          "  late final void Function($dartType) _set${cap}Ptr = _dylib.lookupFunction<Void Function($ffiType), void Function($dartType)>('${prop.setSymbol}');",
        );
      }
    }

    // ── Stream register/release pointers ────────────────────────────────────
    for (final stream in spec.streams) {
      final cap = _cap(stream.dartName);
      s.writeln(
        "  late final void Function(int) _register${cap}Ptr = _dylib.lookupFunction<Void Function(Int64), void Function(int)>('${stream.registerSymbol}');",
      );
      s.writeln(
        "  late final void Function(int) _release${cap}Ptr = _dylib.lookupFunction<Void Function(Int64), void Function(int)>('${stream.releaseSymbol}');",
      );
    }

    // ── dispose() override ───────────────────────────────────────────────────
    s.writeln('  @override');
    s.writeln('  // ignore: unnecessary_overrides');
    s.writeln('  void dispose() {');
    s.writeln(
      '    super.dispose(); // sets isDisposed = true, calls onDestroy()',
    );
    s.writeln('  }');
    s.writeln();

    // ── Method implementations ───────────────────────────────────────────────
    for (final func in spec.functions) {
      final returnType = func.isAsync ? 'Future<${func.returnType.name}>' : func.returnType.name;
      final asyncMod = func.isAsync ? 'async ' : '';

      final needsArena = func.params.any(
        (p) => p.type.isTypedData || p.type.name == 'String' || p.type.isRecord || spec.structs.any((st) => st.name == p.type.name),
      );

      final callArgs = func.params
          .expand((p) {
            final t = p.type.name;
            if (p.type.isRecord) {
              return [_encodeRecordParam(p.type, p.name, 'arena')];
            }
            if (p.type.isTypedData) {
              return ['${p.name}.toPointer(arena)', '${p.name}.length'];
            }
            if (t == 'String') {
              return ['${p.name}.toNativeUtf8(allocator: arena)'];
            }
            if (spec.structs.any((st) => st.name == t)) {
              return ['${p.name}.toNative(arena).cast<Void>()'];
            }
            if (spec.enums.any((en) => en.name == t)) {
              return ['${p.name}.nativeValue'];
            }
            if (t == 'bool') return ['${p.name} ? 1 : 0'];
            return [p.name];
          })
          .join(', ');

      s.writeln('  @override');
      s.writeln(
        '  $returnType ${func.dartName}(${_paramList(func.params)}) $asyncMod{',
      );
      s.writeln('    checkDisposed();');

      final rt = func.returnType.name;
      final isRecordReturn = func.returnType.isRecord;

      String callExpr;
      if (needsArena) {
        callExpr = 'withArena((arena) { final res = _${func.dartName}Ptr($callArgs); NitroRuntime.checkError(_dylib, getErrorName: \'${libStem}_get_error\', clearErrorName: \'${libStem}_clear_error\'); return res; })';
      } else {
        if (func.returnType.name == 'void') {
          callExpr = '() { _${func.dartName}Ptr($callArgs); NitroRuntime.checkError(_dylib, getErrorName: \'${libStem}_get_error\', clearErrorName: \'${libStem}_clear_error\'); }()';
        } else {
          callExpr = '() { final res = _${func.dartName}Ptr($callArgs); NitroRuntime.checkError(_dylib, getErrorName: \'${libStem}_get_error\', clearErrorName: \'${libStem}_clear_error\'); return res; }()';
        }
      }

      if (func.isAsync) {
        final isStructReturn = spec.structs.any((st) => st.name == rt);
        final isEnumReturn = spec.enums.any((en) => en.name == rt);
        final plainCallArgs = func.params.map((p) => p.name).join(', ');
        // Dart return type for callAsync<T> — removes untyped 'result as Pointer<..>'
        // casts at every call site and makes generated code self-documenting.
        final callAsyncType = isRecordReturn
            ? 'Pointer<Uint8>'
            : rt == 'String'
                ? 'Pointer<Utf8>'
                : isStructReturn
                    ? 'Pointer<Void>'
                    : isEnumReturn
                        ? 'int'
                        : _typeToDartFFI(func.returnType, spec);

        if (needsArena) {
          // Arena must outlive the await — use Arena() directly with try/finally.
          // withArena((arena) async { ... }) frees the arena when the callback
          // returns its Future, before the Future completes, causing a
          // use-after-free on any arena-allocated args (strings, struct ptrs).
          s.writeln('    final arena = Arena();');
          s.writeln('    try {');
          if (isRecordReturn) {
            s.writeln('      final rawPtr = await NitroRuntime.callAsync<Pointer<Uint8>>(_${func.dartName}Ptr, [$callArgs]);');
            s.writeln('      NitroRuntime.checkError(_dylib, getErrorName: \'${libStem}_get_error\', clearErrorName: \'${libStem}_clear_error\');');
            s.writeln('      final decoded = ${_decodeRecordExpr(func.returnType, 'rawPtr')};');
            s.writeln('      malloc.free(rawPtr);');
            s.writeln('      return decoded;');
          } else if (rt == 'String') {
            s.writeln('      final rawPtr = await NitroRuntime.callAsync<Pointer<Utf8>>(_${func.dartName}Ptr, [$callArgs]);');
            s.writeln('      NitroRuntime.checkError(_dylib, getErrorName: \'${libStem}_get_error\', clearErrorName: \'${libStem}_clear_error\');');
            s.writeln('      return rawPtr.toDartStringWithFree();');
          } else if (isStructReturn) {
            s.writeln('      final rawPtr = await NitroRuntime.callAsync<Pointer<Void>>(_${func.dartName}Ptr, [$callArgs]);');
            s.writeln('      NitroRuntime.checkError(_dylib, getErrorName: \'${libStem}_get_error\', clearErrorName: \'${libStem}_clear_error\');');
            s.writeln('      final structPtr = Pointer<${rt}Ffi>.fromAddress(rawPtr.address);');
            s.writeln('      final decoded = structPtr.ref.toDart();');
            s.writeln('      malloc.free(structPtr);');
            s.writeln('      return decoded;');
          } else if (isEnumReturn) {
            s.writeln('      final res = await NitroRuntime.callAsync<int>(_${func.dartName}Ptr, [$callArgs]);');
            s.writeln('      NitroRuntime.checkError(_dylib, getErrorName: \'${libStem}_get_error\', clearErrorName: \'${libStem}_clear_error\');');
            s.writeln('      return res.to$rt();');
          } else {
            s.writeln('      final res = await NitroRuntime.callAsync<$callAsyncType>(_${func.dartName}Ptr, [$callArgs]);');
            s.writeln('      NitroRuntime.checkError(_dylib, getErrorName: \'${libStem}_get_error\', clearErrorName: \'${libStem}_clear_error\');');
            if (rt == 'bool') {
              s.writeln('      return res != 0;');
            } else {
              s.writeln('      return res;');
            }
          }
          s.writeln('    } finally {');
          s.writeln('      arena.releaseAll();');
          s.writeln('    }');
        } else {
          // No arena — but @HybridRecord / struct / enum returns still need decode.
          if (isRecordReturn) {
            s.writeln(
              '    final rawPtr = await NitroRuntime.callAsync<Pointer<Uint8>>(_${func.dartName}Ptr, [$plainCallArgs]);',
            );
            s.writeln('    NitroRuntime.checkError(_dylib, getErrorName: \'${libStem}_get_error\', clearErrorName: \'${libStem}_clear_error\');');
            s.writeln('    final decoded = ${_decodeRecordExpr(func.returnType, 'rawPtr')};');
            s.writeln('    malloc.free(rawPtr);');
            s.writeln('    return decoded;');
          } else if (isStructReturn) {
            s.writeln(
              '    final rawPtr = await NitroRuntime.callAsync<Pointer<Void>>(_${func.dartName}Ptr, [$plainCallArgs]);',
            );
            s.writeln('    NitroRuntime.checkError(_dylib, getErrorName: \'${libStem}_get_error\', clearErrorName: \'${libStem}_clear_error\');');
            s.writeln('    final structPtr = Pointer<${rt}Ffi>.fromAddress(rawPtr.address);');
            s.writeln('    final decodedStruct = structPtr.ref.toDart();');
            s.writeln('    malloc.free(structPtr);');
            s.writeln('    return decodedStruct;');
          } else if (isEnumReturn) {
            s.writeln(
              '    final res = await NitroRuntime.callAsync<int>(_${func.dartName}Ptr, [$plainCallArgs]);',
            );
            s.writeln('    NitroRuntime.checkError(_dylib, getErrorName: \'${libStem}_get_error\', clearErrorName: \'${libStem}_clear_error\');');
            s.writeln('    return res.to$rt();');
          } else {
            s.writeln(
              '    final res = await NitroRuntime.callAsync<$callAsyncType>(_${func.dartName}Ptr, [$plainCallArgs]);',
            );
            s.writeln('    NitroRuntime.checkError(_dylib, getErrorName: \'${libStem}_get_error\', clearErrorName: \'${libStem}_clear_error\');');
            if (rt == 'bool') {
              s.writeln('    return res != 0;');
            } else {
              s.writeln('    return res;');
            }
          }
        }
      } else {
        // Synchronous
        if (isRecordReturn) {
          s.writeln('    final rawPtr = $callExpr as Pointer<Uint8>;');
          s.writeln('    final decoded = ${_decodeRecordExpr(func.returnType, 'rawPtr')};');
          s.writeln('    malloc.free(rawPtr);');
          s.writeln('    return decoded;');
        } else if (spec.enums.any((en) => en.name == rt)) {
          s.writeln('    return ($callExpr).to$rt();');
        } else if (spec.structs.any((st) => st.name == rt)) {
          s.writeln('    final structPtr = Pointer<${rt}Ffi>.fromAddress(($callExpr).address);');
          s.writeln('    final decoded = structPtr.ref.toDart();');
          s.writeln('    malloc.free(structPtr);');
          s.writeln('    return decoded;');
        } else if (rt == 'bool') {
          s.writeln('    return $callExpr != 0;');
        } else if (rt == 'String') {
          s.writeln('    return ($callExpr).toDartStringWithFree();');
        } else {
          s.writeln('    return $callExpr;');
        }
      }

      s.writeln('  }');
      s.writeln();
    }

    // ── Property implementations ─────────────────────────────────────────────
    for (final prop in spec.properties) {
      final cap = _cap(prop.dartName);
      final rt = prop.type.name;
      final isRecordProp = prop.type.isRecord;

      if (prop.hasGetter) {
        s.writeln('  @override');
        if (isRecordProp) {
          s.writeln('  $rt get ${prop.dartName} {');
          s.writeln('    checkDisposed();');
          s.writeln(
            '    return ${_decodeRecordExpr(prop.type, '_get${cap}Ptr()')};',
          );
          s.writeln('  }');
        } else {
          s.writeln('  $rt get ${prop.dartName} {');
          s.writeln('    checkDisposed();');
          s.writeln('    final res = _get${cap}Ptr();');
          s.writeln('    NitroRuntime.checkError(_dylib, getErrorName: \'${libStem}_get_error\', clearErrorName: \'${libStem}_clear_error\');');
          if (spec.enums.any((en) => en.name == rt)) {
            s.writeln('    return res.to$rt();');
          } else if (rt == 'bool') {
            s.writeln('    return res != 0;');
          } else if (prop.type.isTypedData) {
            // This is valid if it's in a struct, but here it's a top-level property.
            // Nitrogen currently only supports zero-copy TypedData inside structs with a length field.
            s.writeln('    return res; // ERROR: Naked TypedData return not yet supported');
          } else {
            s.writeln('    return res;');
          }
          s.writeln('  }');
        }
      }

      if (prop.hasSetter) {
        s.writeln('  @override');
        if (isRecordProp) {
          s.writeln('  set ${prop.dartName}($rt value) {');
          s.writeln('    checkDisposed();');
          s.writeln(
            "    withArena((arena) => _set${cap}Ptr(${_encodeRecordParam(prop.type, 'value', 'arena')}));",
          );
          s.writeln('  }');
        } else {
          final propNeedsArena = rt == 'String' || prop.type.isTypedData || spec.structs.any((st) => st.name == rt);

          if (propNeedsArena) {
            String valExpr;
            if (rt == 'String') {
              valExpr = 'value.toNativeUtf8(allocator: arena)';
            } else if (prop.type.isTypedData) {
              valExpr = 'value.toPointer(arena)';
            } else {
              valExpr = 'value.toNative(arena).cast<Void>()';
            }
            s.writeln(
              '  set ${prop.dartName}($rt value) { checkDisposed(); withArena((arena) { _set${cap}Ptr($valExpr); NitroRuntime.checkError(_dylib, getErrorName: \'${libStem}_get_error\', clearErrorName: \'${libStem}_clear_error\'); }); }',
            );
          } else {
            String valExpr = 'value';
            if (spec.enums.any((en) => en.name == rt)) {
              valExpr = 'value.nativeValue';
            } else if (rt == 'bool') {
              valExpr = 'value ? 1 : 0';
            }
            s.writeln(
              '  set ${prop.dartName}($rt value) { checkDisposed(); _set${cap}Ptr($valExpr); NitroRuntime.checkError(_dylib, getErrorName: \'${libStem}_get_error\', clearErrorName: \'${libStem}_clear_error\'); }',
            );
          }
        }
      }
      s.writeln();
    }

    // ── Stream implementations ───────────────────────────────────────────────
    for (final stream in spec.streams) {
      final cap = _cap(stream.dartName);
      final itemType = stream.itemType.name;
      final isRecord = stream.itemType.isRecord;
      final isStruct = spec.structs.any((st) => st.name == itemType);

      final String unpackExpr;
      if (isRecord) {
        final decodeExpr = _decodeRecordExpr(stream.itemType, 'rawPtr');
        unpackExpr = '(rawPtr) => $decodeExpr';
      } else if (isStruct) {
        unpackExpr = '(rawPtr) => Pointer<${itemType}Ffi>.fromAddress(rawPtr).ref.toDart()';
      } else {
        unpackExpr = '(rawPtr) => rawPtr as $itemType';
      }

      s.writeln('  @override');
      s.writeln('  Stream<$itemType> get ${stream.dartName} {');
      s.writeln('    checkDisposed();');
      s.writeln('    return NitroRuntime.openStream<$itemType>(');
      s.writeln('      register: (port) => _register${cap}Ptr(port),');
      s.writeln('      unpack: $unpackExpr,');
      s.writeln('      release: (port) => _release${cap}Ptr(port),');
      s.writeln(
        '      backpressure: Backpressure.${stream.backpressure.name},',
      );
      s.writeln('    );');
      s.writeln('  }');
      s.writeln();
    }

    s.writeln('}');
    return s.toString();
  }

  static String _paramList(List<BridgeParam> params) => params.map((p) => '${p.type.name} ${p.name}').join(', ');

  static String _cap(String name) => name[0].toUpperCase() + name.substring(1);

  static String _toNativeType(BridgeFunction func, BridgeSpec spec) {
    final ret = _typeToFFI(func.returnType, spec);
    final params = func.params
        .expand((p) {
          if (p.type.isTypedData) return [_typeToFFI(p.type, spec), 'Int64'];
          return [_typeToFFI(p.type, spec)];
        })
        .join(', ');
    return '$ret Function($params)';
  }

  static String _toDartType(BridgeFunction func, BridgeSpec spec) {
    final ret = _typeToDartFFI(func.returnType, spec);
    final params = func.params
        .expand((p) {
          if (p.type.isTypedData) return [_typeToDartFFI(p.type, spec), 'int'];
          return [_typeToDartFFI(p.type, spec)];
        })
        .join(', ');
    return '$ret Function($params)';
  }

  static String _typeToFFI(BridgeType bt, BridgeSpec spec) {
    if (bt.isRecord) {
      // Map<String,T> still uses JSON (UTF-8 text); all others use binary.
      return bt.isMap ? 'Pointer<Utf8>' : 'Pointer<Uint8>';
    }
    final name = bt.name.replaceFirst('?', '');
    switch (name) {
      case 'int':
        return 'Int64';
      case 'double':
        return 'Double';
      case 'bool':
        return 'Int8';
      case 'String':
        return 'Pointer<Utf8>';
      case 'Uint8List':
        return 'Pointer<Uint8>';
      case 'Int8List':
        return 'Pointer<Int8>';
      case 'Int16List':
        return 'Pointer<Int16>';
      case 'Int32List':
        return 'Pointer<Int32>';
      case 'Uint16List':
        return 'Pointer<Uint16>';
      case 'Uint32List':
        return 'Pointer<Uint32>';
      case 'Float32List':
        return 'Pointer<Float>';
      case 'Float64List':
        return 'Pointer<Double>';
      case 'Int64List':
        return 'Pointer<Int64>';
      case 'Uint64List':
        return 'Pointer<Uint64>';
      case 'void':
        return 'Void';
    }
    if (spec.enums.any((en) => en.name == name)) return 'Int64';
    return 'Pointer<Void>';
  }

  static String _typeToDartFFI(BridgeType bt, BridgeSpec spec) {
    if (bt.isRecord) {
      return bt.isMap ? 'Pointer<Utf8>' : 'Pointer<Uint8>';
    }
    final name = bt.name.replaceFirst('?', '');
    switch (name) {
      case 'int':
        return 'int';
      case 'double':
        return 'double';
      case 'bool':
        return 'int';
      case 'String':
        return 'Pointer<Utf8>';
      case 'Uint8List':
        return 'Pointer<Uint8>';
      case 'Int8List':
        return 'Pointer<Int8>';
      case 'Int16List':
        return 'Pointer<Int16>';
      case 'Int32List':
        return 'Pointer<Int32>';
      case 'Uint16List':
        return 'Pointer<Uint16>';
      case 'Uint32List':
        return 'Pointer<Uint32>';
      case 'Float32List':
        return 'Pointer<Float>';
      case 'Float64List':
        return 'Pointer<Double>';
      case 'Int64List':
        return 'Pointer<Int64>';
      case 'Uint64List':
        return 'Pointer<Uint64>';
      case 'void':
        return 'void';
    }
    if (spec.enums.any((en) => en.name == name)) return 'int';
    return 'Pointer<Void>';
  }

  /// Generates a Dart expression that decodes [ptrVar] (a pointer) into the
  /// Dart value matching [type]:
  /// - `Map<String, T>`        → JSON path: `jsonDecode(Pointer<Utf8>...)`
  /// - `List<@HybridRecord T>` → `RecordReader.decodeList(...fromReader)`
  /// - `List<primitive>`       → `RecordReader.decodePrimitiveList(...readXxx)`
  /// - `@HybridRecord`         → `TRecordExt.fromNative(Pointer<Uint8>)`
  static String _decodeRecordExpr(BridgeType type, String ptrVar) {
    // Map<String,T> still uses the JSON text path.
    if (type.isMap) {
      return 'jsonDecode(($ptrVar as Pointer<Utf8>).toDartStringWithFree()) as Map<String, dynamic>';
    }
    final item = type.recordListItemType;
    if (item != null) {
      if (type.recordListItemIsPrimitive) {
        final readCall = _primitiveReaderCall(item);
        return 'RecordReader.decodePrimitiveList($ptrVar, (r) => r.$readCall())';
      }
      return 'RecordReader.decodeList($ptrVar, (r) => ${item}RecordExt.fromReader(r))';
    }
    final rt = type.name;
    return '${rt}RecordExt.fromNative($ptrVar)';
  }

  /// Maps a primitive type name to the matching [RecordReader] method.
  static String _primitiveReaderCall(String item) {
    switch (item) {
      case 'int':
        return 'readInt';
      case 'double':
        return 'readDouble';
      case 'bool':
        return 'readBool';
      default:
        return 'readString';
    }
  }

  /// Maps a primitive type name to the matching [RecordWriter] method.
  static String _primitiveWriterCall(String item) {
    switch (item) {
      case 'int':
        return 'writeInt';
      case 'double':
        return 'writeDouble';
      case 'bool':
        return 'writeBool';
      default:
        return 'writeString';
    }
  }

  /// Generates a Dart expression that encodes [varName] to a native binary
  /// pointer for crossing the C FFI boundary:
  /// - `Map<String, T>`        → JSON text path: `jsonEncode(...).toNativeUtf8`
  /// - `List<@HybridRecord T>` → `RecordWriter.encodeList(...writeFields)`
  /// - `List<primitive>`       → `RecordWriter.encodePrimitiveList(...writeXxx)`
  /// - `@HybridRecord`         → `varName.toNative(allocator)`
  static String _encodeRecordParam(
    BridgeType type,
    String varName,
    String allocator,
  ) {
    if (type.isMap) {
      return 'jsonEncode($varName).toNativeUtf8(allocator: $allocator)';
    }
    final item = type.recordListItemType;
    if (item != null) {
      if (type.recordListItemIsPrimitive) {
        final writeCall = _primitiveWriterCall(item);
        return 'RecordWriter.encodePrimitiveList($varName, (w, e) => w.$writeCall(e), $allocator)';
      }
      return 'RecordWriter.encodeList($varName, (w, e) => e.writeFields(w), $allocator)';
    }
    return '$varName.toNative($allocator)';
  }
}
