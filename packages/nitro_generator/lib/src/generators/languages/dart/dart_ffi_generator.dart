    if (bt.isPointer) {
      return 'Pointer<${bt.pointerInnerType}>';
    }
    final name = bt.name.replaceFirst('?', '');
    switch (name) {
      case 'int':
        return 'int';
      case 'double':
        return 'double';
      case 'bool':
        return 'int';
      case 'String':
        return 'Pointer<Utf8>';
      case 'Uint8List':
        return 'Pointer<Uint8>';
      case 'Int8List':
        return 'Pointer<Int8>';
      case 'Int16List':
        return 'Pointer<Int16>';
      case 'Int32List':
        return 'Pointer<Int32>';
      case 'Uint16List':
        return 'Pointer<Uint16>';
