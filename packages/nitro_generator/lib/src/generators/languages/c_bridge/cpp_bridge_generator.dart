import '../../../bridge_spec.dart';
import '../../code_writer.dart';

class CppBridgeGenerator {
  static String generate(BridgeSpec spec) {
    // All targeted platforms use C++ — emit the lean direct-call bridge.
    if (spec.isCppImpl) return _generateCppDirect(spec);

    final iosIsCpp = spec.iosIsCpp;
    final macosIsCpp = spec.macosIsCpp;
    // hasApple covers both iOS and macOS (either may use Swift or C++).
    final hasApple = spec.targetsIos || spec.targetsMacos;

    if (hasApple && !spec.targetsAndroid) {
      return _generateJniSwift(spec, includeAndroid: false, iosIsCpp: iosIsCpp, macosIsCpp: macosIsCpp);
    }
    if (!hasApple && spec.targetsAndroid) {
      return _generateJniSwift(spec, includeIos: false, iosIsCpp: iosIsCpp, macosIsCpp: macosIsCpp);
    }
    return _generateJniSwift(spec, iosIsCpp: iosIsCpp, macosIsCpp: macosIsCpp);
  }

  // ── Direct C++ path (NativeImpl.cpp on all targeted platforms) ─────────────

  static String _generateCppDirect(BridgeSpec spec) {
    final writer = CodeWriter();
    final libStem = spec.lib.replaceAll('-', '_');
    final className = spec.dartClassName;
    final headerName = '$libStem.bridge.g.h';
    final ifaceHeader = '$libStem.native.g.h';

    final recordNames = spec.recordTypes.map((r) => r.name).toSet();
    final enumNames = spec.enums.map((e) => e.name).toSet();
    final structNames = spec.structs.map((st) => st.name).toSet();

    // Platform guard: scope compilation to only the targeted platforms.
    //   __APPLE__   — covers both iOS and macOS (clang always defines this on Apple targets)
    //   __ANDROID__ — Android NDK
    // When Apple + Android are both targeted, the C++ file is compiled on all
    // platforms and no guard is needed — the same source tree is shared.
    final targetsApple = spec.targetsIos || spec.targetsMacos;
    final cppAppleOnly = targetsApple && !spec.targetsAndroid;
    final cppAndroidOnly = spec.targetsAndroid && !targetsApple;

    writer.line('// Generated by Nitrogen Modules. Do not edit.');
    writer.line('// Generated from: ${spec.sourceUri.split('/').last}');
    writer.line('// NativeImpl: cpp — shared C++ virtual-dispatch bridge (no JNI / Swift).');
    if (cppAppleOnly) writer.line('#ifdef __APPLE__    // iOS + macOS');
    if (cppAndroidOnly) writer.line('#ifdef __ANDROID__');
    writer.line('#include <stdint.h>');
    writer.line('#include <stdbool.h>');
    writer.line('#include <string.h>');
    writer.line('#include <stdlib.h>');
    writer.line('#include <string>');
    writer.line('#include <stdexcept>');
    writer.line('#include "dart_api_dl.h"');
    writer.line('#include "$headerName"');
    writer.line('#include "$ifaceHeader"');
    writer.blankLine();

    // Dart API DL init
    writer.line('extern "C" {');
    writer.line('NITRO_EXPORT intptr_t ${libStem}_init_dart_api_dl(void* data) {');
    writer.line('    return Dart_InitializeApiDL(data);');
    writer.line('}');
    writer.line('}');
    writer.blankLine();

    // Error state
    writer.line('static thread_local NitroError g_nitro_error = { 0, nullptr, nullptr, nullptr, nullptr };');
    writer.blankLine();
    writer.line('extern "C" {');
    writer.line('NitroError* ${libStem}_get_error() { return &g_nitro_error; }');
    writer.line('void ${libStem}_clear_error() {');
    writer.line('    if (!g_nitro_error.hasError) return;');
    writer.line('    g_nitro_error.hasError = 0;');
    writer.line('    if (g_nitro_error.name)       { free((void*)g_nitro_error.name);       g_nitro_error.name       = nullptr; }');
    writer.line('    if (g_nitro_error.message)    { free((void*)g_nitro_error.message);    g_nitro_error.message    = nullptr; }');
    writer.line('    if (g_nitro_error.code)       { free((void*)g_nitro_error.code);       g_nitro_error.code       = nullptr; }');
    writer.line('    if (g_nitro_error.stackTrace) { free((void*)g_nitro_error.stackTrace); g_nitro_error.stackTrace = nullptr; }');
    writer.line('}');
    writer.line('static void nitro_report_error(const char* name, const char* message, const char* code, const char* stack) {');
    writer.line('    ${libStem}_clear_error();');
    writer.line('    g_nitro_error.hasError = 1;');
    writer.line('    g_nitro_error.name       = name    ? strdup(name)    : strdup("NativeException");');
    writer.line('    g_nitro_error.message    = message ? strdup(message) : strdup("An unknown C++ exception occurred.");');
    writer.line('    g_nitro_error.code       = code    ? strdup(code)    : nullptr;');
    writer.line('    g_nitro_error.stackTrace = stack   ? strdup(stack)   : nullptr;');
    writer.line('}');
    writer.line('}');
    writer.blankLine();

    // Implementation registry.
    // Thread-safety contract: register_impl() MUST be called (and complete)
    // before any concurrent native call can reach get_impl().  In practice this
    // is guaranteed because registration always happens in an
    // __attribute__((constructor)) which runs synchronously at DSO load —
    // before Dart's isolate threads can invoke any bridge function.
    // We intentionally do NOT use std::atomic here: the pointer is written
    // exactly once at startup and never mutated during concurrent use, so a
    // plain load is safe and avoids seq_cst memory barriers on every hot call.
    writer.line('// g_impl is written once during DSO load (see __attribute__((constructor)))');
    writer.line('// and is read-only during concurrent bridge calls — no std::atomic needed.');
    writer.line('static Hybrid$className* g_impl = nullptr;');
    writer.blankLine();
    writer.line('extern "C" {');
    writer.line('void ${libStem}_register_impl(Hybrid$className* impl) { g_impl = impl; }');
    writer.line('Hybrid$className* ${libStem}_get_impl() { return g_impl; }');
    writer.line('}');
    writer.blankLine();

    // Stream state: one Dart port per stream (simplest model)
    for (final stream in spec.streams) {
      writer.line('static int64_t g_port_${stream.dartName} = 0;');
    }
    if (spec.streams.isNotEmpty) writer.blankLine();

    // Stream emit helpers (called by user's C++ implementation)
    for (final stream in spec.streams) {
      final isStruct = structNames.contains(stream.itemType.name.replaceFirst('?', ''));
      final isRecord = stream.itemType.isRecord;
      final isEnum = enumNames.contains(stream.itemType.name.replaceFirst('?', ''));
      final itemCpp = _cppScalarType(stream.itemType.name, enumNames, structNames);
      writer.line('void Hybrid$className::emit_${stream.dartName}($itemCpp item) {');
      writer.line('    int64_t port = g_port_${stream.dartName};');
      writer.line('    if (port == 0) return;');
      writer.line('    Dart_CObject obj;');
      if (stream.itemType.name == 'double') {
        writer.line('    obj.type = Dart_CObject_kDouble;');
        writer.line('    obj.value.as_double = item;');
      } else if (stream.itemType.name == 'int') {
        writer.line('    obj.type = Dart_CObject_kInt64;');
        writer.line('    obj.value.as_int64 = item;');
      } else if (stream.itemType.name == 'bool') {
        writer.line('    obj.type = Dart_CObject_kBool;');
        writer.line('    obj.value.as_bool = item;');
      } else if (isEnum) {
        writer.line('    obj.type = Dart_CObject_kInt64;');
        writer.line('    obj.value.as_int64 = static_cast<int64_t>(item);');
      } else if (isStruct) {
        final stName = stream.itemType.name.replaceFirst('?', '');
        writer.line('    $stName* st_ptr = ($stName*)malloc(sizeof($stName));');
        writer.line('    *st_ptr = item;');
        writer.line('    obj.type = Dart_CObject_kInt64;');
        writer.line('    obj.value.as_int64 = (intptr_t)st_ptr;');
      } else if (isRecord) {
        // item is void* pointing to malloc'd encoded record bytes
        // (wire format: [4-byte payload_len][payload]).
        // Dart reads it via RecordReader.fromNative and frees with malloc.free.
        writer.line('    obj.type = Dart_CObject_kInt64;');
        writer.line('    obj.value.as_int64 = (intptr_t)item;');
      } else {
        writer.line('    obj.type = Dart_CObject_kNull;');
      }
      writer.line('    Dart_PostCObject_DL(port, &obj);');
      writer.line('}');
      writer.blankLine();
    }

    // Guard snippet used in every exported function
    final notInit =
        'nitro_report_error("NotInitialized", '
        '"No C++ implementation registered. Call ${libStem}_register_impl() first.", '
        'nullptr, nullptr)';

    writer.line('extern "C" {');
    writer.blankLine();

    // ── Methods ──────────────────────────────────────────────────────────────
    for (final func in spec.functions) {
      if (func.isNativeAsync) {
        // ── @NitroNativeAsync — void wrapper with dart_port param ────────────
        // The C function returns void and delegates to the impl, passing the
        // Dart port so the impl can post the result via Dart_PostCObject_DL.
        // No error slot is used — implementations must post errors via the port.
        final paramParts = <String>[];
        for (final p in func.params) {
          if (p.type.isFunction) {
            paramParts.add(_callbackParamToC(p, enumNames));
            continue;
          }
          final isStructParam = structNames.contains(p.type.name.replaceFirst('?', ''));
          final isRecordParam = recordNames.contains(p.type.name.replaceFirst('?', ''));
          paramParts.add('${(isStructParam || isRecordParam) ? 'void*' : _typeToC(p.type.name)} ${p.name}');
          if (p.type.isTypedData) paramParts.add('int64_t ${p.name}_length');
        }
        paramParts.add('int64_t dart_port');
        final paramsDecl = paramParts.join(', ');

        // Build C++ call args (same conversion logic as regular methods)
        final callArgs = <String>[];
        for (final p in func.params) {
          final base = p.type.name.replaceFirst('?', '');
          if (p.type.isFunction) {
            callArgs.add(p.name);
          } else if (base == 'String') {
            callArgs.add('std::string(${p.name})');
          } else if (structNames.contains(base)) {
            callArgs.add('*static_cast<const $base*>(${p.name})');
          } else if (recordNames.contains(base)) {
            callArgs.add('NitroCppBuffer{ (const uint8_t*)${p.name} + 4, (size_t)*(int32_t*)${p.name} }');
          } else if (p.type.isTypedData) {
            callArgs.add(p.name);
            callArgs.add('static_cast<size_t>(${p.name}_length)');
          } else if (enumNames.contains(base)) {
            callArgs.add('static_cast<$base>(${p.name})');
          } else {
            callArgs.add(p.name);
          }
        }
        callArgs.add('dart_port');
        final callArgStr = callArgs.join(', ');

        writer.line('void ${func.cSymbol}($paramsDecl) {');
        writer.line('    if (!g_impl) {');
        writer.line('        Dart_CObject _err = { Dart_CObject_kNull };');
        writer.line('        Dart_PostCObject_DL(dart_port, &_err);');
        writer.line('        return;');
        writer.line('    }');
        writer.line('    g_impl->${func.dartName}($callArgStr);');
        writer.line('}');
        writer.blankLine();
        continue;
      }

      // ── Regular (sync or @nitroAsync) method ─────────────────────────────
      final isEnumRet = enumNames.contains(func.returnType.name.replaceFirst('?', ''));
      final isStructRet = structNames.contains(func.returnType.name.replaceFirst('?', ''));
      // Use func.returnType.isRecord so that List<@HybridStruct T>, List<@HybridRecord T>,
      // and bare @HybridRecord all map to NitroCppBuffer (binary-encoded buffer return).
      final isRecordRet = func.returnType.isRecord;
      final cRet = isEnumRet ? 'int64_t' : _typeToC(func.returnType.name);
      final dflt = _defaultValue(cRet);

      final paramParts = <String>[];
      for (final p in func.params) {
        if (p.type.isFunction) {
          paramParts.add(_callbackParamToC(p, enumNames));
          continue;
        }
        final isStructParam = structNames.contains(p.type.name.replaceFirst('?', ''));
        final isRecordParam = p.type.isRecord;
        paramParts.add('${(isStructParam || isRecordParam) ? 'void*' : _typeToC(p.type.name)} ${p.name}');
        if (p.type.isTypedData) paramParts.add('int64_t ${p.name}_length');
      }
      final paramsDecl = paramParts.isEmpty ? 'void' : paramParts.join(', ');

      writer.line('$cRet ${func.cSymbol}($paramsDecl) {');
      writer.line('    ${libStem}_clear_error();');
      if (func.returnType.name == 'void') {
        writer.line('    if (!g_impl) { $notInit; return; }');
      } else {
        writer.line('    if (!g_impl) { $notInit; return $dflt; }');
      }
      writer.line('    try {');

      // Build call args (C → C++ types)
      final callArgs = <String>[];
      for (final p in func.params) {
        final base = p.type.name.replaceFirst('?', '');
        if (p.type.isFunction) {
          callArgs.add(p.name);
        } else if (base == 'String') {
          callArgs.add('std::string(${p.name})');
        } else if (structNames.contains(base)) {
          callArgs.add('*static_cast<const $base*>(${p.name})');
        } else if (p.type.isRecord) {
          if (p.type.isNullable) {
            writer.line('        NitroCppBuffer _buf_${p.name} = { nullptr, 0 };');
            writer.line('        if (${p.name} != nullptr) {');
            writer.line('            _buf_${p.name}.data = (const uint8_t*)${p.name} + 4;');
            writer.line('            _buf_${p.name}.size = (size_t)*(int32_t*)${p.name};');
            writer.line('        }');
            callArgs.add('_buf_${p.name}');
          } else {
            writer.line('        NitroCppBuffer _buf_${p.name} = { (const uint8_t*)${p.name} + 4, (size_t)*(int32_t*)${p.name} };');
            callArgs.add('_buf_${p.name}');
          }
        } else if (p.type.isTypedData) {
          callArgs.add(p.name);
          callArgs.add('static_cast<size_t>(${p.name}_length)');
        } else if (enumNames.contains(base)) {
          callArgs.add('static_cast<$base>(${p.name})');
        } else {
          callArgs.add(p.name);
        }
      }
      final callArgStr = callArgs.join(', ');

      if (func.returnType.name == 'void') {
        writer.line('        g_impl->${func.dartName}($callArgStr);');
      } else if (func.returnType.name == 'String') {
        writer.line('        std::string _res = g_impl->${func.dartName}($callArgStr);');
        writer.line('        return strdup(_res.c_str());');
      } else if (isEnumRet) {
        writer.line('        return static_cast<int64_t>(g_impl->${func.dartName}($callArgStr));');
      } else if (isStructRet) {
        final stName = func.returnType.name.replaceFirst('?', '');
        writer.line('        $stName _res = g_impl->${func.dartName}($callArgStr);');
        writer.line('        $stName* _ptr = ($stName*)malloc(sizeof($stName));');
        writer.line('        *_ptr = _res;');
        writer.line('        return _ptr;');
      } else if (isRecordRet) {
        writer.line('        NitroCppBuffer _res = g_impl->${func.dartName}($callArgStr);');
        writer.line('        return (void*)_res.data;');
      } else {
        writer.line('        return g_impl->${func.dartName}($callArgStr);');
      }

      writer.line('    } catch (const std::exception& e) {');
      writer.line('        nitro_report_error("CppException", e.what(), nullptr, nullptr);');
      if (func.returnType.name != 'void') {
        writer.line('        return $dflt;');
      }
      writer.line('    } catch (...) {');
      writer.line('        nitro_report_error("CppException", "Unknown C++ exception", nullptr, nullptr);');
      if (func.returnType.name != 'void') {
        writer.line('        return $dflt;');
      }
      writer.line('    }');
      writer.line('}');
      writer.blankLine();
    }

    // ── Properties ───────────────────────────────────────────────────────────
    for (final prop in spec.properties) {
      final isEnum = enumNames.contains(prop.type.name.replaceFirst('?', ''));
      final cType = isEnum ? 'int64_t' : _typeToC(prop.type.name);

      if (prop.hasGetter) {
        writer.line('$cType ${prop.getSymbol}(void) {');
        writer.line('    ${libStem}_clear_error();');
        writer.line('    if (!g_impl) { $notInit; return ${_defaultValue(cType)}; }');
        writer.line('    try {');
        if (prop.type.name == 'String') {
          writer.line('        std::string _res = g_impl->get_${prop.dartName}();');
          writer.line('        return strdup(_res.c_str());');
        } else if (isEnum) {
          writer.line('        return static_cast<int64_t>(g_impl->get_${prop.dartName}());');
        } else if (recordNames.contains(prop.type.name.replaceFirst('?', ''))) {
          writer.line('        NitroCppBuffer _res = g_impl->get_${prop.dartName}();');
          writer.line('        return (void*)_res.data;');
        } else if (structNames.contains(prop.type.name.replaceFirst('?', ''))) {
          final stName = prop.type.name.replaceFirst('?', '');
          writer.line('        $stName _res = g_impl->get_${prop.dartName}();');
          writer.line('        $stName* _ptr = ($stName*)malloc(sizeof($stName));');
          writer.line('        *_ptr = _res;');
          writer.line('        return _ptr;');
        } else {
          writer.line('        return g_impl->get_${prop.dartName}();');
        }
        writer.line('    } catch (const std::exception& e) {');
        writer.line('        nitro_report_error("CppException", e.what(), nullptr, nullptr);');
        writer.line('        return ${_defaultValue(cType)};');
        writer.line('    }');
        writer.line('}');
        writer.blankLine();
      }

      if (prop.hasSetter) {
        final isStructParam = structNames.contains(prop.type.name.replaceFirst('?', ''));
        final isRecordParam = recordNames.contains(prop.type.name.replaceFirst('?', ''));
        final paramCType = (isEnum || isStructParam || isRecordParam) ? (isEnum ? 'int64_t' : 'void*') : _typeToC(prop.type.name);
        writer.line('void ${prop.setSymbol}($paramCType value) {');
        writer.line('    ${libStem}_clear_error();');
        writer.line('    if (!g_impl) { $notInit; return; }');
        writer.line('    try {');
        if (prop.type.name == 'String') {
          writer.line('        g_impl->set_${prop.dartName}(std::string(value));');
        } else if (isEnum) {
          final enumName = prop.type.name.replaceFirst('?', '');
          writer.line('        g_impl->set_${prop.dartName}(static_cast<$enumName>(value));');
        } else if (isRecordParam) {
          final opt = prop.type.name.endsWith('?');
          if (opt) {
            writer.line('        NitroCppBuffer _buf = { nullptr, 0 };');
            writer.line('        if (value != nullptr) {');
            writer.line('            _buf.data = (const uint8_t*)value + 4;');
            writer.line('            _buf.size = (size_t)*(int32_t*)value;');
            writer.line('        }');
            writer.line('        g_impl->set_${prop.dartName}(_buf);');
          } else {
            writer.line('        NitroCppBuffer _buf = { (const uint8_t*)value + 4, (size_t)*(int32_t*)value };');
            writer.line('        g_impl->set_${prop.dartName}(_buf);');
          }
        } else if (isStructParam) {
          final stName = prop.type.name.replaceFirst('?', '');
          writer.line('        g_impl->set_${prop.dartName}(*static_cast<const $stName*>(value));');
        } else {
          writer.line('        g_impl->set_${prop.dartName}(value);');
        }
        writer.line('    } catch (const std::exception& e) {');
        writer.line('        nitro_report_error("CppException", e.what(), nullptr, nullptr);');
        writer.line('    }');
        writer.line('}');
        writer.blankLine();
      }
    }

    // ── Streams ──────────────────────────────────────────────────────────────
    for (final stream in spec.streams) {
      writer.line('void ${stream.registerSymbol}(int64_t dart_port) {');
      writer.line('    g_port_${stream.dartName} = dart_port;');
      writer.line('}');
      writer.line('void ${stream.releaseSymbol}(int64_t dart_port) {');
      writer.line('    if (g_port_${stream.dartName} == dart_port) g_port_${stream.dartName} = 0;');
      writer.line('}');
      writer.blankLine();
    }

    // ── Struct release functions (used by NativeFinalizer in Dart proxy classes) ─
    for (final st in spec.structs) {
      writer.line('// Frees a malloc\'d [${st.name}] wrapper allocated by the stream emitter.');
      writer.line('// Called automatically by NativeFinalizer when the Dart proxy is GC\'d.');
      writer.line('void ${libStem}_release_${st.name}(void* ptr) {');
      writer.line('    if (!ptr) return;');
      final hasStrings = st.fields.any((f) => f.type.name == 'String');
      final hasNestedStructs = st.fields.any((f) => structNames.contains(f.type.name.replaceFirst('?', '')));
      final hasNonZcData = st.fields.any((f) => f.type.isTypedData && !f.zeroCopy);
      final hasZeroCopy = st.fields.any((f) => f.zeroCopy);
      if (hasStrings || hasNestedStructs || hasNonZcData) {
        writer.line('    ${st.name}* st_ptr = (${st.name}*)ptr;');
        for (final f in st.fields) {
          if (f.type.name == 'String') {
            writer.line('    if (st_ptr->${f.name}) free((void*)st_ptr->${f.name});');
          } else if (structNames.contains(f.type.name.replaceFirst('?', ''))) {
            writer.line('    if (st_ptr->${f.name}) { free(st_ptr->${f.name}); st_ptr->${f.name} = nullptr; }');
          } else if (f.type.isTypedData && !f.zeroCopy) {
            writer.line('    if (st_ptr->${f.name}) { free(st_ptr->${f.name}); st_ptr->${f.name} = nullptr; }');
          }
        }
      }
      if (hasZeroCopy) {
        writer.line('#ifdef __ANDROID__');
        writer.line('    {');
        writer.line('        std::lock_guard<std::mutex> _lk(g_zero_copy_refs_mtx);');
        writer.line('        auto it = g_zero_copy_refs.find(ptr);');
        writer.line('        if (it != g_zero_copy_refs.end()) {');
        writer.line('            JNIEnv* _env = GetEnv();');
        writer.line('            if (_env != nullptr) _env->DeleteGlobalRef(it->second);');
        writer.line('            g_zero_copy_refs.erase(it);');
        writer.line('        }');
        writer.line('    }');
        writer.line('#endif // __ANDROID__');
      }
      writer.line('    free(ptr);');
      writer.line('}');
      writer.blankLine();
    }

    writer.line('} // extern "C"');
    if (cppAppleOnly) writer.line('#endif // __APPLE__  // iOS + macOS');
    if (cppAndroidOnly) writer.line('#endif // __ANDROID__');
    return writer.toString();
  }

  // ── Helpers shared between both paths ────────────────────────────────────

  static String _cppScalarType(String dartType, Set<String> enumNames, Set<String> structNames) {
    final base = dartType.replaceFirst('?', '');
    if (base == 'String') return 'std::string';
    if (enumNames.contains(base)) return base;
    if (structNames.contains(base)) return base;
    return _typeToC(base);
  }

  // ── Legacy JNI+Swift path (NativeImpl.kotlin / NativeImpl.swift) ───────────

  // ignore: avoid_positional_boolean_parameters
  static String _generateJniSwift(
    BridgeSpec spec, {
    bool includeAndroid = true,
    bool includeIos = true,
    bool iosIsCpp = false,
    bool macosIsCpp = false,
  }) {
    final writer = CodeWriter();
    final headerName = '${spec.lib.replaceAll('-', '_')}.bridge.g.h';

    writer.line('// Generated by Nitrogen Modules. Do not edit.');
    writer.line('// Generated from: ${spec.sourceUri.split('/').last}');
    writer.line('#include <stdint.h>');
    writer.line('#include <stdbool.h>');
    writer.line('#include <string.h>');
    writer.line('#include <stdlib.h>');
    // C++ standard headers needed when any Apple platform uses NativeImpl.cpp.
    if (iosIsCpp || macosIsCpp) {
      writer.line('#include <string>');
      writer.line('#include <stdexcept>');
    }
    writer.line('#include "dart_api_dl.h"');
    writer.line('#include "$headerName"');
    writer.blankLine();

    final libStem = spec.lib.replaceAll('-', '_');
    final libPkg = 'nitro/${libStem}_module';
    // Pre-build O(1) lookup sets — avoids O(n×m) .any() scans inside the
    // generation loops (functions × enums, params × structs, etc.).
    final enumNames = spec.enums.map((e) => e.name).toSet();
    final structNames = spec.structs.map((s) => s.name).toSet();
    writer.line('extern "C" {');
    writer.line('NITRO_EXPORT intptr_t ${libStem}_init_dart_api_dl(void* data) {');
    writer.line('    return Dart_InitializeApiDL(data);');
    writer.line('}');
    writer.line('}');

    writer.line('static thread_local NitroError g_nitro_error = { 0, nullptr, nullptr, nullptr, nullptr };');
    writer.blankLine();
    writer.line('extern "C" {');
    writer.line('NitroError* ${libStem}_get_error() { return &g_nitro_error; }');
    writer.line('void ${libStem}_clear_error() {');
    writer.line('    g_nitro_error.hasError = 0;');
    writer.line('    if (g_nitro_error.name) { free((void*)g_nitro_error.name); g_nitro_error.name = nullptr; }');
    writer.line('    if (g_nitro_error.message) { free((void*)g_nitro_error.message); g_nitro_error.message = nullptr; }');
    writer.line('    if (g_nitro_error.code) { free((void*)g_nitro_error.code); g_nitro_error.code = nullptr; }');
    writer.line('    if (g_nitro_error.stackTrace) { free((void*)g_nitro_error.stackTrace); g_nitro_error.stackTrace = nullptr; }');
    writer.line('}');
    writer.blankLine();
    writer.line('static void nitro_report_error(const char* name, const char* message, const char* code, const char* stack) {');
    writer.line('    ${libStem}_clear_error();');
    writer.line('    g_nitro_error.hasError = 1;');
    writer.line('    g_nitro_error.name = name ? strdup(name) : strdup("NativeException");');
    writer.line('    g_nitro_error.message = message ? strdup(message) : strdup("An unknown native exception occurred.");');
    writer.line('    g_nitro_error.code = code ? strdup(code) : nullptr;');
    writer.line('    g_nitro_error.stackTrace = stack ? strdup(stack) : nullptr;');
    writer.line('}');
    writer.line('}');
    writer.blankLine();

    // ── Struct release functions (used by NativeFinalizer in Dart proxy classes) ──
    if (spec.structs.isNotEmpty) {
      writer.line('extern "C" {');
      for (final st in spec.structs) {
        writer.line('void ${libStem}_release_${st.name}(void* ptr) {');
        writer.line('    if (!ptr) return;');
        final hasStrings = st.fields.any((f) => f.type.name == 'String');
        final hasNestedStructs = st.fields.any((f) => structNames.contains(f.type.name.replaceFirst('?', '')));
        final hasNonZcDataJni = st.fields.any((f) => f.type.isTypedData && !f.zeroCopy);
        if (hasStrings || hasNestedStructs || hasNonZcDataJni) {
          writer.line('    ${st.name}* st_ptr = (${st.name}*)ptr;');
          for (final f in st.fields) {
            if (f.type.name == 'String') {
              writer.line('    if (st_ptr->${f.name}) free((void*)st_ptr->${f.name});');
            } else if (structNames.contains(f.type.name.replaceFirst('?', ''))) {
              writer.line('    if (st_ptr->${f.name}) { free(st_ptr->${f.name}); st_ptr->${f.name} = nullptr; }');
            } else if (f.type.isTypedData && !f.zeroCopy) {
              writer.line('    if (st_ptr->${f.name}) { free(st_ptr->${f.name}); st_ptr->${f.name} = nullptr; }');
            }
          }
        }
        writer.line('    free(ptr);');
        writer.line('}');
      }
      writer.line('}');
      writer.blankLine();
    }

    // Preprocessor branch for Android JNI vs iOS Swift
    if (includeAndroid && includeIos) writer.line('#ifdef __ANDROID__');
    if (includeAndroid) {
      writer.line('#include <jni.h>');
      writer.line('#include <android/log.h>');
      writer.line(
        '#define LOGE(...) __android_log_print(ANDROID_LOG_ERROR, "Nitrogen", __VA_ARGS__)',
      );
      writer.blankLine();
      writer.line('static JavaVM* g_jvm = nullptr;');
      writer.line('static jclass g_bridgeClass = nullptr;');
      writer.blankLine();
      writer.line('// ── Cached JNI IDs (initialized once in JNI_OnLoad, safe to use from any thread) ──');
      writer.line('static jmethodID g_exc_getName = nullptr;');
      writer.line('static jmethodID g_exc_getMessage = nullptr;');
      for (final func in spec.functions) {
        writer.line('static jmethodID g_mid_${func.dartName}_call = nullptr;');
      }
      for (final prop in spec.properties) {
        if (prop.hasGetter) writer.line('static jmethodID g_mid_${prop.getSymbol}_call = nullptr;');
        if (prop.hasSetter) writer.line('static jmethodID g_mid_${prop.setSymbol}_call = nullptr;');
      }
      for (final stream in spec.streams) {
        writer.line('static jmethodID g_mid_${stream.registerSymbol}_call = nullptr;');
        writer.line('static jmethodID g_mid_${stream.releaseSymbol}_call = nullptr;');
      }
      // Record types used in streams: cache class + encode() method ID for JNI emit
      final emittedRecordGlobals = <String>{};
      for (final stream in spec.streams) {
        if (stream.itemType.isRecord) {
          final recName = stream.itemType.name.replaceFirst('?', '');
          if (emittedRecordGlobals.add(recName)) {
            writer.line('static jclass g_cls_$recName = nullptr;');
            writer.line('static jmethodID g_mid_${recName}_encode = nullptr;');
          }
        }
      }
      for (final st in spec.structs) {
        writer.line('static jclass g_cls_${st.name} = nullptr;');
        writer.line('static jmethodID g_ctor_${st.name} = nullptr;');
        for (final f in st.fields) {
          writer.line('static jfieldID g_fid_${st.name}_${f.name} = nullptr;');
        }
      }
      writer.blankLine();
      final zeroCopyStreamStructs = spec.streams
          .where((st2) => structNames.contains(st2.itemType.name.replaceFirst('?', '')))
          .map((st2) => st2.itemType.name.replaceFirst('?', ''))
          .where((name) => spec.structs.any((st3) => st3.name == name && st3.fields.any((f) => f.zeroCopy)))
          .toSet();
      if (zeroCopyStreamStructs.isNotEmpty) {
        writer.line('#include <unordered_map>');
        writer.line('#include <mutex>');
        writer.line('// GlobalRef map: keeps zero-copy struct backing objects alive until Dart finalizer fires.');
        writer.line('static std::unordered_map<void*, jobject> g_zero_copy_refs;');
        writer.line('static std::mutex g_zero_copy_refs_mtx;');
        writer.blankLine();
      }
      writer.blankLine();
      writer.line('// RAII guard: auto-detaches a thread from the JVM when it exits.');
      writer.line('// One instance is stored in thread-local storage; its destructor fires');
      writer.line('// when the thread terminates, ensuring no JVM thread descriptor leaks.');
      writer.line('struct NitroJniThreadGuard {');
      writer.line('    bool attached = false;');
      writer.line('    ~NitroJniThreadGuard() {');
      writer.line('        if (attached && g_jvm != nullptr) {');
      writer.line('            g_jvm->DetachCurrentThread();');
      writer.line('        }');
      writer.line('    }');
      writer.line('};');
      writer.line('static thread_local NitroJniThreadGuard g_thread_guard;');
      writer.blankLine();
      writer.line('static void nitro_report_jni_exception(JNIEnv* env, jthrowable ex) {');
      writer.line('    // MUST clear the pending exception before making any further JNI calls.');
      writer.line('    // JNI aborts if any JNI function (e.g. GetObjectClass) is called while');
      writer.line('    // an exception is still pending.');
      writer.line('    env->ExceptionClear();');
      writer.line('    jclass ex_class = env->GetObjectClass(ex);');
      writer.line('    jstring j_name = (jstring)env->CallObjectMethod(ex_class, g_exc_getName);');
      writer.line('    const char* name = (j_name != nullptr) ? env->GetStringUTFChars(j_name, 0) : "JavaException";');
      writer.blankLine();
      writer.line('    jstring j_msg = (jstring)env->CallObjectMethod(ex, g_exc_getMessage);');
      writer.line('    const char* msg = (j_msg != nullptr) ? env->GetStringUTFChars(j_msg, 0) : "No message provided";');
      writer.blankLine();
      writer.line('    nitro_report_error(name, msg, nullptr, nullptr);');
      writer.blankLine();
      writer.line('    if (j_name) {');
      writer.line('        env->ReleaseStringUTFChars(j_name, name);');
      writer.line('        env->DeleteLocalRef(j_name);');
      writer.line('    }');
      writer.line('    if (j_msg) {');
      writer.line('        env->ReleaseStringUTFChars(j_msg, msg);');
      writer.line('        env->DeleteLocalRef(j_msg);');
      writer.line('    }');
      writer.line('    env->DeleteLocalRef(ex_class);');
      writer.line('    env->DeleteLocalRef(ex);');
      writer.line('}');
      writer.blankLine();

      // JNI struct unpack helpers (C struct → Java object)
      for (final st in spec.structs) {
        // pack: Java object → C struct (used for stream emit and return values)
        writer.line(
          'static ${st.name} pack_${st.name}_from_jni(JNIEnv* env, jobject obj) {',
        );
        writer.line('    ${st.name} result;');
        for (final f in st.fields) {
          final isEnumField = enumNames.contains(f.type.name.replaceFirst('?', ''));
          final isZeroCopyField = _isZeroCopy(st, f.name);
          final getter = isEnumField ? 'GetLongField' : _jniGetter(f.type.name);
          if (isZeroCopyField) {
            final elemCast = _zeroCopyCElementCast(f.type.name);
            writer.line(
              '    jobject buf_${f.name} = env->GetObjectField(obj, g_fid_${st.name}_${f.name});',
            );
            // Null guard: GetDirectBufferAddress(null) returns null — assign it
            // to the struct field unchecked causes a silent null-deref on use.
            writer.line('    if (buf_${f.name} == nullptr) {');
            writer.line('        jclass npe = env->FindClass("java/lang/NullPointerException");');
            writer.line('        if (npe) env->ThrowNew(npe, "${st.name}.${f.name}: TypedData ByteBuffer is null");');
            writer.line('        return result;');
            writer.line('    }');
            writer.line(
              '    result.${f.name} = ($elemCast)env->GetDirectBufferAddress(buf_${f.name});',
            );
            writer.line('    if (result.${f.name} == nullptr) {');
            writer.line('        jclass iae = env->FindClass("java/lang/IllegalArgumentException");');
            writer.line('        if (iae) env->ThrowNew(iae, "${st.name}.${f.name}: @ZeroCopy requires ByteBuffer.allocateDirect() — heap-backed ByteBuffer.wrap() is not supported.");');
            writer.line('        env->DeleteLocalRef(buf_${f.name});');
            writer.line('        return result;');
            writer.line('    }');
            // When no explicit companion length field exists, auto-populate the
            // synthesized '${field}Length' field from the ByteBuffer's capacity.
            if (_zeroCopyNeedsSynthetic(st, f.name)) {
              final divisor = _elementSizeDivisorExpr(f.type.name);
              writer.line(
                '    result.${f.name}Length = (int64_t)env->GetDirectBufferCapacity(buf_${f.name})$divisor;',
              );
            }
            writer.line('    env->DeleteLocalRef(buf_${f.name});');
          } else if (f.type.name == 'String') {
            writer.line(
              '    jstring j_${f.name} = (jstring)env->GetObjectField(obj, g_fid_${st.name}_${f.name});',
            );
            writer.line(
              '    const char* str_${f.name} = (j_${f.name} != nullptr) ? env->GetStringUTFChars(j_${f.name}, 0) : "";',
            );
            writer.line('    result.${f.name} = strdup(str_${f.name});');
            writer.line('    if (j_${f.name}) {');
            writer.line('        env->ReleaseStringUTFChars(j_${f.name}, str_${f.name});');
            writer.line('        env->DeleteLocalRef(j_${f.name});');
            writer.line('    }');
          } else if (isEnumField) {
            final enumType = f.type.name.replaceFirst('?', '');
            writer.line(
              '    result.${f.name} = ($enumType)(int32_t)env->$getter(obj, g_fid_${st.name}_${f.name});',
            );
          } else if (structNames.contains(f.type.name.replaceFirst('?', ''))) {
            final nestedType = f.type.name.replaceFirst('?', '');
            writer.line('    jobject j_${f.name} = env->GetObjectField(obj, g_fid_${st.name}_${f.name});');
            writer.line('    $nestedType* ${f.name}_ptr = ($nestedType*)malloc(sizeof($nestedType));');
            writer.line('    *${f.name}_ptr = pack_${nestedType}_from_jni(env, j_${f.name});');
            writer.line('    env->DeleteLocalRef(j_${f.name});');
            writer.line('    result.${f.name} = ${f.name}_ptr;');
          } else if (f.type.isTypedData) {
            // Non-zero-copy typed data: extract Java array bytes into a malloc'd C buffer.
            final ops = _typedDataJniOps(f.type.name);
            final cElemType = _zeroCopyCElementCast(f.type.name).replaceAll('*', '').trim();
            writer.line('    ${ops[0]} j_${f.name} = (${ops[0]})env->GetObjectField(obj, g_fid_${st.name}_${f.name});');
            writer.line('    jsize _len_${f.name} = (j_${f.name} != nullptr) ? env->GetArrayLength(j_${f.name}) : 0;');
            writer.line('    result.${f.name} = nullptr;');
            if (_zeroCopyNeedsSynthetic(st, f.name)) {
              writer.line('    result.${f.name}Length = 0;');
            }
            writer.line('    if (j_${f.name} != nullptr && _len_${f.name} > 0) {');
            writer.line('        result.${f.name} = ($cElemType*)malloc(_len_${f.name} * sizeof($cElemType));');
            writer.line('        env->Get${ops[2].replaceFirst('Set', '')}(j_${f.name}, 0, _len_${f.name}, (${ops[3]}*)result.${f.name});');
            if (_zeroCopyNeedsSynthetic(st, f.name)) {
              writer.line('        result.${f.name}Length = (int64_t)_len_${f.name};');
            }
            writer.line('        env->DeleteLocalRef(j_${f.name});');
            writer.line('    }');
          } else {
            writer.line('    result.${f.name} = env->$getter(obj, g_fid_${st.name}_${f.name});');
          }
        }
        writer.line('    return result;');
        writer.line('}');

        // unpack: C struct → Java object (used for passing struct params to Kotlin)
        writer.line(
          'static jobject unpack_${st.name}_to_jni(JNIEnv* env, const ${st.name}* st) {',
        );
        // Pre-compute zero-copy ByteBuffer objects with null guards.
        // NewDirectByteBuffer(nullptr, n) is undefined behaviour in JNI — guard
        // every zeroCopy pointer before passing it to the constructor.
        for (final f in st.fields) {
          if (!_isZeroCopy(st, f.name)) continue;
          final lenField = _zeroCopyLenField(st, f.name);
          final elemSize = _zeroCopyElementSizeExpr(f.type.name);
          writer.line('    if (st->${f.name} == nullptr) {');
          writer.line('        jclass npe = env->FindClass("java/lang/NullPointerException");');
          writer.line('        if (npe) env->ThrowNew(npe, "${st.name}.${f.name}: TypedData pointer is null");');
          writer.line('        return nullptr;');
          writer.line('    }');
          // NewDirectByteBuffer takes a BYTE count, not element count.
          writer.line('    jobject dbuf_${f.name} = env->NewDirectByteBuffer((void*)st->${f.name}, st->$lenField$elemSize);');
        }
        for (final f in st.fields) {
          if (_isZeroCopy(st, f.name)) continue;
          if (f.type.name == 'String') {
            writer.line('    jstring j_${f.name} = env->NewStringUTF(st->${f.name} ? st->${f.name} : "");');
          }
        }
        for (final f in st.fields) {
          if (_isZeroCopy(st, f.name)) continue;
          if (!structNames.contains(f.type.name.replaceFirst('?', ''))) continue;
          final nestedType = f.type.name.replaceFirst('?', '');
          writer.line('    jobject j_${f.name} = unpack_${nestedType}_to_jni(env, st->${f.name});');
        }
        // Pre-compute non-zero-copy typed data: create Java arrays from C buffers.
        for (final f in st.fields) {
          if (_isZeroCopy(st, f.name) || !f.type.isTypedData) continue;
          final ops = _typedDataJniOps(f.type.name);
          final lenField = _zeroCopyLenField(st, f.name);
          writer.line('    ${ops[0]} j_${f.name} = env->${ops[1]}((jsize)st->$lenField);');
          writer.line('    if (j_${f.name} != nullptr && st->${f.name} != nullptr) {');
          writer.line('        env->${ops[2]}(j_${f.name}, 0, (jsize)st->$lenField, (const ${ops[3]}*)st->${f.name});');
          writer.line('    }');
        }
        final ctorArgs = st.fields
            .map((f) {
              final isEnum = enumNames.contains(f.type.name.replaceFirst('?', ''));
              final isNestedStruct = structNames.contains(f.type.name.replaceFirst('?', ''));
              if (_isZeroCopy(st, f.name)) {
                return 'dbuf_${f.name}';
              } else if (f.type.isTypedData) {
                return 'j_${f.name}'; // non-ZC typed data → Java array created above
              } else if (f.type.name == 'String') {
                return 'j_${f.name}';
              } else if (isEnum) {
                return '(jlong)(int32_t)st->${f.name}';
              } else if (isNestedStruct) {
                return 'j_${f.name}';
              } else {
                return '(${_jniCast(f.type.name)})st->${f.name}';
              }
            })
            .join(', ');
        writer.line('    jobject result = env->NewObject(g_cls_${st.name}, g_ctor_${st.name}, $ctorArgs);');
        for (final f in st.fields) {
          if (_isZeroCopy(st, f.name)) {
            writer.line('    if (dbuf_${f.name}) env->DeleteLocalRef(dbuf_${f.name});');
          } else if (!_isZeroCopy(st, f.name) && f.type.isTypedData) {
            writer.line('    if (j_${f.name}) env->DeleteLocalRef(j_${f.name});');
          } else if (f.type.name == 'String') {
            writer.line('    if (j_${f.name}) env->DeleteLocalRef(j_${f.name});');
          } else if (structNames.contains(f.type.name.replaceFirst('?', ''))) {
            writer.line('    if (j_${f.name}) env->DeleteLocalRef(j_${f.name});');
          }
        }
        writer.line('    return result;');
        writer.line('}');
      }
      writer.blankLine();

      writer.line('extern "C" {');
      writer.blankLine();
      writer.line(
        'JNIEXPORT jint JNICALL JNI_OnLoad(JavaVM* vm, void* reserved) {',
      );
      writer.line('    g_jvm = vm;');
      writer.line(
        '    __android_log_print(ANDROID_LOG_INFO, "Nitrogen", "JNI_OnLoad called for ${spec.lib}");',
      );
      writer.line('    JNIEnv* env = nullptr;');
      writer.line(
        '    if (vm->GetEnv(reinterpret_cast<void**>(&env), JNI_VERSION_1_6) != JNI_OK) {',
      );
      writer.line('        return -1;');
      writer.line('    }');
      writer.line(
        '    // Cache standard-library method IDs only — system class loader is always',
      );
      writer.line(
        '    // available here. Application class IDs are deferred to initialize().',
      );
      writer.line('    {');
      writer.line('        jclass cls_class = env->FindClass("java/lang/Class");');
      writer.line('        if (cls_class) { g_exc_getName = env->GetMethodID(cls_class, "getName", "()Ljava/lang/String;"); env->DeleteLocalRef(cls_class); }');
      writer.line('        jclass throwable_class = env->FindClass("java/lang/Throwable");');
      writer.line('        if (throwable_class) { g_exc_getMessage = env->GetMethodID(throwable_class, "getMessage", "()Ljava/lang/String;"); env->DeleteLocalRef(throwable_class); }');
      writer.line('    }');
      writer.line('    return JNI_VERSION_1_6;');
      writer.line('}');
      writer.blankLine();
      writer.line('static JNIEnv* GetEnv() {');
      writer.line('    if (g_jvm == nullptr) return nullptr;');
      writer.line('    JNIEnv* env = nullptr;');
      writer.line(
        '    int status = g_jvm->GetEnv(reinterpret_cast<void**>(&env), JNI_VERSION_1_6);',
      );
      writer.line('    if (status == JNI_EDETACHED) {');
      writer.line('        g_jvm->AttachCurrentThread(&env, nullptr);');
      writer.line('        g_thread_guard.attached = true; // will DetachCurrentThread on thread exit');
      writer.line('    }');
      writer.line('    return env;');
      writer.line('}');
      writer.blankLine();

      // ── Functions ─────────────────────────────────────────────────────────────
      for (final func in spec.functions) {
        // ── @nitroNativeAsync: void + dart_port, delegates to CallStaticVoidMethod ──
        if (func.isNativeAsync) {
          final paramsDeclParts = <String>[];
          for (final p in func.params) {
            paramsDeclParts.add('${_paramTypeToC(p.type.name, structNames)} ${p.name}');
            if (p.type.isTypedData) paramsDeclParts.add('int64_t ${p.name}_length');
          }
          paramsDeclParts.add('int64_t dart_port');
          final paramsDecl = paramsDeclParts.join(', ');
          final jniNativeAsyncSig = _jniNativeAsyncSig(func.params, structNames, libPkg);

          writer.line('void ${func.cSymbol}($paramsDecl) {');
          writer.line('    JNIEnv* env = GetEnv();');
          writer.line('    if (env == nullptr) return;');
          writer.line('    jmethodID methodId = g_mid_${func.dartName}_call;');
          writer.line('    if (methodId == nullptr) { LOGE("Method not found: ${func.dartName}_call sig=$jniNativeAsyncSig"); return; }');
          writer.blankLine();
          writer.line('    ${libStem}_clear_error();');
          writer.line('    if (env->PushLocalFrame(16) != 0) return;');

          final callArgsList = <String>[];
          for (final p in func.params) {
            final pt = p.type.name;
            if (pt == 'String') {
              writer.line('    jstring j_${p.name} = env->NewStringUTF(${p.name});');
              callArgsList.add('j_${p.name}');
            } else if (pt == 'String?') {
              writer.line('    jstring j_${p.name} = (${p.name} != nullptr) ? env->NewStringUTF(${p.name}) : nullptr;');
              callArgsList.add('j_${p.name}');
            } else if (structNames.contains(pt.replaceFirst('?', ''))) {
              final baseType = pt.replaceFirst('?', '');
              if (pt.endsWith('?')) {
                writer.line('    jobject jobj_${p.name} = (${p.name} != nullptr) ? unpack_${baseType}_to_jni(env, (const $baseType*)${p.name}) : nullptr;');
              } else {
                writer.line('    jobject jobj_${p.name} = unpack_${baseType}_to_jni(env, (const $baseType*)${p.name});');
              }
              callArgsList.add('jobj_${p.name}');
            } else if (p.zeroCopy && p.type.isTypedData) {
              final elemSize = _zeroCopyElementSizeExpr(pt);
              writer.line('    jobject j_${p.name} = env->NewDirectByteBuffer(${p.name}, ${p.name}_length$elemSize);');
              callArgsList.add('j_${p.name}');
            } else if (!p.zeroCopy && p.type.isTypedData) {
              final ops = _typedDataJniOps(pt);
              writer.line('    ${ops[0]} j_${p.name} = env->${ops[1]}((jsize)${p.name}_length);');
              writer.line('    env->${ops[2]}(j_${p.name}, 0, (jsize)${p.name}_length, (const ${ops[3]}*)${p.name});');
              callArgsList.add('j_${p.name}');
            } else {
              callArgsList.add(p.name);
            }
          }
          callArgsList.add('(jlong)dart_port');
          final callArgs = callArgsList.join(', ');
          writer.line('    env->CallStaticVoidMethod(g_bridgeClass, methodId, $callArgs);');
          writer.line('    if (env->ExceptionCheck()) {');
          writer.line('        nitro_report_jni_exception(env, env->ExceptionOccurred());');
          writer.line('    }');
          writer.line('    env->PopLocalFrame(nullptr);');
          writer.line('}');
          writer.blankLine();
          continue;
        }

        final isEnum = enumNames.contains(func.returnType.name);
        final isStruct = structNames.contains(func.returnType.name);
        final isRecord = func.returnType.isRecord && !func.returnType.isMap;
        // For enum returns: bridge returns Long (nativeValue); C returns int64_t
        // For struct returns: bridge returns jobject; C packs to C struct via malloc
        // For record returns: bridge returns ByteArray; C copies bytes to malloc'd buffer
        final cReturnType = isEnum ? 'int64_t' : _typeToC(func.returnType.name);
        final paramsDeclParts = <String>[];
        for (final p in func.params) {
          paramsDeclParts.add('${_paramTypeToC(p.type.name, structNames)} ${p.name}');
          if (p.type.isTypedData) paramsDeclParts.add('int64_t ${p.name}_length');
        }
        final paramsDecl = paramsDeclParts.join(', ');

        writer.line(
          '$cReturnType ${func.cSymbol}(${paramsDecl.isEmpty ? 'void' : paramsDecl}) {',
        );
        writer.line('    JNIEnv* env = GetEnv();');
        if (func.returnType.name == 'void') {
          writer.line('    if (env == nullptr) return;');
        } else {
          writer.line(
            '    if (env == nullptr) return ${_defaultValue(cReturnType)};',
          );
        }
        writer.line('    jmethodID methodId = g_mid_${func.dartName}_call;');
        final jniSigForLog = _jniSig(func.params, func.returnType, enumNames, structNames, libPkg);
        if (func.returnType.name == 'void') {
          writer.line('    if (methodId == nullptr) { LOGE("Method not found: ${func.dartName}_call sig=$jniSigForLog"); return; }');
        } else {
          writer.line('    if (methodId == nullptr) { LOGE("Method not found: ${func.dartName}_call sig=$jniSigForLog"); return ${_defaultValue(cReturnType)}; }');
        }
        writer.blankLine();
        writer.line('    ${libStem}_clear_error();');
        if (func.returnType.name == 'void') {
          writer.line('    if (env->PushLocalFrame(16) != 0) return;');
        } else {
          writer.line('    if (env->PushLocalFrame(16) != 0) return ${_defaultValue(cReturnType)};');
        }

        // Build call args (converting C types to JNI types)
        final callArgsList = <String>[];
        for (final p in func.params) {
          final pt = p.type.name;
          if (pt == 'String') {
            writer.line('    jstring j_${p.name} = env->NewStringUTF(${p.name});');
            callArgsList.add('j_${p.name}');
          } else if (pt == 'String?') {
            writer.line('    jstring j_${p.name} = (${p.name} != nullptr) ? env->NewStringUTF(${p.name}) : nullptr;');
            callArgsList.add('j_${p.name}');
          } else if (structNames.contains(pt.replaceFirst('?', ''))) {
            final baseType = pt.replaceFirst('?', '');
            if (pt.endsWith('?')) {
              writer.line(
                '    jobject jobj_${p.name} = (${p.name} != nullptr) ? unpack_${baseType}_to_jni(env, (const $baseType*)${p.name}) : nullptr;',
              );
            } else {
              writer.line(
                '    jobject jobj_${p.name} = unpack_${baseType}_to_jni(env, (const $baseType*)${p.name});',
              );
            }
            callArgsList.add('jobj_${p.name}');
          } else if (p.zeroCopy && p.type.isTypedData) {
            final elemSize = _zeroCopyElementSizeExpr(pt);
            writer.line('    jobject j_${p.name} = env->NewDirectByteBuffer(${p.name}, ${p.name}_length$elemSize);');
            callArgsList.add('j_${p.name}');
          } else if (!p.zeroCopy && p.type.isTypedData) {
            final ops = _typedDataJniOps(pt);
            writer.line('    ${ops[0]} j_${p.name} = env->${ops[1]}((jsize)${p.name}_length);');
            writer.line('    env->${ops[2]}(j_${p.name}, 0, (jsize)${p.name}_length, (const ${ops[3]}*)${p.name});');
            callArgsList.add('j_${p.name}');
          } else {
            callArgsList.add(p.name);
          }
        }

        final callArgs = callArgsList.join(', ');
        final bridgeArgs = callArgs.isEmpty ? '' : ', $callArgs';

        if (func.returnType.name == 'void') {
          writer.line(
            '    env->CallStaticVoidMethod(g_bridgeClass, methodId$bridgeArgs);',
          );
          writer.line('    env->PopLocalFrame(nullptr);');
        } else if (func.returnType.name == 'double' || func.returnType.name == 'double?') {
          writer.line(
            '    double res = env->CallStaticDoubleMethod(g_bridgeClass, methodId$bridgeArgs);',
          );
          writer.line('    if (env->ExceptionCheck()) {');
          writer.line('        nitro_report_jni_exception(env, env->ExceptionOccurred());');
          writer.line('        env->PopLocalFrame(nullptr);');
          writer.line('        return 0.0;');
          writer.line('    }');
          writer.line('    env->PopLocalFrame(nullptr);');
          writer.line('    return res;');
        } else if (func.returnType.name == 'int' || func.returnType.name == 'int?') {
          writer.line(
            '    int64_t res = env->CallStaticLongMethod(g_bridgeClass, methodId$bridgeArgs);',
          );
          writer.line('    if (env->ExceptionCheck()) {');
          writer.line('        nitro_report_jni_exception(env, env->ExceptionOccurred());');
          writer.line('        env->PopLocalFrame(nullptr);');
          writer.line('        return 0;');
          writer.line('    }');
          writer.line('    env->PopLocalFrame(nullptr);');
          writer.line('    return res;');
        } else if (func.returnType.name == 'bool' || func.returnType.name == 'bool?') {
          writer.line(
            '    bool res = env->CallStaticBooleanMethod(g_bridgeClass, methodId$bridgeArgs);',
          );
          writer.line('    if (env->ExceptionCheck()) {');
          writer.line('        nitro_report_jni_exception(env, env->ExceptionOccurred());');
          writer.line('        env->PopLocalFrame(nullptr);');
          writer.line('        return false;');
          writer.line('    }');
          writer.line('    env->PopLocalFrame(nullptr);');
          writer.line('    return res;');
        } else if (func.returnType.name == 'String' || func.returnType.name == 'String?') {
          writer.line(
            '    jstring jstr = (jstring)env->CallStaticObjectMethod(g_bridgeClass, methodId$bridgeArgs);',
          );
          writer.line('    if (env->ExceptionCheck()) {');
          writer.line('        nitro_report_jni_exception(env, env->ExceptionOccurred());');
          writer.line('        env->PopLocalFrame(nullptr);');
          writer.line('        return nullptr;');
          writer.line('    }');
          writer.line('    if (jstr == nullptr) {');
          writer.line('        env->PopLocalFrame(nullptr);');
          writer.line('        return nullptr;');
          writer.line('    }');
          writer.line(
            '    const char* nativeStr = env->GetStringUTFChars(jstr, 0);',
          );
          writer.line('    char* result = strdup(nativeStr);');
          writer.line('    env->ReleaseStringUTFChars(jstr, nativeStr);');
          writer.line('    env->PopLocalFrame(nullptr);');
          writer.line('    return result;');
        } else if (isEnum) {
          // Bridge returns Long (nativeValue)
          writer.line(
            '    int64_t res = env->CallStaticLongMethod(g_bridgeClass, methodId$bridgeArgs);',
          );
          writer.line('    if (env->ExceptionCheck()) {');
          writer.line('        nitro_report_jni_exception(env, env->ExceptionOccurred());');
          writer.line('        env->PopLocalFrame(nullptr);');
          writer.line('        return 0;');
          writer.line('    }');
          writer.line('    env->PopLocalFrame(nullptr);');
          writer.line('    return res;');
        } else if (isStruct) {
          // Bridge returns the Kotlin data class; pack it to C struct via malloc
          final stName = func.returnType.name;
          writer.line(
            '    jobject jobj = env->CallStaticObjectMethod(g_bridgeClass, methodId$bridgeArgs);',
          );
          writer.line('    if (env->ExceptionCheck()) {');
          writer.line('        nitro_report_jni_exception(env, env->ExceptionOccurred());');
          writer.line('        env->PopLocalFrame(nullptr);');
          writer.line('        return nullptr;');
          writer.line('    }');
          writer.line('    if (jobj == nullptr) {');
          writer.line('        env->PopLocalFrame(nullptr);');
          writer.line('        return nullptr;');
          writer.line('    }');
          writer.line('    $stName* result = ($stName*)malloc(sizeof($stName));');
          writer.line('    *result = pack_${stName}_from_jni(env, jobj);');
          writer.line('    env->PopLocalFrame(nullptr);');
          writer.line('    return result;');
        } else if (isRecord) {
          // Bridge returns ByteArray (serialized @HybridRecord / List<@HybridRecord>)
          // Copy bytes to malloc'd buffer and return as void* for Dart RecordReader
          writer.line('    jbyteArray jarr = (jbyteArray)env->CallStaticObjectMethod(g_bridgeClass, methodId$bridgeArgs);');
          writer.line('    if (env->ExceptionCheck()) {');
          writer.line('        nitro_report_jni_exception(env, env->ExceptionOccurred());');
          writer.line('        env->PopLocalFrame(nullptr);');
          writer.line('        return nullptr;');
          writer.line('    }');
          writer.line('    if (jarr == nullptr) {');
          writer.line('        env->PopLocalFrame(nullptr);');
          writer.line('        return nullptr;');
          writer.line('    }');
          writer.line('    jsize len = env->GetArrayLength(jarr);');
          writer.line('    uint8_t* result = (uint8_t*)malloc(len);');
          writer.line('    env->GetByteArrayRegion(jarr, 0, len, (jbyte*)result);');
          writer.line('    env->PopLocalFrame(nullptr);');
          writer.line('    return result;');
        } else {
          writer.line('    env->PopLocalFrame(nullptr);');
          writer.line('    return ${_defaultValue(cReturnType)};');
        }
        if (func.returnType.name == 'void') {
          writer.line('    if (env->ExceptionCheck()) { nitro_report_jni_exception(env, env->ExceptionOccurred()); }');
        }
        writer.line('}');
        writer.blankLine();
      }

      // ── Properties ────────────────────────────────────────────────────────────
      for (final prop in spec.properties) {
        final isEnum = enumNames.contains(prop.type.name);
        final cType = isEnum ? 'int64_t' : _typeToC(prop.type.name);

        if (prop.hasGetter) {
          writer.line('$cType ${prop.getSymbol}(void) {');
          writer.line('    JNIEnv* env = GetEnv();');
          writer.line('    if (env == nullptr) return ${_defaultValue(cType)};');
          writer.line('    jmethodID methodId = g_mid_${prop.getSymbol}_call;');
          final jniGetSig = '()${isEnum ? 'J' : _jniSigType(prop.type.name)}';
          writer.line(
            '    if (methodId == nullptr) { LOGE("Method not found: ${prop.getSymbol}_call sig=$jniGetSig"); return ${_defaultValue(cType)}; }',
          );
          writer.line('    if (env->PushLocalFrame(8) != 0) return ${_defaultValue(cType)};');
          if (prop.type.name == 'double') {
            writer.line(
              '    double res = env->CallStaticDoubleMethod(g_bridgeClass, methodId);',
            );
            writer.line('    env->PopLocalFrame(nullptr);');
            writer.line('    return res;');
          } else if (prop.type.name == 'int' || isEnum) {
            writer.line(
              '    $cType res = ($cType)env->CallStaticLongMethod(g_bridgeClass, methodId);',
            );
            writer.line('    env->PopLocalFrame(nullptr);');
            writer.line('    return res;');
          } else if (prop.type.name == 'bool') {
            writer.line(
              '    bool res = env->CallStaticBooleanMethod(g_bridgeClass, methodId);',
            );
            writer.line('    env->PopLocalFrame(nullptr);');
            writer.line('    return res;');
          } else if (prop.type.name == 'String') {
            writer.line(
              '    jstring jstr = (jstring)env->CallStaticObjectMethod(g_bridgeClass, methodId);',
            );
            writer.line('    if (jstr == nullptr) { env->PopLocalFrame(nullptr); return nullptr; }');
            writer.line(
              '    const char* nativeStr = env->GetStringUTFChars(jstr, 0);',
            );
            writer.line('    char* result = strdup(nativeStr);');
            writer.line('    env->ReleaseStringUTFChars(jstr, nativeStr);');
            writer.line('    env->PopLocalFrame(nullptr);');
            writer.line('    return result;');
          } else {
            writer.line('    env->PopLocalFrame(nullptr);');
            writer.line('    return ${_defaultValue(cType)};');
          }
          writer.line('}');
          writer.blankLine();
        }

        if (prop.hasSetter) {
          final paramCType = isEnum ? 'int64_t' : _typeToC(prop.type.name);
          writer.line('void ${prop.setSymbol}($paramCType value) {');
          writer.line('    JNIEnv* env = GetEnv();');
          writer.line('    if (env == nullptr) return;');
          writer.line('    jmethodID methodId = g_mid_${prop.setSymbol}_call;');
          final jniSetSig = '(${isEnum ? 'J' : _jniSigType(prop.type.name)})V';
          writer.line(
            '    if (methodId == nullptr) { LOGE("Method not found: ${prop.setSymbol}_call sig=$jniSetSig"); return; }',
          );
          writer.line('    if (env->PushLocalFrame(8) != 0) return;');
          if (prop.type.name == 'String') {
            writer.line('    jstring jval = env->NewStringUTF(value);');
            writer.line(
              '    env->CallStaticVoidMethod(g_bridgeClass, methodId, jval);',
            );
          } else if (structNames.contains(prop.type.name)) {
            writer.line('    jobject jval = unpack_${prop.type.name}_to_jni(env, (const ${prop.type.name}*)value);');
            writer.line('    env->CallStaticVoidMethod(g_bridgeClass, methodId, jval);');
          } else {
            writer.line(
              '    env->CallStaticVoidMethod(g_bridgeClass, methodId, value);',
            );
          }
          writer.line('    env->PopLocalFrame(nullptr);');
          writer.line('}');
          writer.blankLine();
        }
      }

      // ── Streams ───────────────────────────────────────────────────────────────
      for (final stream in spec.streams) {
        final isStruct = structNames.contains(stream.itemType.name);
        // JNI name: "nitro" + "_" + "{lib}_module" (with internal _ → _1)
        // e.g. nitro.my_camera_module → nitro_my_1camera_1module (NOT nitro_1my_1camera_1module)

        writer.line('void ${stream.registerSymbol}(int64_t dart_port) {');
        writer.line('    JNIEnv* env = GetEnv();');
        writer.line('    if (env == nullptr) return;');
        writer.line('    jmethodID methodId = g_mid_${stream.registerSymbol}_call;');
        writer.line('    if (methodId == nullptr) { LOGE("Method not found: ${stream.registerSymbol}_call sig=(J)V"); return; }');
        writer.line('    env->CallStaticVoidMethod(g_bridgeClass, methodId, dart_port);');
        writer.line('}');
        writer.blankLine();
        writer.line('void ${stream.releaseSymbol}(int64_t dart_port) {');
        writer.line('    JNIEnv* env = GetEnv();');
        writer.line('    if (env == nullptr) return;');
        writer.line('    jmethodID methodId = g_mid_${stream.releaseSymbol}_call;');
        writer.line('    if (methodId == nullptr) { LOGE("Method not found: ${stream.releaseSymbol}_call sig=(J)V"); return; }');
        writer.line('    env->CallStaticVoidMethod(g_bridgeClass, methodId, dart_port);');
        writer.line('}');
        writer.blankLine();

        final jniEmit = _jniMethodName(
          spec.lib,
          spec.dartClassName,
          'emit_${stream.dartName}',
        );
        writer.line(
          'JNIEXPORT void JNICALL $jniEmit(JNIEnv* env, jobject thiz, jlong dartPort, ${_jniSigTypeC(stream.itemType.name)} item) {',
        );
        writer.line('    Dart_CObject obj;');
        if (stream.itemType.name == 'double') {
          writer.line('    obj.type = Dart_CObject_kDouble;');
          writer.line('    obj.value.as_double = item;');
        } else if (stream.itemType.name == 'int') {
          writer.line('    obj.type = Dart_CObject_kInt64;');
          writer.line('    obj.value.as_int64 = item;');
        } else if (stream.itemType.name == 'bool') {
          writer.line('    obj.type = Dart_CObject_kBool;');
          writer.line('    obj.value.as_bool = item;');
        } else if (isStruct) {
          final stName = stream.itemType.name.replaceFirst('?', '');
          writer.line(
            '    $stName* st_ptr = ($stName*)malloc(sizeof($stName));',
          );
          writer.line(
            '    *st_ptr = pack_${stream.itemType.name}_from_jni(env, item);',
          );
          writer.line('    // Check if pack_ threw an exception (e.g., heap ByteBuffer for @ZeroCopy)');
          writer.line('    if (env->ExceptionCheck()) {');
          writer.line('        env->ExceptionDescribe();');
          writer.line('        env->ExceptionClear();');
          writer.line('        free(st_ptr);');
          writer.line('        return;');
          writer.line('    }');
          writer.line('    obj.type = Dart_CObject_kInt64;');
          writer.line('    obj.value.as_int64 = (intptr_t)st_ptr;');
          final structDef = spec.structs.firstWhere((st3) => st3.name == stName, orElse: () => spec.structs.first);
          final hasZeroCopy = structDef.fields.any((f) => f.zeroCopy);
          if (hasZeroCopy) {
            writer.line('    // Keep item alive so JVM does not GC the ByteBuffer backing st_ptr\'s zero-copy fields.');
            writer.line('    {');
            writer.line('        std::lock_guard<std::mutex> _lk(g_zero_copy_refs_mtx);');
            writer.line('        g_zero_copy_refs[(void*)st_ptr] = env->NewGlobalRef(item);');
            writer.line('    }');
          }
        } else if (stream.itemType.isRecord) {
          // Serialize the Kotlin @HybridRecord to bytes via encode(), copy to a
          // malloc'd native buffer, and send the pointer as kInt64.
          // Dart reads it via RecordReader.fromNative and frees with malloc.free.
          final recName = stream.itemType.name.replaceFirst('?', '');
          writer.line('    if (g_cls_$recName == nullptr || g_mid_${recName}_encode == nullptr) {');
          writer.line('        LOGE("$recName encode method not cached — skipping emit");');
          writer.line('        return;');
          writer.line('    }');
          writer.line('    jbyteArray encoded = (jbyteArray)env->CallObjectMethod(item, g_mid_${recName}_encode);');
          writer.line('    if (encoded == nullptr) { LOGE("$recName.encode() returned null"); return; }');
          writer.line('    jsize len = env->GetArrayLength(encoded);');
          writer.line('    uint8_t* buf = (uint8_t*)malloc((size_t)len);');
          writer.line('    env->GetByteArrayRegion(encoded, 0, len, (jbyte*)buf);');
          writer.line('    env->DeleteLocalRef(encoded);');
          writer.line('    obj.type = Dart_CObject_kInt64;');
          writer.line('    obj.value.as_int64 = (intptr_t)buf;');
        } else if (enumNames.contains(stream.itemType.name.replaceFirst('?', ''))) {
          writer.line('    // item is a jobject (Kotlin enum). Extract its nativeValue Long field.');
          writer.line('    jclass enumCls = env->GetObjectClass(item);');
          writer.line('    jfieldID fid = enumCls ? env->GetFieldID(enumCls, "nativeValue", "J") : nullptr;');
          writer.line('    if (fid == nullptr) { LOGE("emit_${stream.dartName}: cannot find nativeValue on ${stream.itemType.name}"); if (enumCls) env->DeleteLocalRef(enumCls); return; }');
          writer.line('    obj.type = Dart_CObject_kInt64;');
          writer.line('    obj.value.as_int64 = (int64_t)env->GetLongField(item, fid);');
          writer.line('    env->DeleteLocalRef(enumCls);');
        } else {
          writer.line('    obj.type = Dart_CObject_kNull;');
        }
        writer.line('    Dart_PostCObject_DL(dartPort, &obj);');
        writer.line('}');
        writer.blankLine();
      }

      final jniInit = _jniMethodName(spec.lib, spec.dartClassName, 'initialize');
      writer.line(
        'JNIEXPORT void JNICALL $jniInit(JNIEnv* env, jobject thiz, jclass localClass) {',
      );
      writer.line('    if (g_bridgeClass == nullptr) {');
      writer.line(
        '        g_bridgeClass = (jclass)env->NewGlobalRef(localClass);',
      );
      writer.line('        env->DeleteLocalRef(localClass);');
      writer.line('    }');
      writer.line(
        '    // Re-cache method IDs every time (safe; idempotent; works even if JNI_OnLoad',
      );
      writer.line(
        '    // could not find the app class. initialize() is called from Kotlin with the',
      );
      writer.line(
        '    // correct class loader.)',
      );
      writer.line('    if (g_bridgeClass != nullptr) {');
      writer.line('        // Cache bridge method IDs');
      for (final func in spec.functions) {
        final String jniSig;
        if (func.isNativeAsync) {
          jniSig = _jniNativeAsyncSig(func.params, structNames, libPkg);
        } else {
          jniSig = _jniSig(func.params, func.returnType, enumNames, structNames, libPkg);
        }
        writer.line('        g_mid_${func.dartName}_call = env->GetStaticMethodID(g_bridgeClass, "${func.dartName}_call", "$jniSig");');
      }
      for (final prop in spec.properties) {
        final isEnum = enumNames.contains(prop.type.name);
        if (prop.hasGetter) {
          final jniRetSig = isEnum ? 'J' : _jniSigType(prop.type.name);
          writer.line('        g_mid_${prop.getSymbol}_call = env->GetStaticMethodID(g_bridgeClass, "${prop.getSymbol}_call", "()$jniRetSig");');
        }
        if (prop.hasSetter) {
          final jniParamSig = isEnum ? 'J' : _jniSigType(prop.type.name);
          writer.line('        g_mid_${prop.setSymbol}_call = env->GetStaticMethodID(g_bridgeClass, "${prop.setSymbol}_call", "($jniParamSig)V");');
        }
      }
      for (final stream in spec.streams) {
        writer.line('        g_mid_${stream.registerSymbol}_call = env->GetStaticMethodID(g_bridgeClass, "${stream.registerSymbol}_call", "(J)V");');
        writer.line('        g_mid_${stream.releaseSymbol}_call = env->GetStaticMethodID(g_bridgeClass, "${stream.releaseSymbol}_call", "(J)V");');
      }
      writer.line('    }');
      writer.blankLine();

      // Cache record class + encode() method IDs for record-typed stream items
      final cachedRecordClasses = <String>{};
      for (final stream in spec.streams) {
        if (stream.itemType.isRecord) {
          final recName = stream.itemType.name.replaceFirst('?', '');
          if (cachedRecordClasses.add(recName)) {
            final jniRecClass = 'nitro/${spec.lib.replaceAll('-', '_')}_module/$recName';
            writer.line('    // Cache $recName class + encode() for stream serialisation');
            writer.line('    {');
            writer.line('        jclass local_cls_$recName = env->FindClass("$jniRecClass");');
            writer.line('        if (local_cls_$recName != nullptr) {');
            writer.line('            g_cls_$recName = (jclass)env->NewGlobalRef(local_cls_$recName);');
            writer.line('            env->DeleteLocalRef(local_cls_$recName);');
            writer.line('            g_mid_${recName}_encode = env->GetMethodID(g_cls_$recName, "encode", "()[B");');
            writer.line('        } else {');
            writer.line('            LOGE("Failed to find class $jniRecClass");');
            writer.line('        }');
            writer.line('    }');
          }
        }
      }
      if (spec.structs.isNotEmpty) {
        writer.line('    // Cache struct class + ctor + field IDs');
        for (final st in spec.structs) {
          final jniClass = 'nitro/${spec.lib.replaceAll('-', '_')}_module/${st.name}';

          final ctorSig =
              '(${st.fields.map((f) {
                final isEnum = enumNames.contains(f.type.name.replaceFirst('?', ''));
                final isNestedStruct = structNames.contains(f.type.name.replaceFirst('?', ''));
                if (isEnum) return 'J';
                if (_isZeroCopy(st, f.name)) return 'Ljava/nio/ByteBuffer;';
                if (isNestedStruct) return 'L$libPkg/${f.type.name.replaceFirst('?', '')};';
                return _jniSigType(f.type.name);
              }).join('')})V';
          writer.line('    {');
          writer.line('        jclass local_cls_${st.name} = env->FindClass("$jniClass");');
          writer.line('        if (local_cls_${st.name} != nullptr) {');
          writer.line('            g_cls_${st.name} = (jclass)env->NewGlobalRef(local_cls_${st.name});');
          writer.line('            env->DeleteLocalRef(local_cls_${st.name});');
          writer.line('            g_ctor_${st.name} = env->GetMethodID(g_cls_${st.name}, "<init>", "$ctorSig");');
          for (final f in st.fields) {
            final isEnum = enumNames.contains(f.type.name.replaceFirst('?', ''));
            final isZeroCopy = _isZeroCopy(st, f.name);
            final isNestedStruct = structNames.contains(f.type.name.replaceFirst('?', ''));
            final sig = isEnum ? 'J' : (isZeroCopy ? 'Ljava/nio/ByteBuffer;' : (isNestedStruct ? 'L$libPkg/${f.type.name.replaceFirst('?', '')};' : _jniSigType(f.type.name)));
            writer.line('            g_fid_${st.name}_${f.name} = env->GetFieldID(g_cls_${st.name}, "${f.name}", "$sig");');
          }
          writer.line('        }');
          writer.line('    }');
        }
      }
      writer.line('}');
      writer.blankLine();

      // ── postXxxToPort helpers (used by @nitroNativeAsync Kotlin bridge) ──────
      final hasNativeAsync = spec.functions.any((f) => f.isNativeAsync);
      if (hasNativeAsync) {
        final jniPostNull = _jniMethodName(spec.lib, spec.dartClassName, 'postNullToPort');
        final jniPostInt64 = _jniMethodName(spec.lib, spec.dartClassName, 'postInt64ToPort');
        final jniPostDouble = _jniMethodName(spec.lib, spec.dartClassName, 'postDoubleToPort');
        final jniPostBool = _jniMethodName(spec.lib, spec.dartClassName, 'postBoolToPort');
        final jniPostString = _jniMethodName(spec.lib, spec.dartClassName, 'postStringToPort');

        writer.line('// ── postXxxToPort helpers for @nitroNativeAsync ──');
        // postNullToPort
        writer.line('JNIEXPORT void JNICALL $jniPostNull(JNIEnv*, jclass, jlong dartPort) {');
        writer.line('    Dart_CObject obj;');
        writer.line('    obj.type = Dart_CObject_kNull;');
        writer.line('    Dart_PostCObject_DL((Dart_Port)dartPort, &obj);');
        writer.line('}');
        writer.blankLine();
        // postInt64ToPort
        writer.line('JNIEXPORT void JNICALL $jniPostInt64(JNIEnv*, jclass, jlong dartPort, jlong value) {');
        writer.line('    Dart_CObject obj;');
        writer.line('    obj.type = Dart_CObject_kInt64;');
        writer.line('    obj.value.as_int64 = (int64_t)value;');
        writer.line('    Dart_PostCObject_DL((Dart_Port)dartPort, &obj);');
        writer.line('}');
        writer.blankLine();
        // postDoubleToPort
        writer.line('JNIEXPORT void JNICALL $jniPostDouble(JNIEnv*, jclass, jlong dartPort, jdouble value) {');
        writer.line('    Dart_CObject obj;');
        writer.line('    obj.type = Dart_CObject_kDouble;');
        writer.line('    obj.value.as_double = (double)value;');
        writer.line('    Dart_PostCObject_DL((Dart_Port)dartPort, &obj);');
        writer.line('}');
        writer.blankLine();
        // postBoolToPort
        writer.line('JNIEXPORT void JNICALL $jniPostBool(JNIEnv*, jclass, jlong dartPort, jboolean value) {');
        writer.line('    Dart_CObject obj;');
        writer.line('    obj.type = Dart_CObject_kBool;');
        writer.line('    obj.value.as_bool = (bool)value;');
        writer.line('    Dart_PostCObject_DL((Dart_Port)dartPort, &obj);');
        writer.line('}');
        writer.blankLine();
        // postStringToPort
        writer.line('JNIEXPORT void JNICALL $jniPostString(JNIEnv* env, jclass, jlong dartPort, jstring value) {');
        writer.line('    const char* cStr = env->GetStringUTFChars(value, nullptr);');
        writer.line('    Dart_CObject obj;');
        writer.line('    obj.type = Dart_CObject_kString;');
        writer.line('    obj.value.as_string = const_cast<char*>(cStr);');
        writer.line('    Dart_PostCObject_DL((Dart_Port)dartPort, &obj);');
        writer.line('    env->ReleaseStringUTFChars(value, cStr);');
        writer.line('}');
        writer.blankLine();
      }

      writer.line('} // extern "C"');
    } // end if (includeAndroid)

    // ── Apple section: NativeImpl.swift / NativeImpl.cpp / mixed ─────────────
    // Determine per-platform Apple strategy.
    final appleBothCpp = iosIsCpp && macosIsCpp;
    final appleMixedMacosCpp = macosIsCpp && !iosIsCpp && spec.targetsMacos;
    final appleMixedIosCpp = iosIsCpp && !macosIsCpp && spec.targetsIos;
    // The Apple section is emitted when iOS is Swift/C++ OR macOS is C++.
    final includeApple = includeIos || (macosIsCpp && spec.targetsMacos);

    if (includeAndroid && includeApple) writer.line('#elif __APPLE__');

    if (includeApple) {
      if (appleBothCpp) {
        // Both iOS and macOS use NativeImpl.cpp — unified C++ dispatch.
        _emitAppleCppDispatch(writer, spec, libStem, enumNames, structNames);
      } else if (appleMixedMacosCpp) {
        // macOS → NativeImpl.cpp, iOS → NativeImpl.swift
        writer.line('#include <TargetConditionals.h>');
        writer.line('#if TARGET_OS_OSX  // macOS: NativeImpl.cpp — direct C++ dispatch');
        _emitAppleCppDispatch(writer, spec, libStem, enumNames, structNames);
        if (includeIos) {
          writer.line('#else  // iOS: NativeImpl.swift — call through Swift bridge');
          _emitSwiftBridgeSection(writer, spec, libStem, enumNames, structNames);
        }
        writer.line('#endif  // TARGET_OS_OSX');
      } else if (appleMixedIosCpp) {
        // iOS → NativeImpl.cpp, macOS → NativeImpl.swift (or absent)
        writer.line('#include <TargetConditionals.h>');
        writer.line('#if TARGET_OS_IOS  // iOS: NativeImpl.cpp — direct C++ dispatch');
        _emitAppleCppDispatch(writer, spec, libStem, enumNames, structNames);
        if (spec.targetsMacos) {
          writer.line('#else  // macOS: NativeImpl.swift — call through Swift bridge');
          _emitSwiftBridgeSection(writer, spec, libStem, enumNames, structNames);
        }
        writer.line('#endif  // TARGET_OS_IOS');
      } else if (includeIos) {
        // Pure Swift on all Apple platforms (legacy / default path).
        _emitSwiftBridgeSection(writer, spec, libStem, enumNames, structNames);
      }
    }
    if (includeAndroid && includeApple) writer.line('#endif');
    return writer.toString();
  }

  // ── Swift bridge section emitter ─────────────────────────────────────────
  // Emits the extern "C" block that calls into Swift via _namespace_call_* symbols.
  static void _emitSwiftBridgeSection(
    CodeWriter writer,
    BridgeSpec spec,
    String libStem,
    Set<String> enumNames,
    Set<String> structNames,
  ) {
    writer.line('extern "C" {');

    for (final func in spec.functions) {
      final isEnum = enumNames.contains(func.returnType.name);
      final paramParts = <String>[];
      final callParamParts = <String>[];
      for (final p in func.params) {
        paramParts.add('${_paramTypeToC(p.type.name, structNames)} ${p.name}');
        callParamParts.add(p.name);
        if (p.type.isTypedData) {
          paramParts.add('int64_t ${p.name}_length');
          callParamParts.add('${p.name}_length');
        }
      }

      if (func.isNativeAsync) {
        paramParts.add('int64_t dart_port');
        callParamParts.add('dart_port');
        final params = paramParts.join(', ');
        final callParams = callParamParts.join(', ');
        writer.line('extern void _${spec.namespace}_call_${func.dartName}(${params.isEmpty ? 'void' : params});');
        writer.line('void ${func.cSymbol}($params) {');
        writer.line('    ${libStem}_clear_error();');
        writer.line('    _${spec.namespace}_call_${func.dartName}($callParams);');
        writer.line('}');
        writer.blankLine();
        continue;
      }

      final cReturnType = isEnum ? 'int64_t' : _typeToC(func.returnType.name);
      final params = paramParts.join(', ');
      final callParams = callParamParts.join(', ');
      writer.line('extern $cReturnType _${spec.namespace}_call_${func.dartName}(${params.isEmpty ? 'void' : params});');
      writer.line('$cReturnType ${func.cSymbol}(${params.isEmpty ? 'void' : params}) {');
      writer.line('    ${libStem}_clear_error();');
      writer.line('#ifdef __OBJC__');
      writer.line('    @try {');
      if (func.returnType.name != 'void') {
        writer.line('        return _${spec.namespace}_call_${func.dartName}($callParams);');
      } else {
        writer.line('        _${spec.namespace}_call_${func.dartName}($callParams);');
      }
      writer.line('    } @catch (NSException* e) {');
      writer.line('        nitro_report_error([e.name UTF8String], [e.reason UTF8String], nullptr, nullptr);');
      if (func.returnType.name != 'void') {
        writer.line('        return ${_defaultValue(cReturnType)};');
      }
      writer.line('    }');
      writer.line('#else');
      if (func.returnType.name != 'void') {
        writer.line('    return _${spec.namespace}_call_${func.dartName}($callParams);');
      } else {
        writer.line('    _${spec.namespace}_call_${func.dartName}($callParams);');
      }
      writer.line('#endif');
      writer.line('}');
      writer.blankLine();
    }

    for (final prop in spec.properties) {
      final isEnum = enumNames.contains(prop.type.name);
      final cType = isEnum ? 'int64_t' : _typeToC(prop.type.name);
      if (prop.hasGetter) {
        writer.line('extern $cType _${spec.namespace}_call_get_${prop.dartName}(void);');
        writer.line('$cType ${prop.getSymbol}(void) {');
        writer.line('    return _${spec.namespace}_call_get_${prop.dartName}();');
        writer.line('}');
        writer.blankLine();
      }
      if (prop.hasSetter) {
        final paramCType = isEnum ? 'int64_t' : _typeToC(prop.type.name);
        writer.line('extern void _${spec.namespace}_call_set_${prop.dartName}($paramCType value);');
        writer.line('void ${prop.setSymbol}($paramCType value) {');
        writer.line('    _${spec.namespace}_call_set_${prop.dartName}(value);');
        writer.line('}');
        writer.blankLine();
      }
    }

    for (final stream in spec.streams) {
      final isStruct = structNames.contains(stream.itemType.name);
      final isRecord = stream.itemType.isRecord;
      final isEnum = enumNames.contains(stream.itemType.name);
      final itemCType = (isStruct || isRecord) ? 'void*' : _typeToC(stream.itemType.name);
      writer.line('void _emit_${stream.dartName}_to_dart(int64_t dartPort, $itemCType item) {');
      writer.line('    Dart_CObject obj;');
      if (stream.itemType.name == 'double') {
        writer.line('    obj.type = Dart_CObject_kDouble;');
        writer.line('    obj.value.as_double = item;');
      } else if (stream.itemType.name == 'int') {
        writer.line('    obj.type = Dart_CObject_kInt64;');
        writer.line('    obj.value.as_int64 = (int64_t)item;');
      } else if (stream.itemType.name == 'bool') {
        writer.line('    obj.type = Dart_CObject_kBool;');
        writer.line('    obj.value.as_bool = item;');
      } else if (isEnum) {
        writer.line('    obj.type = Dart_CObject_kInt64;');
        writer.line('    obj.value.as_int64 = (int64_t)item;');
      } else if (isStruct || isRecord) {
        writer.line('    obj.type = Dart_CObject_kInt64;');
        writer.line('    obj.value.as_int64 = (intptr_t)item;');
      } else {
        writer.line('    obj.type = Dart_CObject_kNull;');
      }
      writer.line('    Dart_PostCObject_DL(dartPort, &obj);');
      writer.line('}');
      writer.blankLine();
      writer.line('extern void _${spec.namespace}_register_${stream.dartName}_stream(int64_t dartPort, void (*emitCb)(int64_t, $itemCType));');
      writer.line('void ${stream.registerSymbol}(int64_t dart_port) {');
      writer.line('    _${spec.namespace}_register_${stream.dartName}_stream(dart_port, _emit_${stream.dartName}_to_dart);');
      writer.line('}');
      writer.line('extern void _${spec.namespace}_release_${stream.dartName}_stream(int64_t dart_port);');
      writer.line('void ${stream.releaseSymbol}(int64_t dart_port) {');
      writer.line('    _${spec.namespace}_release_${stream.dartName}_stream(dart_port);');
      writer.line('}');
      writer.blankLine();
    }

    writer.line('} // extern "C"');
  }

  // ── Apple C++ dispatch section emitter ────────────────────────────────────
  // Emits the virtual-dispatch bridge for an Apple platform using NativeImpl.cpp.
  // Headers and global error state are already emitted before this is called.
  // Struct release functions are emitted globally in the JNI section — not here.
  static void _emitAppleCppDispatch(
    CodeWriter writer,
    BridgeSpec spec,
    String libStem,
    Set<String> enumNames,
    Set<String> structNames,
  ) {
    final className = spec.dartClassName;
    final ifaceHeader = '$libStem.native.g.h';
    final recordNames = spec.recordTypes.map((r) => r.name).toSet();

    writer.line('#include "$ifaceHeader"');
    writer.blankLine();

    for (final stream in spec.streams) {
      writer.line('static int64_t g_port_${stream.dartName} = 0;');
    }
    if (spec.streams.isNotEmpty) writer.blankLine();

    for (final stream in spec.streams) {
      final isStruct = structNames.contains(stream.itemType.name.replaceFirst('?', ''));
      final isRecord = stream.itemType.isRecord;
      final isEnum = enumNames.contains(stream.itemType.name.replaceFirst('?', ''));
      final itemCpp = _cppScalarType(stream.itemType.name, enumNames, structNames);
      writer.line('void Hybrid$className::emit_${stream.dartName}($itemCpp item) {');
      writer.line('    int64_t port = g_port_${stream.dartName};');
      writer.line('    if (port == 0) return;');
      writer.line('    Dart_CObject obj;');
      if (stream.itemType.name == 'double') {
        writer.line('    obj.type = Dart_CObject_kDouble;');
        writer.line('    obj.value.as_double = item;');
      } else if (stream.itemType.name == 'int') {
        writer.line('    obj.type = Dart_CObject_kInt64;');
        writer.line('    obj.value.as_int64 = item;');
      } else if (stream.itemType.name == 'bool') {
        writer.line('    obj.type = Dart_CObject_kBool;');
        writer.line('    obj.value.as_bool = item;');
      } else if (isEnum) {
        writer.line('    obj.type = Dart_CObject_kInt64;');
        writer.line('    obj.value.as_int64 = static_cast<int64_t>(item);');
      } else if (isStruct) {
        final stName = stream.itemType.name.replaceFirst('?', '');
        writer.line('    $stName* st_ptr = ($stName*)malloc(sizeof($stName));');
        writer.line('    *st_ptr = item;');
        writer.line('    obj.type = Dart_CObject_kInt64;');
        writer.line('    obj.value.as_int64 = (intptr_t)st_ptr;');
      } else if (isRecord) {
        writer.line('    obj.type = Dart_CObject_kInt64;');
        writer.line('    obj.value.as_int64 = (intptr_t)item;');
      } else {
        writer.line('    obj.type = Dart_CObject_kNull;');
      }
      writer.line('    Dart_PostCObject_DL(port, &obj);');
      writer.line('}');
      writer.blankLine();
    }

    final notInit =
        'nitro_report_error("NotInitialized", '
        '"No C++ implementation registered. Call ${libStem}_register_impl() first.", '
        'nullptr, nullptr)';

    writer.line('static Hybrid$className* g_impl = nullptr;');
    writer.blankLine();
    writer.line('extern "C" {');
    writer.line('void ${libStem}_register_impl(Hybrid$className* impl) { g_impl = impl; }');
    writer.line('Hybrid$className* ${libStem}_get_impl() { return g_impl; }');
    writer.blankLine();

    for (final func in spec.functions) {
      if (func.isNativeAsync) {
        final paramParts = <String>[];
        for (final p in func.params) {
          final isStructParam = structNames.contains(p.type.name.replaceFirst('?', ''));
          final isRecordParam = recordNames.contains(p.type.name.replaceFirst('?', ''));
          paramParts.add('${(isStructParam || isRecordParam) ? 'void*' : _typeToC(p.type.name)} ${p.name}');
          if (p.type.isTypedData) paramParts.add('int64_t ${p.name}_length');
        }
        paramParts.add('int64_t dart_port');
        final paramsDecl = paramParts.join(', ');
        final callArgs = <String>[];
        for (final p in func.params) {
          final base = p.type.name.replaceFirst('?', '');
          if (base == 'String') {
            callArgs.add('std::string(${p.name})');
          } else if (structNames.contains(base)) {
            callArgs.add('*static_cast<const $base*>(${p.name})');
          } else if (recordNames.contains(base)) {
            callArgs.add('NitroCppBuffer{ (const uint8_t*)${p.name} + 4, (size_t)*(int32_t*)${p.name} }');
          } else if (p.type.isTypedData) {
            callArgs.add(p.name);
            callArgs.add('static_cast<size_t>(${p.name}_length)');
          } else if (enumNames.contains(base)) {
            callArgs.add('static_cast<$base>(${p.name})');
          } else {
            callArgs.add(p.name);
          }
        }
        callArgs.add('dart_port');
        writer.line('void ${func.cSymbol}($paramsDecl) {');
        writer.line('    if (!g_impl) {');
        writer.line('        Dart_CObject _err = { Dart_CObject_kNull };');
        writer.line('        Dart_PostCObject_DL(dart_port, &_err);');
        writer.line('        return;');
        writer.line('    }');
        writer.line('    g_impl->${func.dartName}(${callArgs.join(', ')});');
        writer.line('}');
        writer.blankLine();
        continue;
      }

      final isEnumRet = enumNames.contains(func.returnType.name.replaceFirst('?', ''));
      final isStructRet = structNames.contains(func.returnType.name.replaceFirst('?', ''));
      final isRecordRet = func.returnType.isRecord;
      final cRet = isEnumRet ? 'int64_t' : _typeToC(func.returnType.name);
      final dflt = _defaultValue(cRet);
      final paramParts = <String>[];
      for (final p in func.params) {
        final isStructParam = structNames.contains(p.type.name.replaceFirst('?', ''));
        final isRecordParam = p.type.isRecord;
        paramParts.add('${(isStructParam || isRecordParam) ? 'void*' : _typeToC(p.type.name)} ${p.name}');
        if (p.type.isTypedData) paramParts.add('int64_t ${p.name}_length');
      }
      final paramsDecl = paramParts.isEmpty ? 'void' : paramParts.join(', ');

      writer.line('$cRet ${func.cSymbol}($paramsDecl) {');
      writer.line('    ${libStem}_clear_error();');
      if (func.returnType.name == 'void') {
        writer.line('    if (!g_impl) { $notInit; return; }');
      } else {
        writer.line('    if (!g_impl) { $notInit; return $dflt; }');
      }
      writer.line('    try {');
      final callArgs = <String>[];
      for (final p in func.params) {
        final base = p.type.name.replaceFirst('?', '');
        if (base == 'String') {
          callArgs.add('std::string(${p.name})');
        } else if (structNames.contains(base)) {
          callArgs.add('*static_cast<const $base*>(${p.name})');
        } else if (p.type.isRecord) {
          if (p.type.isNullable) {
            writer.line('        NitroCppBuffer _buf_${p.name} = { nullptr, 0 };');
            writer.line('        if (${p.name} != nullptr) {');
            writer.line('            _buf_${p.name}.data = (const uint8_t*)${p.name} + 4;');
            writer.line('            _buf_${p.name}.size = (size_t)*(int32_t*)${p.name};');
            writer.line('        }');
            callArgs.add('_buf_${p.name}');
          } else {
            writer.line('        NitroCppBuffer _buf_${p.name} = { (const uint8_t*)${p.name} + 4, (size_t)*(int32_t*)${p.name} };');
            callArgs.add('_buf_${p.name}');
          }
        } else if (p.type.isTypedData) {
          callArgs.add(p.name);
          callArgs.add('static_cast<size_t>(${p.name}_length)');
        } else if (enumNames.contains(base)) {
          callArgs.add('static_cast<$base>(${p.name})');
        } else {
          callArgs.add(p.name);
        }
      }
      final callArgStr = callArgs.join(', ');

      if (func.returnType.name == 'void') {
        writer.line('        g_impl->${func.dartName}($callArgStr);');
      } else if (func.returnType.name == 'String') {
        writer.line('        std::string _res = g_impl->${func.dartName}($callArgStr);');
        writer.line('        return strdup(_res.c_str());');
      } else if (isEnumRet) {
        writer.line('        return static_cast<int64_t>(g_impl->${func.dartName}($callArgStr));');
      } else if (isStructRet) {
        final stName = func.returnType.name.replaceFirst('?', '');
        writer.line('        $stName _res = g_impl->${func.dartName}($callArgStr);');
        writer.line('        $stName* _ptr = ($stName*)malloc(sizeof($stName));');
        writer.line('        *_ptr = _res;');
        writer.line('        return _ptr;');
      } else if (isRecordRet) {
        writer.line('        NitroCppBuffer _res = g_impl->${func.dartName}($callArgStr);');
        writer.line('        return (void*)_res.data;');
      } else {
        writer.line('        return g_impl->${func.dartName}($callArgStr);');
      }
      writer.line('    } catch (const std::exception& e) {');
      writer.line('        nitro_report_error("CppException", e.what(), nullptr, nullptr);');
      if (func.returnType.name != 'void') writer.line('        return $dflt;');
      writer.line('    } catch (...) {');
      writer.line('        nitro_report_error("CppException", "Unknown C++ exception", nullptr, nullptr);');
      if (func.returnType.name != 'void') writer.line('        return $dflt;');
      writer.line('    }');
      writer.line('}');
      writer.blankLine();
    }

    for (final prop in spec.properties) {
      final isEnum = enumNames.contains(prop.type.name.replaceFirst('?', ''));
      final cType = isEnum ? 'int64_t' : _typeToC(prop.type.name);
      if (prop.hasGetter) {
        writer.line('$cType ${prop.getSymbol}(void) {');
        writer.line('    ${libStem}_clear_error();');
        writer.line('    if (!g_impl) { $notInit; return ${_defaultValue(cType)}; }');
        writer.line('    try {');
        if (prop.type.name == 'String') {
          writer.line('        std::string _res = g_impl->get_${prop.dartName}();');
          writer.line('        return strdup(_res.c_str());');
        } else if (isEnum) {
          writer.line('        return static_cast<int64_t>(g_impl->get_${prop.dartName}());');
        } else if (recordNames.contains(prop.type.name.replaceFirst('?', ''))) {
          writer.line('        NitroCppBuffer _res = g_impl->get_${prop.dartName}();');
          writer.line('        return (void*)_res.data;');
        } else if (structNames.contains(prop.type.name.replaceFirst('?', ''))) {
          final stName = prop.type.name.replaceFirst('?', '');
          writer.line('        $stName _res = g_impl->get_${prop.dartName}();');
          writer.line('        $stName* _ptr = ($stName*)malloc(sizeof($stName));');
          writer.line('        *_ptr = _res;');
          writer.line('        return _ptr;');
        } else {
          writer.line('        return g_impl->get_${prop.dartName}();');
        }
        writer.line('    } catch (const std::exception& e) {');
        writer.line('        nitro_report_error("CppException", e.what(), nullptr, nullptr);');
        writer.line('        return ${_defaultValue(cType)};');
        writer.line('    }');
        writer.line('}');
        writer.blankLine();
      }
      if (prop.hasSetter) {
        final isStructParam = structNames.contains(prop.type.name.replaceFirst('?', ''));
        final isRecordParam = recordNames.contains(prop.type.name.replaceFirst('?', ''));
        final paramCType = (isEnum || isStructParam || isRecordParam) ? (isEnum ? 'int64_t' : 'void*') : _typeToC(prop.type.name);
        writer.line('void ${prop.setSymbol}($paramCType value) {');
        writer.line('    ${libStem}_clear_error();');
        writer.line('    if (!g_impl) { $notInit; return; }');
        writer.line('    try {');
        if (prop.type.name == 'String') {
          writer.line('        g_impl->set_${prop.dartName}(std::string(value));');
        } else if (isEnum) {
          final enumName = prop.type.name.replaceFirst('?', '');
          writer.line('        g_impl->set_${prop.dartName}(static_cast<$enumName>(value));');
        } else if (isRecordParam) {
          final opt = prop.type.name.endsWith('?');
          if (opt) {
            writer.line('        NitroCppBuffer _buf = { nullptr, 0 };');
            writer.line('        if (value != nullptr) {');
            writer.line('            _buf.data = (const uint8_t*)value + 4;');
            writer.line('            _buf.size = (size_t)*(int32_t*)value;');
            writer.line('        }');
            writer.line('        g_impl->set_${prop.dartName}(_buf);');
          } else {
            writer.line('        NitroCppBuffer _buf = { (const uint8_t*)value + 4, (size_t)*(int32_t*)value };');
            writer.line('        g_impl->set_${prop.dartName}(_buf);');
          }
        } else if (isStructParam) {
          final stName = prop.type.name.replaceFirst('?', '');
          writer.line('        g_impl->set_${prop.dartName}(*static_cast<const $stName*>(value));');
        } else {
          writer.line('        g_impl->set_${prop.dartName}(value);');
        }
        writer.line('    } catch (const std::exception& e) {');
        writer.line('        nitro_report_error("CppException", e.what(), nullptr, nullptr);');
        writer.line('    }');
        writer.line('}');
        writer.blankLine();
      }
    }

    for (final stream in spec.streams) {
      writer.line('void ${stream.registerSymbol}(int64_t dart_port) {');
      writer.line('    g_port_${stream.dartName} = dart_port;');
      writer.line('}');
      writer.line('void ${stream.releaseSymbol}(int64_t dart_port) {');
      writer.line('    if (g_port_${stream.dartName} == dart_port) g_port_${stream.dartName} = 0;');
      writer.line('}');
      writer.blankLine();
    }

    writer.line('} // extern "C"');
  }

  static String _typeToC(String dartType) {
    switch (dartType.replaceFirst('?', '')) {
      case 'int':
        return 'int64_t';
      case 'double':
        return 'double';
      case 'bool':
        return 'int8_t';
      case 'String':
        return 'const char*';
      case 'Uint8List':
        return 'uint8_t*';
      case 'Int8List':
        return 'int8_t*';
      case 'Int16List':
        return 'int16_t*';
      case 'Int32List':
        return 'int32_t*';
      case 'Uint16List':
        return 'uint16_t*';
      case 'Uint32List':
        return 'uint32_t*';
      case 'Float32List':
        return 'float*';
      case 'Float64List':
        return 'double*';
      case 'Int64List':
        return 'int64_t*';
      case 'Uint64List':
        return 'uint64_t*';
      case 'void':
        return 'void';
      default:
        return 'void*';
    }
  }

  static String _callbackParamToC(BridgeParam param, Set<String> enumNames) {
    final callback = param.type;
    final ret = _callbackTypeToC(callback.functionReturnType ?? 'void', enumNames);
    final params = callback.functionParams.map((p) => _callbackTypeToC(p.name, enumNames, bridgeType: p)).join(', ');
    final paramStr = params.isEmpty ? 'void' : params;
    return '$ret (*${param.name})($paramStr)';
  }

  static String _callbackTypeToC(String dartType, Set<String> enumNames, {BridgeType? bridgeType}) {
    if (bridgeType?.isPointer == true) {
      return _pointerInnerToC(bridgeType!.pointerInnerType);
    }
    final base = dartType.replaceFirst('?', '');
    if (enumNames.contains(base)) return 'int64_t';
    return _typeToC(base);
  }

