import '../../../bridge_spec.dart';
import '../../code_writer.dart';
import '../../generator_metadata.dart';

class CMakeGenerator {
  static String generate(BridgeSpec spec) {
    final checksum = bridgeSpecChecksum(spec);

    final s = CodeWriter();
    s.raw(generatedFileHeader('#', sourceUri: spec.sourceUri));
    s.line('cmake_minimum_required(VERSION 3.10)');
    s.blankLine();
    s.line('set(NITRO_MODULE_NAME ${spec.lib})');
    s.blankLine();

    // ── Source files ──────────────────────────────────────────────────────────
    s.line('add_library(\${NITRO_MODULE_NAME} SHARED');
    s.line('    # Bridge — generated by Nitrogen. Do not edit.');
    s.line('    \${CMAKE_CURRENT_SOURCE_DIR}/../lib/src/generated/cpp/${spec.lib}.bridge.g.cpp');
    s.line('    # Dart API DL — required by every Nitro module for Dart_PostCObject_DL.');
    s.line('    \${CMAKE_CURRENT_SOURCE_DIR}/dart_api_dl.c');
    s.line(')');
    s.blankLine();

    // ── Include directories ───────────────────────────────────────────────────
    s.line('set(NITRO_NATIVE "\${CMAKE_CURRENT_SOURCE_DIR}/native")');
    s.line('set(GENERATED_CPP "\${CMAKE_CURRENT_SOURCE_DIR}/../lib/src/generated/cpp")');
    s.blankLine();
    s.line('target_include_directories(\${NITRO_MODULE_NAME} PRIVATE');
    s.line('    \${CMAKE_CURRENT_SOURCE_DIR}');
    s.line('    \${GENERATED_CPP}');
    s.line('    \${NITRO_NATIVE}');
    s.line(')');
    s.blankLine();

    // ── Compile definitions ───────────────────────────────────────────────────
    s.line('target_compile_definitions(\${NITRO_MODULE_NAME} PUBLIC DART_SHARED_LIB)');
    s.blankLine();

    // ── C++17 ─────────────────────────────────────────────────────────────────
    s.line('set_property(TARGET \${NITRO_MODULE_NAME} PROPERTY CXX_STANDARD 17)');
    s.line('set_property(TARGET \${NITRO_MODULE_NAME} PROPERTY CXX_STANDARD_REQUIRED ON)');
    s.blankLine();

    // ── Platform-specific link libraries ─────────────────────────────────────
    // Android needs libanroid + liblog for JNI and logging.
    // Linux needs libdl (dlopen) and libpthread.
    // Windows and Apple need no extra system libs.
    // Using generator expressions keeps a single CMakeLists.txt portable.
    s.line('if(ANDROID)');
    s.line('    target_link_libraries(\${NITRO_MODULE_NAME} PRIVATE android log)');
    s.line('elseif(WIN32)');
    s.line('    # No extra system libs — MSVC links the Universal CRT automatically.');
    s.line('    # dart_api_dl.c must be compiled as C (not C++) on MSVC:');
    s.line('    set_source_files_properties(');
    s.line('        \${CMAKE_CURRENT_SOURCE_DIR}/dart_api_dl.c');
    s.line('        PROPERTIES LANGUAGE C');
    s.line('    )');
    s.line('elseif(UNIX AND NOT APPLE)');
    s.line('    # Linux: libdl for dlopen, pthreads for IsolatePool worker threads.');
    s.line('    target_link_libraries(\${NITRO_MODULE_NAME} PRIVATE dl pthread)');
    s.line('endif()');
    s.blankLine();

    // ── Checksum comment (stale-generation detection) ─────────────────────────
    s.line('# nitro bridge checksum: $checksum');

    return s.toString();
  }
}
