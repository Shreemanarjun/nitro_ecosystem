import '../bridge_spec.dart';
import 'code_writer.dart';

/// Field names that are treated as the element-count of a zero-copy TypedData sibling.
const _kLengthFieldNames = {
  'length',
  'size',
  'stride',
  'bytelength',
  'bytelen',
  'len',
};

/// Returns the companion element-count field name for a zero-copy field, or
/// null when no explicit companion is declared (caller should use the synthetic
/// '${fieldName}Length' name injected by [generateCStructs]).
String? _zeroCopyCompanionField(BridgeStruct st, String zeroCopyFieldName) {
  // Field-specific names take priority (pcmLength, pcmSize).
  for (final c in ['${zeroCopyFieldName}Length', '${zeroCopyFieldName}Size']) {
    if (st.fields.any((f) => f.name == c && f.type.name == 'int')) return c;
  }
  // Generic length names.
  for (final c in _kLengthFieldNames) {
    if (st.fields.any((f) => f.name == c && f.type.name == 'int')) return c;
  }
  return null;
}

/// True when [zeroCopyFieldName] needs a synthetic '${name}Length' companion.
bool _needsSyntheticLen(BridgeStruct st, String zeroCopyFieldName) => _zeroCopyCompanionField(st, zeroCopyFieldName) == null;

/// Generates Dart extension helpers for HybridStructs.
/// The struct class itself MUST already be declared in the .native.dart spec file.
/// We only emit the FFI conversion extension here (in the generated part).
class StructGenerator {
  /// Called by DartFfiGenerator. Emits FFI Struct representations of HybridStructs.
  /// Generates Dart FFI struct representations and conversion extensions.
  /// Skips imported structs (their extensions are generated in the other .g.dart file).
  static String generateDartExtensions(BridgeSpec spec) {
    final localStructs = spec.localStructs;
    if (localStructs.isEmpty) return '';
    // Use ALL names (local + imported) for field-type classification.
    final enumNames = spec.enums.map((e) => e.name).toSet();
    final structNames = spec.structs.map((s) => s.name).toSet();
    final s = CodeWriter();
    s.writeln('// --- Struct FFI representations (generated by Nitrogen) ---');
    s.writeln();
    for (final st in localStructs) {
      if (st.packed) {
        s.writeln('@Packed(1)');
      }
      s.writeln('final class ${st.name}Ffi extends Struct {');
      for (final f in st.fields) {
        final ffiType = _dartTypeToFfi(f.type.name, enumNames, structNames);
        final annotationType = _dartTypeToFfiAnnotation(f.type.name, enumNames, structNames);
        if (annotationType.isNotEmpty) {
          s.writeln('  @$annotationType()');
        }
        s.writeln('  external $ffiType ${f.name};');
        // Mirror the synthetic C field so the Dart FFI struct layout matches.
        if (f.type.isTypedData && _needsSyntheticLen(st, f.name)) {
          s.writeln('  @Int64()');
          s.writeln('  external int ${f.name}Length;');
        }
      }
      s.writeln('}');
      s.writeln();
      s.writeln('extension ${st.name}FfiExt on ${st.name}Ffi {');
      s.writeln('  ${st.name} toDart() {');
      s.writeln('    return ${st.name}(');
      // Positional parameters first (Dart requires positional before named).
      for (final f in st.fields.where((f) => !f.isNamed)) {
        s.writeln('      ${_fieldConversion(f, st, enumNames, structNames)},');
      }
      // Named parameters after positional.
      for (final f in st.fields.where((f) => f.isNamed)) {
        s.writeln('      ${f.name}: ${_fieldConversion(f, st, enumNames, structNames)},');
      }
      s.writeln('    );');
      s.writeln('  }');
      s.writeln();
      s.writeln('  /// Frees internal fields (like strings) that were allocated on the C heap.');
      s.writeln('  /// Does NOT free the struct itself.');
      s.writeln('  ///');
      s.writeln('  /// [nativeFree] must be the free function matching the allocator that');
      s.writeln('  /// produced the fields — for native-returned structs, the module\'s');
      s.writeln('  /// `_nitroFree` (the exported C-runtime free). package:ffi\'s malloc.free');
      s.writeln('  /// is CoTaskMemFree on Windows and corrupts the heap on these pointers.');
      s.writeln('  void freeFields(void Function(Pointer<NativeType>) nativeFree) {');
      for (final f in st.fields) {
        final typeName = f.type.name.replaceFirst('?', '');
        if (f.type.name == 'String') {
          s.writeln('    if (${f.name} != nullptr) {');
          s.writeln('      nativeFree(${f.name});');
          s.writeln('    }');
        } else if (structNames.contains(typeName)) {
          // Nested struct pointer: free its own fields, then the pointer.
          s.writeln('    if (${f.name} != nullptr) {');
          s.writeln('      ${f.name}.ref.freeFields(nativeFree);');
          s.writeln('      nativeFree(${f.name});');
          s.writeln('    }');
        } else if (f.type.isTypedData && !f.zeroCopy) {
          // Non-zero-copy typed data is malloc'd by the native side; free it.
          s.writeln('    if (${f.name} != nullptr) {');
          s.writeln('      nativeFree(${f.name});');
          s.writeln('    }');
        }
      }
      s.writeln('  }');
      s.writeln('}');
      s.writeln();
      s.writeln('extension ${st.name}Ext on ${st.name} {');
      s.writeln('  Pointer<${st.name}Ffi> toNative(Arena arena) {');
      s.writeln('    final ptr = arena<${st.name}Ffi>();');
      for (final f in st.fields) {
        final typeName = f.type.name.replaceFirst('?', '');
        if (f.type.isTypedData) {
          s.writeln('    ptr.ref.${f.name} = ${f.name}.toPointer(arena);');
          // Populate the synthetic length field so C can round-trip the size.
          if (_needsSyntheticLen(st, f.name)) {
            s.writeln('    ptr.ref.${f.name}Length = ${f.name}.length;');
          }
        } else if (f.type.name == 'bool') {
          s.writeln('    ptr.ref.${f.name} = ${f.name} ? 1 : 0;');
        } else if (f.type.name == 'String') {
          s.writeln(
            '    ptr.ref.${f.name} = ${f.name}.toNativeUtf8(allocator: arena);',
          );
        } else if (enumNames.contains(typeName)) {
          s.writeln('    ptr.ref.${f.name} = ${f.name}.nativeValue;');
        } else if (structNames.contains(typeName)) {
          // Nested struct: call its toNative() extension (arena-managed pointer).
          s.writeln('    ptr.ref.${f.name} = ${f.name}.toNative(arena);');
        } else {
          s.writeln('    ptr.ref.${f.name} = ${f.name};');
        }
      }
      s.writeln('    return ptr;');
      s.writeln('  }');
      s.writeln('}');
      s.writeln();
    }
    return s.toString();
  }

  /// Generates a zero-copy [${name}Proxy] class for every HybridStruct.
  ///
  /// Each proxy **extends** its corresponding value class (e.g. `BenchmarkBoxProxy
  /// extends BenchmarkBox`) and overrides every getter to read lazily from the
  /// native heap. Because `BenchmarkBoxProxy <: BenchmarkBox`, Dart's covariant
  /// generic type system accepts `Stream<BenchmarkBoxProxy>` wherever
  /// `Stream<BenchmarkBox>` is expected — so the generated impl override requires
  /// **no `.map()` or eager copy at all**.
  ///
  /// A [NativeFinalizer] backed by the generated C release symbol frees native
  /// memory when the proxy is GC'd. The caller must NOT call `malloc.free`.
  ///
  /// Use [toDartAndRelease] to eagerly copy and free before GC if needed.
  /// Generates zero-copy struct native proxies. Skips imported structs.
  static String generateDartProxies(BridgeSpec spec) {
    final localStructs = spec.localStructs;
    if (localStructs.isEmpty) return '';
    final libStem = spec.lib.replaceAll('-', '_');
    final enumNames = spec.enums.map((e) => e.name).toSet();
    final structNames = spec.structs.map((s) => s.name).toSet();
    final structMap = {for (final st in spec.structs) st.name: st};
    final s = CodeWriter();
    s.writeln('// --- Struct Native Proxies (zero-copy, lazy field access) ---');
    s.writeln();
    for (final st in localStructs) {
      final releaseSym = '${libStem}_release_${st.name}';
      // Build the super() call with zero-value defaults so the proxy can
      // satisfy the base class constructor without copying any native data.
      // Positional params first (no label), named params after (with label).
      final positionalDefaults = st.fields.where((f) => !f.isNamed).map((f) => _superDefault(f.type.name, enumNames, structNames, structMap)).join(', ');
      final namedDefaults = st.fields.where((f) => f.isNamed).map((f) => '${f.name}: ${_superDefault(f.type.name, enumNames, structNames, structMap)}').join(', ');
      final superArgs = [
        if (positionalDefaults.isNotEmpty) positionalDefaults,
        if (namedDefaults.isNotEmpty) namedDefaults,
      ].join(', ');
      s.writeln('/// Zero-copy proxy for [${st.name}].');
      s.writeln('/// Extends [${st.name}] and overrides every getter to read lazily from');
      s.writeln('/// native memory — no field is copied at construction time.');
      s.writeln('/// Because `${st.name}Proxy <: ${st.name}`, a `Stream<${st.name}Proxy>`');
      s.writeln('/// satisfies `Stream<${st.name}>` via Dart covariant generics.');
      s.writeln('/// Native memory is freed via a [NativeFinalizer] backed by the');
      s.writeln("/// generated C symbol '$releaseSym'.");
      s.writeln('/// Ownership: this proxy owns the generated struct shell. Zero-copy');
      s.writeln('/// field buffers remain owned by the native implementation and must');
      s.writeln('/// stay valid until this proxy is released by the finalizer or');
      s.writeln('/// [toDartAndRelease].');
      s.writeln('final class ${st.name}Proxy extends ${st.name} implements Finalizable {');
      s.writeln('  final Pointer<${st.name}Ffi> _native;');
      s.writeln();
      s.writeln('  static NativeFinalizer? _finalizer;');
      s.writeln('  static void Function(Pointer<Void>)? _releaseFn;');
      s.writeln();
      s.writeln('  /// Binds the generated release symbol from [dylib].');
      s.writeln('  /// Must be called once — typically in the impl class constructor.');
      s.writeln('  /// Idempotent: subsequent calls are a no-op.');
      s.writeln('  static void _init(DynamicLibrary dylib) {');
      s.writeln('    _finalizer ??= NativeFinalizer(');
      s.writeln("      dylib.lookup<NativeFunction<Void Function(Pointer<Void>)>>('$releaseSym'),");
      s.writeln('    );');
      s.writeln("    _releaseFn ??= dylib.lookupFunction<Void Function(Pointer<Void>), void Function(Pointer<Void>)>('$releaseSym');");
      s.writeln('  }');
      s.writeln();
      s.writeln('  /// Takes ownership of [native]. Super fields are zeroed and never read;');
      s.writeln('  /// all getters below are overridden to read from native memory instead.');
      s.writeln('  /// Do NOT call [malloc.free] on the struct shell after passing it here,');
      s.writeln('  /// and do not free zero-copy field buffers while the proxy may be read.');
      s.writeln('  ${st.name}Proxy(this._native) : super($superArgs) {');
      s.writeln("    assert(_finalizer != null, '${st.name}Proxy._init() was not called. Ensure the Nitro impl class constructor ran before creating proxies.');");
      s.writeln('    _finalizer!.attach(this, _native.cast(), detach: this, externalSize: 512);');
      s.writeln('  }');
      s.writeln();
      s.writeln('  // @override lazy getters — read native memory on demand, zero allocation.');
      for (final f in st.fields) {
        final typeName = f.type.name.replaceFirst('?', '');
        final dartFieldType = f.type.name;
        String readExpr;
        if (f.type.isTypedData) {
          final companion = _zeroCopyCompanionField(st, f.name);
          // Prefer explicit companion field; fall back to synthesized ${fieldName}Length.
          final lenRef = '_native.ref.${companion ?? '${f.name}Length'}';
          readExpr = '_native.ref.${f.name}.asTypedList($lenRef)';
        } else if (f.type.name == 'bool') {
          readExpr = '_native.ref.${f.name} != 0';
        } else if (f.type.name == 'String') {
          readExpr = '_native.ref.${f.name}.toDartString()';
        } else if (enumNames.contains(typeName)) {
          readExpr = '_native.ref.${f.name}.to$typeName()';
        } else if (structNames.contains(typeName)) {
          readExpr = '_native.ref.${f.name}.ref.toDart()';
        } else {
          readExpr = '_native.ref.${f.name}';
        }
        s.writeln('  @override');
        s.writeln('  $dartFieldType get ${f.name} => $readExpr;');
      }
      s.writeln();
      s.writeln('  /// Eagerly copies all fields to a plain [${st.name}] value, detaches');
      s.writeln('  /// the finalizer, and frees native memory immediately.');
      s.writeln('  /// Use this only when you need an immutable snapshot; for streams');
      s.writeln('  /// prefer consuming the proxy fields lazily then letting it GC.');
      s.writeln('  /// Must not be called more than once.');
      s.writeln('  ${st.name} toDartAndRelease() {');
      s.writeln('    final v = _native.ref.toDart();');
      s.writeln('    _finalizer?.detach(this);');
      // Release through the same generated C symbol the finalizer uses — it
      // frees the shell AND its native-owned fields with the native
      // allocator. malloc.free here was wrong twice: package:ffi's free is
      // CoTaskMemFree on Windows (heap corruption on a native malloc'd
      // pointer), and it leaked the fields the release symbol frees.
      s.writeln('    _releaseFn!(_native.cast());');
      s.writeln('    return v;');
      s.writeln('  }');
      s.writeln('}');
      s.writeln();
    }
    return s.toString();
  }

  /// Returns the Dart expression to read [f] from its FFI counterpart for use
  /// in the `toDart()` constructor call.  Does NOT include the `field:` prefix
  /// for named params — the caller adds that when needed.
  static String _fieldConversion(
    BridgeField f,
    BridgeStruct st,
    Set<String> enumNames,
    Set<String> structNames,
  ) {
    final typeName = f.type.name.replaceFirst('?', '');
    if (f.type.isTypedData) {
      final companion = _zeroCopyCompanionField(st, f.name);
      // Prefer explicit companion field; fall back to synthesized ${fieldName}Length.
      final lenExpr = companion ?? '${f.name}Length';
      return '$typeName.fromList(${f.name}.asTypedList($lenExpr))';
    } else if (f.type.name == 'bool') {
      return '${f.name} != 0';
    } else if (f.type.name == 'String') {
      return '${f.name}.toDartString()';
    } else if (enumNames.contains(typeName)) {
      return '${f.name}.to$typeName()';
    } else if (structNames.contains(typeName)) {
      return '${f.name}.ref.toDart()';
    }
    return f.name;
  }

  /// Returns a Dart literal that is a safe zero-value default for [type].
  /// Used only for the proxy's `super(...)` call — those fields are never read.
  static String _superDefault(
    String type,
    Set<String> enumNames, [
    Set<String> structNames = const {},
    Map<String, BridgeStruct>? structMap,
  ]) {
    final base = type.replaceFirst('?', '');
    if (enumNames.contains(base)) return '$base.values.first';
    if (structNames.contains(base) && structMap != null) {
      // Generate a zero-value constructor call for the nested struct type,
      // respecting the nested type's own positional/named param layout.
      final nestedSt = structMap[base];
      if (nestedSt != null) {
        final positional = nestedSt.fields.where((f) => !f.isNamed).map((f) => _superDefault(f.type.name, enumNames, structNames, structMap)).join(', ');
        final named = nestedSt.fields.where((f) => f.isNamed).map((f) => '${f.name}: ${_superDefault(f.type.name, enumNames, structNames, structMap)}').join(', ');
        final args = [if (positional.isNotEmpty) positional, if (named.isNotEmpty) named].join(', ');
        return '$base($args)';
      }
    }
    switch (base) {
      case 'double':
        return '0.0';
      case 'int':
        return '0';
      case 'bool':
        return 'false';
      case 'String':
        return "''";
      case 'Uint8List':
        return 'Uint8List(0)';
      case 'Int8List':
        return 'Int8List(0)';
      case 'Int16List':
        return 'Int16List(0)';
      case 'Int32List':
        return 'Int32List(0)';
      case 'Uint16List':
        return 'Uint16List(0)';
      case 'Uint32List':
        return 'Uint32List(0)';
      case 'Float32List':
        return 'Float32List(0)';
      case 'Float64List':
        return 'Float64List(0)';
      case 'Int64List':
        return 'Int64List(0)';
      case 'Uint64List':
        return 'Uint64List(0)';
      default:
        return 'null';
    }
  }

  /// Generates C structs for the header file.
  ///
  /// Each struct is wrapped in a `#ifndef NITRO_STRUCT_<NAME>_DEFINED` guard so
  /// that the same struct type can appear in multiple generated bridge headers
  /// (e.g. a shared `BenchmarkPoint` in both `benchmark.bridge.g.h` and
  /// `benchmark_cpp.bridge.g.h`) without causing "typedef redefinition" errors
  /// when both headers are compiled into the same translation unit.
  /// Generates C struct typedefs. Skips imported structs (declared in other bridge header).
  static String generateCStructs(BridgeSpec spec) {
    final localStructs = spec.localStructs;
    if (localStructs.isEmpty) return '';
    final enumNames = spec.enums.map((e) => e.name).toSet();
    final structNames = spec.structs.map((s) => s.name).toSet();
    final s = CodeWriter();
    s.writeln('// --- Structs ---');
    for (final st in localStructs) {
      final guard = 'NITRO_STRUCT_${st.name.toUpperCase()}_DEFINED';
      s.writeln('#ifndef $guard');
      s.writeln('#define $guard');
      if (st.packed) s.writeln('#pragma pack(push, 1)');
      s.writeln('typedef struct {');
      for (final f in st.fields) {
        final cType = _dartTypeToCType(f.type.name, enumNames, structNames);
        s.writeln('  $cType ${f.name}; ${f.zeroCopy ? "/* zero-copy */" : ""}');
        // When a TypedData field has no explicit companion length field, inject
        // a synthetic int64_t '${field}Length' field so the JNI bridge can
        // round-trip the element count. This covers both @ZeroCopy buffers
        // (direct ByteBuffer address) and regular copy-typed arrays (malloc'd).
        if (f.type.isTypedData && _needsSyntheticLen(st, f.name)) {
          final comment = f.zeroCopy ? '/* synthesized: element count for zero-copy buffer */' : '/* synthesized: element count for data buffer */';
          s.writeln('  int64_t ${f.name}Length; $comment');
        }
      }
      s.writeln('} ${st.name};');
      if (st.packed) s.writeln('#pragma pack(pop)');
      s.writeln('#endif // $guard');
      s.writeln();
    }
    return s.toString();
  }

  /// Generates Kotlin @Keep data classes for structs, including binary
  /// [decodeFrom] / [decode] / [writeFieldsTo] / [encode] codec methods so
  /// structs can be used as item types inside @HybridRecord list fields.
  /// Generates Kotlin data classes for structs. Skips imported structs.
  static String generateKotlin(BridgeSpec spec) {
    final localStructs = spec.localStructs;
    if (localStructs.isEmpty) return '';
    final enumNames = spec.enums.map((e) => e.name).toSet();
    final structNames = spec.structs.map((s) => s.name).toSet();
    final s = CodeWriter();
    s.writeln('// --- Structs ---');
    for (final st in localStructs) {
      s.writeln('@androidx.annotation.Keep');
      final params = st.fields
          .map(
            (f) => 'val ${f.name}: ${_dartTypeToKotlin(f.type.name, f.type.isNullable, f.zeroCopy, enumNames, structNames)}',
          )
          .join(', ');
      s.writeln('data class ${st.name}($params) {');

      // ── companion object: decodeFrom(buf) + decode(bytes) ──────────────
      s.writeln('    companion object {');
      s.writeln('        @JvmStatic fun decodeFrom(buf: java.nio.ByteBuffer): ${st.name} {');
      for (final f in st.fields) {
        s.writeln('            val ${f.name} = ${_kotlinReadCallForStruct(f, enumNames, structNames)}');
      }
      final ctorArgs = st.fields.map((f) => f.name).join(', ');
      s.writeln('            return ${st.name}($ctorArgs)');
      s.writeln('        }');
      s.writeln('        @JvmStatic fun decode(bytes: ByteArray): ${st.name} {');
      s.writeln('            val buf = java.nio.ByteBuffer.wrap(bytes).order(java.nio.ByteOrder.LITTLE_ENDIAN)');
      s.writeln('            return decodeFrom(buf)');
      s.writeln('        }');
      s.writeln('    }');
      s.writeln();

      // ── writeFieldsTo: writes fields into a shared stream/buffer ───────
      s.writeln('    fun writeFieldsTo(out: java.io.ByteArrayOutputStream, buf: java.nio.ByteBuffer) {');
      s.writeln('        fun writeInt(v: Long) { buf.clear(); buf.putLong(v); out.write(buf.array()) }');
      s.writeln('        @Suppress("UNUSED_PARAMETER") fun writeInt32(v: Int) { buf.clear(); buf.putInt(v); out.write(buf.array(), 0, 4) }');
      s.writeln('        fun writeDouble(v: Double) { buf.clear(); buf.putDouble(v); out.write(buf.array()) }');
      s.writeln('        fun writeBool(v: Boolean) { out.write(if (v) 1 else 0) }');
      s.writeln('        fun writeString(v: String) { val b = v.toByteArray(Charsets.UTF_8); writeInt32(b.size); out.write(b) }');
      for (final f in st.fields) {
        if (f.type.isNullable) {
          s.writeln('        out.write(if (${f.name} == null) 0 else 1)');
          final writeExpr = _kotlinWriteCallForStruct(f, enumNames, structNames, 'it');
          s.writeln('        ${f.name}?.let { $writeExpr }');
        } else {
          final writeExpr = _kotlinWriteCallForStruct(f, enumNames, structNames);
          s.writeln('        $writeExpr');
        }
      }
      s.writeln('    }');
      s.writeln();

      // ── encode(): ByteArray — standalone serialization ──────────────────
      final capacity = _structBytesHint(st, enumNames, structNames);
      s.writeln('    fun encode(): ByteArray {');
      s.writeln('        val out = java.io.ByteArrayOutputStream($capacity)');
      s.writeln('        val buf = java.nio.ByteBuffer.allocate(8).order(java.nio.ByteOrder.LITTLE_ENDIAN)');
      s.writeln('        writeFieldsTo(out, buf)');
      s.writeln('        return out.toByteArray()');
      s.writeln('    }');
      s.writeln('}');
      s.writeln();
    }
    return s.toString();
  }

  /// Kotlin expression to read one struct field from a [java.nio.ByteBuffer].
  static String _kotlinReadCallForStruct(BridgeField f, Set<String> enumNames, Set<String> structNames) {
    var expr = _kotlinReadExprNoNull(f, enumNames, structNames);
    if (f.type.isNullable) {
      return '(if (buf.get().toInt() != 0) $expr else null)';
    }
    return expr;
  }

  static String _kotlinReadExprNoNull(BridgeField f, Set<String> enumNames, Set<String> structNames) {
    final base = f.type.name.replaceFirst('?', '');
    if (enumNames.contains(base)) return 'buf.long';
    if (structNames.contains(base)) return '$base.decodeFrom(buf)';
    if (f.type.isTypedData) {
      if (f.zeroCopy) {
        // Return a dummy/empty ByteBuffer for now as we don't have a safe way
        // to wrap a Long pointer in pure Kotlin without JNI.
        // This fixes compilation when the struct is embedded in a @HybridRecord.
        return '{ buf.long; java.nio.ByteBuffer.allocate(0) }()';
      } else {
        return '{ val len = buf.int; val b = ByteArray(len); buf.get(b); b }()';
      }
    }
    switch (base) {
      case 'int':
        return 'buf.long';
      case 'double':
        return 'buf.double';
      case 'bool':
        return '(buf.get().toInt() != 0)';
      case 'String':
        return '{ val len = buf.int; val b = ByteArray(len); buf.get(b); b.toString(Charsets.UTF_8) }()';
      default:
        return 'buf.long';
    }
  }

  /// Kotlin statement to write one struct field to [out]/[buf].
  /// Assumes local helper functions [writeInt], [writeDouble], etc. exist in scope.
  static String _kotlinWriteCallForStruct(BridgeField f, Set<String> enumNames, Set<String> structNames, [String? varOverride]) {
    final name = varOverride ?? f.name;
    final base = f.type.name.replaceFirst('?', '');
    if (enumNames.contains(base)) return 'writeInt($name.toLong())';
    if (structNames.contains(base)) return '$name.writeFieldsTo(out, buf)';
    if (f.type.isTypedData) {
      if (f.zeroCopy) {
        // We write 0 as a placeholder for the pointer since we can't easily
        // get the address of a DirectByteBuffer in pure Kotlin.
        return 'writeInt(0L)';
      } else {
        return 'writeInt32($name.size); out.write($name)';
      }
    }
    switch (base) {
      case 'int':
        return 'writeInt($name)';
      case 'double':
        return 'writeDouble($name)';
      case 'bool':
        return 'writeBool($name)';
      case 'String':
        return 'writeString($name)';
      default:
        return 'writeInt($name.toLong())';
    }
  }

  /// Estimates the serialized byte size of one [BridgeStruct] instance.
  /// Used to pre-size [ByteArrayOutputStream] in [encode].
  static int _structBytesHint(BridgeStruct st, Set<String> enumNames, Set<String> structNames) {
    var total = 0;
    for (final f in st.fields) {
      if (f.type.isNullable) total += 1;
      final base = f.type.name.replaceFirst('?', '');
      if (enumNames.contains(base) || base == 'int') {
        total += 8;
      } else if (base == 'double') {
        total += 8;
      } else if (base == 'bool') {
        total += 1;
      } else if (base == 'String') {
        total += 36; // 4-byte len + ~32 avg content
      } else if (structNames.contains(base)) {
        total += 32; // rough estimate for nested struct
      } else if (f.type.isTypedData) {
        total += 36; // 4-byte len + ~32 byte hint
      } else {
        total += 8;
      }
    }
    return total > 0 ? total : 32;
  }

  /// Generates Swift structs and their C-ABI shadow structs.
  ///
  /// Each public struct uses idiomatic Swift types (String, Bool, Data, etc.)
  /// for use in the protocol and user implementation.
  ///
  /// A private `_${Name}C` shadow struct is also emitted for each struct, with
  // ignore: unintended_html_in_doc_comment
  /// C-ABI-compatible field types (UnsafeMutablePointer<CChar>?, Int8, Int32,
  /// etc.) that match the C header layout exactly. The shadow is used by the
  /// @_cdecl bridge functions to correctly marshal structs across the FFI
  /// boundary without Swift String Small-String-Optimization (SSO) crashes.
  /// Generates Swift public structs. Skips imported structs (declared in other bridge file).
  static String generateSwift(BridgeSpec spec) {
    final localStructs = spec.localStructs;
    if (localStructs.isEmpty) return '';
    final enumNames = spec.enums.map((e) => e.name).toSet();
    final structNames = spec.structs.map((s) => s.name).toSet();
    final s = CodeWriter();
    s.writeln('// --- Structs ---');
    for (final st in localStructs) {
      // ── Public user-facing Swift struct ──────────────────────────────────
      s.writeln('public struct ${st.name} {');
      for (final f in st.fields) {
        final swiftType = _dartTypeToSwift(f.type.name, f.zeroCopy, enumNames, structNames);
        s.writeln('  public var ${f.name}: $swiftType');
        // For zero-copy typed data without an explicit companion, expose the
        // synthesized length field so callers can set it. Non-zero-copy fields
        // derive their length from Data.count — no extra field needed.
        if (f.zeroCopy && f.type.isTypedData && _needsSyntheticLen(st, f.name)) {
          s.writeln('  public var ${f.name}Length: Int64');
        }
      }
      s.writeln('}');
      s.writeln();

      // ── C-ABI shadow struct ───────────────────────────────────────────────
      // Fields use raw C types (pointer-sized, 1-byte booleans, Int32 enums)
      // so that UnsafeMutablePointer<_${Name}C> has EXACTLY the same layout
      // as the C typedef in the bridge header.
      s.writeln('// C-ABI shadow — layout matches the C typedef ${st.name}');
      s.writeln('fileprivate struct _${st.name}C {');
      for (final f in st.fields) {
        final shadowType = _dartTypeToSwiftCShadow(f.type.name, f.zeroCopy, enumNames, structNames);
        s.writeln('  var ${f.name}: $shadowType');
        if (f.type.isTypedData && _needsSyntheticLen(st, f.name)) {
          s.writeln('  var ${f.name}Length: Int64');
        }
      }
      s.writeln();

      // fromSwift: convert user-facing struct → C shadow (for returns/emit)
      s.writeln('  static func fromSwift(_ s: ${st.name}) -> _${st.name}C {');
      // Pre-compute any typed-data buffers (they need temp vars before the struct literal)
      for (final f in st.fields) {
        if (f.type.isTypedData && !f.zeroCopy) {
          s.writeln('    var _buf_${f.name}: UnsafeMutablePointer<${_swiftTypedDataElement(f.type.name)}>? = nil');
          s.writeln('    var _len_${f.name}: Int64 = 0');
          s.writeln('    if !s.${f.name}.isEmpty {');
          s.writeln('      _buf_${f.name} = UnsafeMutablePointer<${_swiftTypedDataElement(f.type.name)}>.allocate(capacity: s.${f.name}.count)');
          s.writeln('      s.${f.name}.copyBytes(to: _buf_${f.name}!, count: s.${f.name}.count)');
          s.writeln('      _len_${f.name} = Int64(s.${f.name}.count)');
          s.writeln('    }');
        }
      }
      s.writeln('    return _${st.name}C(');
      final fromFields = <String>[];
      for (final f in st.fields) {
        fromFields.add('      ${f.name}: ${_swiftFromSwiftExpr(f, enumNames, structNames)}');
        if (f.type.isTypedData && !f.zeroCopy && _needsSyntheticLen(st, f.name)) {
          fromFields.add('      ${f.name}Length: _len_${f.name}');
        }
      }
      s.writeln(fromFields.join(',\n'));
      s.writeln('    )');
      s.writeln('  }');
      s.writeln();

      // fromSwiftPtr: heap-allocated shadow for NESTED struct fields — the
      // outer C shadow stores nested structs as UnsafeMutablePointer<_XxxC>?,
      // and _swiftFromSwiftExpr emits `_XxxC.fromSwiftPtr(s.field)` for them.
      s.writeln('  static func fromSwiftPtr(_ s: ${st.name}) -> UnsafeMutablePointer<_${st.name}C> {');
      s.writeln('    let p = UnsafeMutablePointer<_${st.name}C>.allocate(capacity: 1)');
      s.writeln('    p.initialize(to: fromSwift(s))');
      s.writeln('    return p');
      s.writeln('  }');
      s.writeln();

      // toSwift: convert C shadow → user-facing struct (for received params)
      s.writeln('  func toSwift() -> ${st.name} {');
      // Pre-compute any typed-data arrays
      for (final f in st.fields) {
        if (f.type.isTypedData && !f.zeroCopy) {
          s.writeln('    let _arr_${f.name} = ${f.name}.map { Data(bytes: \$0, count: Int(${_lenExprForField(st, f.name)})) } ?? Data()');
        }
      }
      s.writeln('    return ${st.name}(');
      final toFields = <String>[];
      for (final f in st.fields) {
        if (f.type.isTypedData && !f.zeroCopy) {
          toFields.add('      ${f.name}: _arr_${f.name}');
        } else {
          toFields.add('      ${f.name}: ${_swiftToSwiftExpr(f, enumNames, structNames)}');
        }
      }
      s.writeln(toFields.join(',\n'));
      s.writeln('    )');
      s.writeln('  }');
      s.writeln('}');
      s.writeln();
    }
    return s.toString();
  }

  /// C-ABI Swift type for a shadow struct field. Must exactly match the C struct.
  static String _dartTypeToSwiftCShadow(String t, [bool isZeroCopy = false, Set<String> enumNames = const {}, Set<String> structNames = const {}]) {
    final base = t.replaceFirst('?', '');
    if (enumNames.contains(base)) return 'Int32'; // C enum is int32_t
    if (structNames.contains(base)) return 'UnsafeMutablePointer<_${base}C>?';
    switch (base) {
      case 'int':
        return 'Int64';
      case 'double':
        return 'Double';
      case 'bool':
        return 'Int8'; // C int8_t
      case 'String':
        return 'UnsafeMutablePointer<CChar>?'; // C const char*
      case 'Uint8List':
        return 'UnsafeMutablePointer<UInt8>?';
      case 'Int8List':
        return 'UnsafeMutablePointer<Int8>?';
      case 'Int16List':
      case 'Uint16List':
        return 'UnsafeMutablePointer<Int16>?';
      case 'Int32List':
      case 'Uint32List':
        return 'UnsafeMutablePointer<Int32>?';
      case 'Float32List':
        return 'UnsafeMutablePointer<Float>?';
      case 'Float64List':
        return 'UnsafeMutablePointer<Double>?';
      case 'Int64List':
      case 'Uint64List':
        return 'UnsafeMutablePointer<Int64>?';
      default:
        return 'UnsafeMutableRawPointer?';
    }
  }

  /// Swift element type for a TypedData field (used to allocate temp buffers).
  static String _swiftTypedDataElement(String t) {
    switch (t.replaceFirst('?', '')) {
      case 'Uint8List':
        return 'UInt8';
      case 'Int8List':
        return 'Int8';
      case 'Int16List':
      case 'Uint16List':
        return 'Int16';
      case 'Int32List':
      case 'Uint32List':
        return 'Int32';
      case 'Float32List':
        return 'Float';
      case 'Float64List':
        return 'Double';
      case 'Int64List':
      case 'Uint64List':
        return 'Int64';
      default:
        return 'UInt8';
    }
  }

  /// Swift expression to convert a Swift struct field value → C shadow field.
  static String _swiftFromSwiftExpr(BridgeField f, Set<String> enumNames, Set<String> structNames) {
    final base = f.type.name.replaceFirst('?', '');
    if (f.type.isTypedData && !f.zeroCopy) return '_buf_${f.name}';
    if (f.type.isTypedData && f.zeroCopy) return 's.${f.name}';
    if (enumNames.contains(base)) return 'Int32(s.${f.name}.rawValue)';
    if (structNames.contains(base)) return '_${base}C.fromSwiftPtr(s.${f.name})'; // pointer alloc
    switch (base) {
      case 'String':
        return 'strdup(s.${f.name})';
      case 'bool':
        return 's.${f.name} ? 1 : 0';
      default:
        return 's.${f.name}';
    }
  }

  /// Swift expression to convert a C shadow field value → Swift struct field.
  static String _swiftToSwiftExpr(BridgeField f, Set<String> enumNames, Set<String> structNames) {
    final base = f.type.name.replaceFirst('?', '');
    if (enumNames.contains(base)) {
      // Force-unwrap: the C bridge always sends a valid rawValue; crashing on
      // corruption is intentional and avoids silent wrong-enum-case bugs.
      // ignore: unnecessary_brace_in_string_interps
      return '${base}(rawValue: Int64(${f.name}))!';
    }
    if (structNames.contains(base)) return '${f.name}!.pointee.toSwift()';
    switch (base) {
      case 'String':
        return '${f.name}.map { String(cString: \$0) } ?? ""';
      case 'bool':
        return '${f.name} != 0';
      default:
        return f.name;
    }
  }

  /// Returns the length field name for a typed-data field (in the C shadow struct).
  static String _lenExprForField(BridgeStruct st, String fieldName) {
    // Check for explicit companion first
    for (final c in ['${fieldName}Length', '${fieldName}Size']) {
      if (st.fields.any((f) => f.name == c && f.type.name == 'int')) return c;
    }
    const generic = ['length', 'size', 'stride', 'bytelength', 'bytelen', 'len'];
    for (final c in generic) {
      if (st.fields.any((f) => f.name == c && f.type.name == 'int')) return c;
    }
    return '${fieldName}Length'; // synthetic
  }

  static String _dartTypeToCType(String t, [Set<String> enumNames = const {}, Set<String> structNames = const {}]) {
    final base = t.replaceFirst('?', '');
    if (enumNames.contains(base)) return 'int32_t';
    // Nested @HybridStruct — passed by pointer so layout matches dart:ffi.
    if (structNames.contains(base)) return '$base*';
    switch (base) {
      case 'int':
        return 'int64_t';
      case 'double':
        return 'double';
      case 'bool':
        return 'int8_t';
      case 'String':
        return 'const char*';
      case 'Uint8List':
        return 'uint8_t*';
      case 'Int8List':
        return 'int8_t*';
      case 'Int16List':
        return 'int16_t*';
      case 'Int32List':
        return 'int32_t*';
      case 'Uint16List':
        return 'uint16_t*';
      case 'Uint32List':
        return 'uint32_t*';
      case 'Float32List':
        return 'float*';
      case 'Float64List':
        return 'double*';
      case 'Int64List':
        return 'int64_t*';
      case 'Uint64List':
        return 'uint64_t*';
      default:
        return 'void*';
    }
  }

  static String _dartTypeToKotlin(String t, bool isNullable, [bool isZeroCopy = false, Set<String> enumNames = const {}, Set<String> structNames = const {}]) {
    final base = t.replaceFirst('?', '');
    String kotlinType;
    // Enum fields are stored as Long in Kotlin data classes (matches JNI Long
    // bridging used for enum function params/returns).
    if (enumNames.contains(base)) {
      kotlinType = 'Long';
    } else if (structNames.contains(base)) {
      // Nested @HybridStruct — use its Kotlin data class by the same name.
      kotlinType = base;
    } else {
      switch (base) {
        case 'int':
          kotlinType = 'Long';
          break;
        case 'double':
          kotlinType = 'Double';
          break;
        case 'bool':
          kotlinType = 'Boolean';
          break;
        case 'String':
          kotlinType = 'String';
          break;
        case 'Uint8List':
        case 'Int8List':
        case 'Int16List':
        case 'Int32List':
        case 'Uint16List':
        case 'Uint32List':
        case 'Float32List':
        case 'Float64List':
        case 'Int64List':
        case 'Uint64List':
          kotlinType = isZeroCopy ? 'java.nio.ByteBuffer' : 'ByteArray';
          break;
        default:
          kotlinType = 'Any?';
          break;
      }
    }
    return isNullable ? '$kotlinType?' : kotlinType;
  }

  static String _dartTypeToSwift(String t, [bool isZeroCopy = false, Set<String> enumNames = const {}, Set<String> structNames = const {}]) {
    final base = t.replaceFirst('?', '');
    if (enumNames.contains(base)) return base;
    // Nested @HybridStruct — use its Swift struct by the same name.
    if (structNames.contains(base)) return base;
    switch (base) {
      case 'int':
        return 'Int64';
      case 'double':
        return 'Double';
      case 'bool':
        return 'Bool';
      case 'String':
        return 'String';
      case 'Uint8List':
      case 'Int8List':
        return isZeroCopy ? 'UnsafeMutablePointer<UInt8>?' : 'Data';
      case 'Int16List':
      case 'Uint16List':
        return isZeroCopy ? 'UnsafeMutablePointer<Int16>?' : '[Int16]';
      case 'Int32List':
      case 'Uint32List':
        return isZeroCopy ? 'UnsafeMutablePointer<Int32>?' : '[Int32]';
      case 'Float32List':
        return isZeroCopy ? 'UnsafeMutablePointer<Float>?' : '[Float]';
      case 'Float64List':
        return isZeroCopy ? 'UnsafeMutablePointer<Double>?' : '[Double]';
      case 'Int64List':
      case 'Uint64List':
        return isZeroCopy ? 'UnsafeMutablePointer<Int64>?' : '[Int64]';
      default:
        return 'Any?';
    }
  }

  /// Returns the Dart field type to use inside a dart:ffi Struct declaration.
  /// Nested @HybridStruct types are stored as [Pointer<NestedFfi>] since C
  /// passes them by pointer.  dart:ffi rules: int → `int`, double → `double`.
  static String _dartTypeToFfi(String t, [Set<String> enumNames = const {}, Set<String> structNames = const {}]) {
    final base = t.replaceFirst('?', '');
    if (enumNames.contains(base)) return 'int'; // stored as Int32 in C enum
    // Nested @HybridStruct — C passes a heap pointer to the nested struct.
    if (structNames.contains(base)) return 'Pointer<${base}Ffi>';
    switch (base) {
      case 'int':
        return 'int';
      case 'double':
        return 'double';
      case 'bool':
        return 'int'; // stored as Int8 in C; use int in Dart
      case 'String':
        return 'Pointer<Utf8>';
      case 'Uint8List':
        return 'Pointer<Uint8>';
      case 'Int8List':
        return 'Pointer<Int8>';
      case 'Int16List':
        return 'Pointer<Int16>';
      case 'Int32List':
        return 'Pointer<Int32>';
      case 'Uint16List':
        return 'Pointer<Uint16>';
      case 'Uint32List':
        return 'Pointer<Uint32>';
      case 'Float32List':
        return 'Pointer<Float>';
      case 'Float64List':
        return 'Pointer<Double>';
      case 'Int64List':
        return 'Pointer<Int64>';
      case 'Uint64List':
        return 'Pointer<Uint64>';
      default:
        return 'Pointer<Void>';
    }
  }

  /// Returns the @XxxSize() annotation class name for scalar numeric fields.
  /// Pointer fields (including nested struct pointers) need no annotation.
  static String _dartTypeToFfiAnnotation(String t, [Set<String> enumNames = const {}, Set<String> structNames = const {}]) {
    final base = t.replaceFirst('?', '');
    if (enumNames.contains(base)) return 'Int32'; // C enum is int32-sized
    if (structNames.contains(base)) return ''; // Pointer<NestedFfi> — no annotation
    switch (base) {
      case 'int':
        return 'Int64';
      case 'double':
        return 'Double';
      case 'bool':
        return 'Int8';
      default:
        return ''; // Pointer<X> — no size annotation needed
    }
  }
