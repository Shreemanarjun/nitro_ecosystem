import 'package:nitro_annotations/nitro_annotations.dart';
import '../../bridge_spec.dart';

class CppClassGenerator {
  static String generate(BridgeSpec spec) {
    if (spec.iosImpl != NativeImpl.cpp && spec.androidImpl != NativeImpl.cpp) {
      return '';
    }

    final s = StringBuffer();
    s.writeln('// Generated by Nitrogen Modules. Do not edit.');
    s.writeln('// Generated from: ${spec.sourceUri.split('/').last}');
    s.writeln('#pragma once');
    s.writeln();
    s.writeln('#include <stdint.h>');
    s.writeln('#include <string>');
    s.writeln('#include <vector>');
    s.writeln('#include <memory>');
    s.writeln('#include "${spec.lib.replaceAll('-', '_')}.bridge.g.h"');
    s.writeln();
    s.writeln('namespace nitro {');
    s.writeln();
    s.writeln('/**');
    s.writeln(' * C++ abstract base class for the ${spec.dartClassName} NitroModule.');
    s.writeln(' * Implement this class in your C++ code to provide the module logic.');
    s.writeln(' */');
    s.writeln('class Hybrid${spec.dartClassName} {');
    s.writeln('public:');
    s.writeln('    virtual ~Hybrid${spec.dartClassName}() = default;');
    s.writeln();

    for (final func in spec.functions) {
      final retType = _typeToCpp(func.returnType.name);
      final params = func.params.map((p) => '${_typeToCpp(p.type.name)} ${p.name}').join(', ');
      s.writeln('    virtual $retType ${func.dartName}($params) = 0;');
    }

    for (final prop in spec.properties) {
      final cppType = _typeToCpp(prop.type.name);
      if (prop.hasGetter) {
        s.writeln('    virtual $cppType get${_capitalize(prop.dartName)}() = 0;');
      }
      if (prop.hasSetter) {
        s.writeln('    virtual void set${_capitalize(prop.dartName)}($cppType value) = 0;');
      }
    }

    s.writeln('};');
    s.writeln();
    s.writeln('/**');
    s.writeln(' * Registry for the C++ ${spec.dartClassName} implementation.');
    s.writeln(' */');
    s.writeln('class ${spec.dartClassName}Registry {');
    s.writeln('public:');
    s.writeln('    static void registerImpl(std::shared_ptr<Hybrid${spec.dartClassName}> impl) {');
    s.writeln('        instance().impl_ = impl;');
    s.writeln('    }');
    s.writeln();
    s.writeln('    static std::shared_ptr<Hybrid${spec.dartClassName}> getImpl() {');
    s.writeln('        return instance().impl_;');
    s.writeln('    }');
    s.writeln();
    s.writeln('private:');
    s.writeln('    ${spec.dartClassName}Registry() = default;');
    s.writeln('    static ${spec.dartClassName}Registry& instance() {');
    s.writeln('        static ${spec.dartClassName}Registry instance;');
    s.writeln('        return instance;');
    s.writeln('    }');
    s.writeln('    std::shared_ptr<Hybrid${spec.dartClassName}> impl_;');
    s.writeln('};');
    s.writeln();
    s.writeln('} // namespace nitro');

    return s.toString();
  }

  static String _typeToCpp(String dartType) {
    switch (dartType.replaceFirst('?', '')) {
      case 'int':
        return 'int64_t';
      case 'double':
        return 'double';
      case 'bool':
        return 'bool';
      case 'String':
        return 'std::string';
      case 'void':
        return 'void';
      case 'Uint8List':
        return 'std::vector<uint8_t>';
      default:
        return 'void*';
    }
  }

  static String _capitalize(String s) => s.isEmpty ? s : s[0].toUpperCase() + s.substring(1);
}
