import 'dart:typed_data';
import 'package:nitro/nitro.dart';

part 'benchmark_cpp.g.dart';

/// Packed zero-copy struct passed as a raw C pointer across the FFI boundary.
///
/// The generator produces a [BenchmarkPointProxy] that **extends** this class
/// and overrides every getter to read lazily from native heap memory. Stream
/// and method consumers receive a plain [BenchmarkPoint]-typed variable, but
/// at runtime field access goes directly to the native struct — no copy.
///
/// ```dart
/// BenchmarkCpp.instance.dataStream.listen((point) {
///   // point is BenchmarkPointProxy at runtime.
///   // These reads touch native memory directly — zero allocation.
///   final x = point.x;
///   final y = point.y;
/// });
/// ```
@HybridStruct(packed: true)
class BenchmarkPoint {
  final double x;
  final double y;
  const BenchmarkPoint({required this.x, required this.y});
}

/// Packed zero-copy struct for the visual streaming stress test.
///
/// Emitted from C++ via [BenchmarkCpp.boxStream] at the maximum rate the
/// platform allows. The generated [BenchmarkBoxProxy] extends this class:
/// [color], [width], and [height] are lazy getters that read from the
/// native heap only when accessed, then the proxy is freed by its
/// `NativeFinalizer` when GC'd — no manual `malloc.free` required.
///
/// ```dart
/// BenchmarkCpp.instance.boxStream.listen((box) {
///   // box is BenchmarkBoxProxy at runtime — IS-A BenchmarkBox.
///   // Unused fields are never copied. Only what you access is read.
///   renderBox(Color(box.color), box.width, box.height);
/// });
/// ```
@HybridStruct(packed: true)
class BenchmarkBox {
  /// ARGB colour packed as a 32-bit integer.
  final int color;
  final double width;
  final double height;
  const BenchmarkBox({
    required this.color,
    required this.width,
    required this.height,
  });
}

/// Binary-encoded record returned by [BenchmarkCpp.computeStats].
///
/// Crosses the FFI boundary as a compact little-endian binary buffer
/// (not JSON). The wire format is:
/// ```
/// [4-byte payload length | int64 count | float64 meanUs | float64 minUs | float64 maxUs]
/// ```
/// All fields are decoded in a single pass with one allocation.
@HybridRecord()
class BenchmarkStats {
  /// Number of native iterations measured.
  final int count;

  /// Arithmetic mean call latency in microseconds.
  final double meanUs;

  /// Minimum observed call latency in microseconds.
  final double minUs;

  /// Maximum observed call latency in microseconds.
  final double maxUs;

  const BenchmarkStats({
    required this.count,
    required this.meanUs,
    required this.minUs,
    required this.maxUs,
  });
}

/// NativeImpl.cpp benchmark module — direct C++ virtual dispatch, no JNI or
/// Swift bridge overhead.
///
/// All bindings are generated by Nitrogen into `benchmark_cpp.g.dart`.
/// Primitive-return methods use `isLeaf: true` to skip the Dart VM safepoint
/// transition, reducing per-call overhead to the raw virtual-dispatch floor.
///
/// ## Usage
///
/// ```dart
/// import 'package:benchmark/benchmark.dart';
///
/// // Sync primitive — ~0.45 µs on M4 Pro (Leaf Call)
/// final result = BenchmarkCpp.instance.addFast(3.14, 2.71);
///
/// // Zero-copy struct round-trip
/// final scaled = BenchmarkCpp.instance.scalePoint(
///   BenchmarkPoint(x: 1.0, y: 2.0), 2.0);
///
/// // Async HybridRecord return — runs on a background thread
/// final stats = await BenchmarkCpp.instance.computeStats(50000);
/// print('mean: ${stats.meanUs} µs');
///
/// // Zero-copy struct stream — subscribe and read fields lazily
/// BenchmarkCpp.instance.boxStream.listen((box) {
///   // box.color / box.width / box.height are lazy native reads
///   renderBox(Color(box.color), box.width, box.height);
/// }); // NativeFinalizer frees each box when it goes out of scope
/// ```
@NitroModule(lib: 'benchmark_cpp', ios: NativeImpl.cpp, android: NativeImpl.cpp)
abstract class BenchmarkCpp extends HybridObject {
  /// Shared singleton. Loads the `benchmark_cpp` native library once and
  /// initialises proxy finalizers. Thread-safe after first access.
  static final BenchmarkCpp instance = _BenchmarkCppImpl();

  /// Baseline sync primitive. Measures direct C++ virtual-dispatch overhead.
  ///
  /// Generated with standard error-checking. Compare with [addFast] to
  /// isolate the cost of `NitroRuntime.checkError`.
  double add(double a, double b);

  /// Leaf-call sync primitive. Skips the Dart VM safepoint transition and
  /// error-check overhead — closest to the raw FFI floor.
  ///
  /// Binding uses `isLeaf: true`; the native function must never call back
  /// into Dart. Use for absolute-minimum-latency hot paths.
  double addFast(double a, double b);

  /// Sync string round-trip. Measures UTF-8 encoding + native heap allocation
  /// overhead. Returns `"Hello, <name>!"` from C++.
  String getGreeting(String name);

  /// Sync zero-copy struct param + return. Passes [point] as a raw C pointer
  /// and receives a new malloc'd struct back. Measures struct-passing overhead
  /// without any primitive-only fast path.
  BenchmarkPoint scalePoint(BenchmarkPoint point, double factor);

  /// Async returning a [BenchmarkStats] record. The C++ side runs [iterations]
  /// timed add-calls on a background thread, encodes the stats into a binary
  /// buffer, and posts the pointer to Dart via `Dart_PostCObject_DL`.
  ///
  /// Measures end-to-end latency of `@nitroAsync` including thread dispatch,
  /// binary encoding, and the `Future` machinery.
  @nitroAsync
  Future<BenchmarkStats> computeStats(int iterations);

  /// Continuous stream of zero-copy [BenchmarkPoint] structs.
  ///
  /// Each item is delivered as a [BenchmarkPointProxy] at runtime — a proxy
  /// that *extends* [BenchmarkPoint] with lazy getter overrides. The stream
  /// type remains `Stream<BenchmarkPoint>` for API compatibility; no consumer
  /// code changes are needed to benefit from zero-copy delivery.
  ///
  /// Items are dropped (not queued) if Dart falls behind — use the FPS
  /// counter in the stress dashboard to measure actual throughput.
  @NitroStream(backpressure: Backpressure.dropLatest)
  Stream<BenchmarkPoint> get dataStream;

  /// Stream of zero-copy [BenchmarkBox] structs for the visual stress test.
  ///
  /// Each item is a [BenchmarkBoxProxy] at runtime — extends [BenchmarkBox]
  /// with `@override` lazy getters reading directly from native memory:
  ///
  /// ```dart
  /// BenchmarkCpp.instance.boxStream.listen((box) {
  ///   // box is BenchmarkBoxProxy — IS-A BenchmarkBox.
  ///   // Field access is a native-heap read: zero allocation, zero copy.
  ///   final color = box.color;   // _native.ref.color
  ///   final w     = box.width;   // _native.ref.width
  ///   // Proxy is freed by NativeFinalizer when it goes out of scope.
  /// });
  /// ```
  @NitroStream(backpressure: Backpressure.dropLatest)
  Stream<BenchmarkBox> get boxStream;

  /// High-bandwidth test with memory-access checksum (4 KB stride walk).
  ///
  /// Pins the [buffer] for the duration of the call to allow zero-copy native
  /// access. The 100 ms pinning overhead dominates for large buffers — use
  /// [sendLargeBufferNoopFast] or [sendLargeBufferUnsafe] to isolate it.
  int sendLargeBufferFast(Uint8List buffer);

  /// Baseline throughput test — no memory access, pure dispatch overhead.
  ///
  /// Same pinning cost as [sendLargeBufferFast] but the native side does not
  /// touch the buffer, isolating dispatch + pinning from memory bandwidth.
  int sendLargeBufferNoop(Uint8List buffer);

  /// Ultra-fast baseline with `isLeaf: true` — Leaf-call pinning floor.
  int sendLargeBufferNoopFast(Uint8List buffer);

  /// Unsafe baseline — passes a raw `Pointer<Uint8>` without managed pinning.
  ///
  /// Bypasses the 100 ms `Uint8List` pinning cost entirely. The caller is
  /// responsible for keeping [ptr] valid for the duration of the call.
  /// Matches Raw FFI theoretical throughput on pinning-bound benchmarks.
  int sendLargeBufferUnsafe(Pointer<Uint8> ptr, int length);
}
